  na Tela Principal do Sistema. Nesta barra, a opção busca ao ser selecionada 
  aciona a tela inicial do Módulo Busca (formulário simplificado de busca) onde 
  se pode executar as seguintes ações:</span></font></p>
<p class=EstiloJustificado><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:9.0pt;
