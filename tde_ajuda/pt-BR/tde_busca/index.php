<p align=center style='text-align:center'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:14.0pt;font-family:Arial'>Sistema de Publicação Eletrônica de 
  Teses e Dissertações</span></font></p>
<p align=center style='text-align:center'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:14.0pt;font-family:Arial'>Módulo Busca</span></font></p>
<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:12.0pt;'>1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Introdução</span></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  Módulo Busca do sistema TEDE tem como função oferecer aos usuários da biblioteca 
  digital local, criada pelo sistema, a possibilidade de localizar as teses e 
  dissertações publicadas eletronicamente. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Neste 
  módulo, a busca pode ser realizada de forma simplificada ou de forma mais complexa. 
  O módulo oferece aos usuários opções de seleção de campos de busca e filtros 
  de resultados. As buscas podem ser efetuadas de forma simples ou avançada e 
  os resultados destas buscas podem ser visualizados de forma simplificada ou 
  detalhada. Os usuários terão acesso a uma <u>pasta de trabalhos</u> <u>virtual</u> 
  na qual poderão inserir as consultas realizadas. Esta pasta permite uma melhor 
  organização da pesquisa realizada e possibilita a otimização de consulta posterior. 
  O conteúdo desta parta poderá ser enviado para alguém como uma mensagem de <i>e-mail</i>.</span></font></p>
<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:12.0pt;'>2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:12.0pt'>Acesso e Operação do Módulo Busca</span></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
