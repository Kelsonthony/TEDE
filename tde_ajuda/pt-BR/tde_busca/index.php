<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:12.0pt;'>3<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opções de Operação do Módulo Busca</span></font></h1>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Busca Simples</font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>A 
  busca simples pode ser realizada por meio da página inicial do módulo de busca 
  (formulário simplificado de busca) ou por meio do <i>link</i> existente na página 
  de busca avançada.</span></font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Formulário simplificado de busca</font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  formulário simplificado de busca possui um <u>campo (campo de texto)</u> onde 
  o usuário deve digitar os termos (expressão de busca) que deseja pesquisar para 
  localizar as TDEs armazenadas.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>A 
  expressão de busca pode conter, caracteres, palavras e conjuntos de termos. 
  O usuário pode inserir múltiplos termos separados por espaços. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Um 
  conjunto de termos é delimitado por aspas duplas. Ex.: “<i>termo1 termo2 termo3 
  ... termoX</i>” e será considerado como um termo único indivisível.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>A 
  expressão de busca pode ainda conter operadores de conexão entre os termos da 
  pesquisa. Os operadores definidos são:</span></font></p>
<ol start=1 type=1>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>AND ou &amp;: </span></b><span
     style='font-family:Arial'>Operador “E” lógico. Funciona como uma operação 
    de intersecção de expressões.</span></font></li>
</ol>
<ol start=2 type=1>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>OR ou +: </span></b><span
     style='font-family:Arial'>Operador “OU” lógico. Funciona como uma operação 
    de união de expressões.</span></font></li>
</ol>
<ol start=3 type=1>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>NOT ou !: </span></b><span
     style='font-family:Arial'>Operador “Não” lógico. Funciona como uma operação 
    de subtração de expressões.</span></font></li>
</ol>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Os 
  operadores podem ser inseridos independente de caracteres maiúsculo ou minúsculo. 
  O operador padrão é o OR, desta forma termos inseridos sem operadores serão 
  conectados por este operador. Exemplo:</span></font></p>
<p align=center style='text-align:center'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><i><span style='font-family:Arial'>termo1 
  termo2 </span></i><span style='font-family:Arial'>“<i>termo3 termo4</i>”</span></font></p>
<p align=center style='text-align:center'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-family:Arial'>É interpretado como</span></font></p>
<p align=center style='text-align:center'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><i><span style='font-family:Arial'>termo1 
  </span></i><b><span style='font-family:Arial'>OR</span></b><i><span
style='font-family:Arial'> termo2 </span></i><b><span style='font-family:Arial'>OR</span></b><i><span
style='font-family:Arial'> </span></i><span
style='font-family:Arial'>“<i>termo3 termo4</i>”</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>A 
  busca é realizada de forma independente da acentuação e de caracteres maiúsculos 
  ou minúsculos. Os termos são pesquisados nos campos Autor, Título, Contribuidor, 
  Resumo e Assunto.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Abaixo 
  do campo de busca, existe um seletor que permite ao usuário escolher a quantidade 
  de registros a serem  exibidos em cada página do resultado.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Para 
  executar a busca tecle <u>Enter</u> no campo de texto (logo após a expressão 
  de busca) ou clique no botão de comando <u>Buscar</u> à direita do campo de 
  texto.</span></font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span><i>Link</i>s de acesso a outras páginas de operação de Busca</font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Na 
  página de busca simples, à direita da caixa do campo de texto existem dois <i>link</i>s 
  de acesso.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  <i>link</i> Pesquisa Avançada,  direciona para a página de Busca Avançada (item 
  3.2)</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  <i>link</i> Pasta de Trabalhos, direciona para a página da Pasta de Trabalhos 
  (item 4.2)</span></font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Listas de TDEs</font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Na 
  parte inferior do formulário de busca simples, o sistema mostra de um lado a 
  relação das últimas TDEs publicadas eletronicamente e do outro uma relação das 
  TDEs em preparação para serem publicadas.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>As 
  TDEs dos dois grupos estão listadas pelos títulos e autores. Para consultar 
  uma TDE já publicada basta clicar sobre o título da mesma. As TDEs em preparação 
  não estão disponíveis para busca.</span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Busca Avançada </font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  acesso para a página de Busca Avançada é realizado através do <i>link</i> existente 
  na página de Busca Simples (vide o item 3.1.2).</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>A 
  página de Busca Avançada contém os seguintes elementos:</span></font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.2.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Formulário para definição da Expressão de Busca Avançada</font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  Formulário contém uma caixa para expressões constituída por quatro campos de 
  texto com seletores de campo de pesquisa, compondo as quatro expressões de busca. 
  As três últimas expressões contêm conectores lógicos, que se relacionam com 
  a expressão anterior. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Não 
  é obrigatório preencher todas as expressões, mas <u>deve-se iniciar sempre pela 
  primeira</u>. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Uma 
  expressão só é processada se estiver com os campos de texto de seleção de campo 
  de pesquisa e conectores preenchidos e selecionados, c<u>om exceção da primeira</u>, 
  na qual não existe campo de conector.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  usuário deve preencher o campo de texto com os termos que deseja pesquisar. 
  Os termos para pesquisa são iguais aos da Busca Simples. Podem ser caracteres, 
  palavras e conjuntos de termos.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  usuário pode inserir múltiplos termos separados por espaços. Um conjunto de 
  termos é delimitado por aspas duplas (“<i>termo1 termo2 termo3 ... termoX</i>”), 
  e será considerado como um termo único indivisível. Também é possível utilizar 
  os mesmos operadores de conexão descritos na Busca Simples. (vide item 3.1)</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Para 
  cada campo de texto informado, o usuário deve selecionar em que campo de pesquisa 
  os termos de busca deverão ser pesquisados. As opções de termos são: <u>Autor</u>, 
  <u>Título</u>, <u>Contribuidor</u>, <u>Assunto</u>, <u>Resumo</u> e <u>Todos</u>. 
  A opção <u>Todos</u> pesquisa os termos em todos os campos de pesquisa anteriores.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Nos 
  últimos três campos de expressão, o usuário deverá escolher conectores lógicos 
  que farão a ligação entre as expressões imediatamente anteriores. As opções 
  de conectores são:</span></font></p>
<ol start=1 type=1>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>AND</span></b><span
     style='font-family:Arial'> – Conector operativo “E” de lógica. Funciona como 
    conector de intersecção de expressões;</span></font></li>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>OR</span></b><span
     style='font-family:Arial'> – Conector operativo “OU” de lógica. Funciona 
    como conector de união de expressões;</span></font></li>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>NOT</span></b><span
     style='font-family:Arial'> – Conector operativo “NÃO” de lógica. Funciona 
    como uma subtração de expressões.</span></font></li>
</ol>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>A 
  ordem de prioridade de operação são os conectores AND e NOT, ambos no mesmo 
  nível de prioridade, seguido pelo conector OR, um nível abaixo. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Como 
  exemplo temos as seguintes expressões:</span></font></p>
<p align=center style='text-align:center'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><u><span
style='font-family:Arial'>A</span></u><b><span style='font-family:Arial'>  OR   
  </span></b><u><span style='font-family:Arial'>B</span></u><b><span
style='font-family:Arial'>   NOT   </span></b><u><span
style='font-family:Arial'>C</span></u><span
style='font-family:Arial'>     será interpretada como     <u>A</u><b>   OR  </b> (B<b> 
    NOT   </b>C)</span></font></p>
<p align=center style='text-align:center'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-family:Arial'>O resultado da expressão   <u>B</u>  –   (menos) o resultado 
  da expressão   <u>C</u>   é unido ao </span></font></p>
<p align=center style='text-align:center'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-family:Arial'>resultado da expressão A.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>E 
  a expressão:</span></font></p>
<p align=center style='text-align:center;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><u><span
style='font-family:Arial'>A</span></u><b><span style='font-family:Arial'>   AND  
   </span></b><u><span
style='font-family:Arial'>B</span></u><span
style='font-family:Arial'>  <b> NOT  </b><u>C</u>    será interpretada como   
  (A<b>  AND   </b>B)<b>   NOT   </b><u>C</u></span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'> O 
  resultado da expressão     <u>A</u>     intersecção resultado da expressão      <u>B</u> 
  </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>   
                               é subtraído do resultado da expressão <u>C</u>.</span></font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.2.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Formulário para filtros de resultados</font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  formulário para filtros de resultados se localiza abaixo do formulário de expressões 
  de busca. Os filtros permitem ao usuário realizar uma seleção das TDEs que resultarão 
  da busca executada pelas expressões definidas anteriormente. Este formulário 
  apresenta quatro campos de filtros:</span></font></p>
<ul type=disc>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>Grau da Tese/Dissertação: </span></b><span style='     font-family:Arial'>Permite 
    filtrar os resultados em relação ao grau de titulação da TDE. Qualquer titulação, 
    Mestrado, Doutorado ou Outros.</span></font></li>
</ul>
<ul type=disc>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>Idioma da Tese: </span></b><span
     style='font-family:Arial'>Permite filtrar os resultados em relação à língua 
    em que  a TDE foi publicada. Contém as seguintes opções: Qualquer Idioma, 
    Português, Inglês, Francês, Alemão e Espanhol.</span></font></li>
</ul>
<ul type=disc>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>Data de Defesa: </span></b><span
     style='font-family:Arial'>Permite filtrar os resultados em relação à data 
    de defesa da TDE. O usuário pode definir limitadores de datas, sendo que não 
    é obrigatório o preenchimento de ambos os campos. O formato de preenchimento 
    das datas tem como separadores aceitos para dia, mês e ano, a ‘/ ’ (barra: 
    dd/mm/aaaa) e o ‘-’ (hífen: dd-mm-aaaa).</span></font></li>
</ul>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-family:Arial'>Por exemplo, o usuário pode definir que deseja somente 
  buscar TDEs com data de defesa a partir de uma data  X  até a data atual. Desta 
  forma não é necessário preencher o segundo campo de limite final, assim como 
  não será necessário preencher o primeiro campo se o usuário desejar buscar somente 
  TDEs com datas de defesas desde a data mais antiga até uma data  Y.</span></font></p>
<ul type=disc>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>Registros por Página: </span></b><span
     style='font-family:Arial'>Este campo não necessariamente se caracteriza como 
    filtro de resultados, mas permite ao usuário definir quantos registros serão 
    exibidos por página de resultado.</span></font></li>
</ul>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>A 
  busca é executada, teclando ENTER nos campos de texto ou clicando no botão de 
  comando <u>Buscar</u>. O botão de comando <u>Retornar Valores</u> retorna o 
  formulário para os valores anteriormente preenchidos.</span></font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.2.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span><i>Link</i>s de acesso a outras páginas de operação de Busca</font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Semelhante 
  à Busca Simples, a página de Busca avançada apresenta dois <i>link</i>s de acesso 
  localizados à direita do formulário de busca. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  <i>link</i> Pesquisa Simples direciona para a página de Busca Simples (Descrita 
  anteriormente no item 4).</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  <i>link</i> Pasta de Trabalhos direciona para a página da Pasta de Trabalhos 
  (Descrita posteriormente no item 7).</span></font></p>
<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:12.0pt;'>4<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Lista de Resultados</span></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Qualquer 
  busca tem seus resultados exibidos em páginas de resultados. A lista de resultados 
  apresenta um cabeçalho que informa:</span></font></p>
<ul type=disc>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:
     Arial'>quantidade total de registros;</span></font></li>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:
     Arial'>listagem das páginas – lista as páginas, indicando a atual em negrito 
    e disponibilizando <i>link</i>s para as demais;</span></font></li>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:
     Arial'>índices dos registros apresentados na página atual;</span></font></li>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:
     Arial'>total de páginas.</span></font></li>
</ul>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  cabeçalho é seguido pela lista. Cada registro do resultado é apresentado em 
  uma caixa individual e pode ser exibido de dois modos: simplificado e detalhado. 
  Os termos definidos durante a busca, quando encontrados, são marcados em negrito 
  nos dados exibidos.</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-family:Arial'>O modo de exibição inicial é o simplificado, o qual 
  mostra somente os campos: Autor, Título e Data de Defesa. Caso uma Busca Avançada 
  tenha sido realizada, além destes três campos, serão exibidos também os campos 
  que foram selecionados na busca (Por exemplo Resumo, Assunto, Contribuidor).</span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Exibição Detalhada</font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Para 
  alternar o modo de exibição do registro de <u>simplificado</u> para <u>detalhado</u>, 
  o usuário deve marcar a opção <u>Detalhar</u> correspondente e clicar no botão 
  de comando Processar. Para retornar ao modo de exibição simplificado, basta 
  desmarcar a opção Detalhar e clicar no botão de comando <u>Processar</u>.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  modo de exibição detalhado apresenta os seguintes campos do registro: Autor, 
  Título, Data de defesa, Resumo, Titulação, Assunto, Arquivos e <i>E-mail</i>. 
  </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Os 
  campos que não possuem dados não serão exibidos. O campo Arquivos apresenta 
  os documentos e arquivos, referentes à TDEs, disponíveis ao usuário para consulta. 
  O campo <i>E-mail</i> só será exibido se houver algum endereço autorizado para 
  o usuário.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>É 
  possível detalhar mais de um registro, neste caso o usuário precisa marcar a 
  opção <u>Detalhar</u> nos registros que desejar e então clicar qualquer botão 
  de comando <u>Processar</u>. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>A 
  exibição detalhada só fica ativa na página atual, caso o usuário acesse outra 
  página e retorne à página de resultados, os registros voltarão a serem exibidos 
  de forma simplificada.</span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Armazenamento das buscas realizadas</font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  usuário pode guardar os registros de busca na Pasta de Trabalhos para uma consulta 
  posterior. Para isto, deve marcar nos registros que deseja guardar a opção <u>Incluir 
  na pasta</u>  e clicar em qualquer botão de comando <u>Processar</u>.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Os 
  registros incluídos são exibidos com um ícone da pasta no canto inferior direito, 
  o qual também é um <i>link</i> para a página Pasta de Trabalhos. Para remover 
  os registros da pasta, o usuário deve marcar a opção <u>Remover da pasta</u> 
  (opção que substitui a opção Incluir na pasta nos registros incluídos na pasta) 
  dos registros desejados e clicar em qualquer botão de comando  <u>Processar</u>.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>No 
  final da listagem, existe um rodapé que repete informações sobre os índices 
  dos registros exibidos e a lista de páginas. Logo abaixo é apresentado o formulário 
  de busca que foi utilizado, preenchido com os termos definidos pelo usuário. 
  O botão de comando <u>Retornar Valores</u> do formulário de busca avançada retorna 
  para os valores definidos na busca atual.</span></font></p>
