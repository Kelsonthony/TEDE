<p class=EstiloJustificado><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:9.0pt;
font-family:Symbol;'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-family:Arial'>Acessar a pasta de trabalhos;</span></font></p>
<p class=EstiloJustificado><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:9.0pt;
font-family:Symbol;'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
