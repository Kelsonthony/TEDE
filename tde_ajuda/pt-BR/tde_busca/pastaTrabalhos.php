<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:12.0pt;'>5<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Pasta de Trabalhos</span></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>A 
  página Pasta de Trabalhos fornece ao usuário uma pasta virtual, na qual ele 
  pode inserir as TDEs pesquisadas, consultá-as e enviá-las em forma de <i>e-mail</i>. 
  O acesso a esta página é realizado através dos <i>link</i>s existentes nos formulários 
  de busca (vide itens 3.1.2 e 3.2.3) e no ícone correspondente encontrado nos 
  registros incluídos na “pasta”. A duração do conteúdo da pasta corresponde a 
  uma sessão de navegação, desta forma cada vez que o usuário abre um navegador 
  e realiza uma nova consulta na página de busca, ele estará iniciando uma nova 
  pasta.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Os 
  registros são exibidos e removidos da pasta da mesma forma como na lista de 
  resultados (Descrita anteriormente no item 4.2). Neste caso o número de registros 
  por página está fixado em cinco.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Além 
  da lista de registros, esta página oferece quatro <i>link</i>s:</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-family:Symbol;'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><b><span
style='font-family:Arial'>Apaga pasta</span></b><span
style='font-family:Arial'> - Limpa a pasta, retirando todos os registros;</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-family:Symbol;'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><b><span
style='font-family:Arial'>Retornar</span></b><span
style='font-family:Arial'> - Retorna para a página anterior da qual o usuário 
  acessou a Pasta de Trabalhos;</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-family:Symbol;'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><b><span
style='font-family:Arial'>Pesquisar</span></b><span
style='font-family:Arial'> - Retorna diretamente para a página inicial de busca, 
  mantendo o último tipo de busca (Simples ou Avançada);</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-family:Symbol;'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><b><span
style='font-family:Arial'>Envie por <i>e-mail</i></span></b><span style='font-family:Arial'> 
  - Permite ao usuário enviar o conteúdo da pasta de trabalhos através de um <i>e-mail</i>. 
  </span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif">5.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Envio por <i>e-mail</i></font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Para 
  enviar o conteúdo da Pasta de Trabalhos via <i>e-mail</i>, o usuário deve acessar 
  o <i>link</i> <u>Envie por <i>e-mail</i></u> existente na página. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Será 
  apresentado um formulário para montar o <i>e-mail</i> com os seguintes campos:</span></font></p>
<ul type=disc>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>Para (<i>e-mail</i>) -</span></b><span
     style='font-family:Arial'> O endereço destinatário (Obrigatório);</span></font></li>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>De (<i>e-mail</i>) -</span></b><span
     style='font-family:Arial'> O endereço de resposta;</span></font></li>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>Assunto -</span></b><span
     style='font-family:Arial'> Descrição do assunto;</span></font></li>
  <li style='text-align:justify;     '><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
     style='font-family:Arial'>Texto -</span></b><span
     style='font-family:Arial'> Informações adicionais que o usuário queira enviar 
    junto com o conteúdo.</span></font></li>
</ul>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><i><span style='font-family:Arial'>Obs: 
  O endereço de origem do e-mail é fixo e corresponde ao sistema de busca TEDE. 
  O conteúdo é enviado em formato HTML.</span></i></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Abaixo 
  dos botões do formulário, será mostrado o conteúdo da Pasta de Trabalhos, da 
  forma como será enviado no <i>e-mail</i>. Os registros são exibidos de forma 
  detalhada, com exceção do campo Arquivos que nesta operação é suprimido. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>O 
  usuário deve preencher os campos e clicar no botão de comando  <u>Enviar</u>. 
  Caso o usuário queira reiniciar o preenchimento, ele pode limpar os campos através 
  do botão de comando <u>Limpar</u>.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Após 
  clicar no botão de comando <u>Enviar</u> o <i>e-mail</i> é processado e uma 
  página de resposta é apresentada. Esta página mostra o <i>e-mail</i> montado 
  (incluindo os cabeçalhos) e informa sobre o sucesso do envio. No caso de envio 
  bem-sucedido um <i>link</i> será apresentado para retornar à Pasta de Trabalhos. 
  Em caso contrário, o <i>link</i> apresentado retornará para a página do formulário 
  de <i>e-mail</i>, para uma nova tentativa.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Arial'>Para 
  retornar à Pasta de Trabalhos a partir da página com o formulário de <i>e-mail</i>, 
  o usuário pode clicar no <i>link</i> Retorna para Pasta de Trabalhos, disponível 
  no topo da página.</span></font></p>
