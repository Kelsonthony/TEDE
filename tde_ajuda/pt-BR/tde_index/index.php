  <h1><font face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc61261431"><span
style='font-size:13.0pt'>2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
    </span>Orientações Gerais</span></a></font></h1>
  <h2><font face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc61261432">2.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
    </span>Objetivo do Sistema</a></font></h2>
  <p style='text-align:justify'><font face="Verdana, Arial, Helvetica, sans-serif">O Sistema de Publicação Eletrônica de Teses e 
    Dissertações (Sistema TEDE) foi desenvolvido pelo Instituto Brasileiro de 
    Informação em Ciência e Tecnologia (IBICT), com o objetivo de apoiar as Instituições 
    de Ensino Superior (IES) que ainda não implementaram bibliotecas digitais 
    de teses e dissertações. O sistema foi desenvolvido de forma que as IES poderão 
    com ele efetuar a publicação eletrônica de teses e dissertações correntes 
    e gerar suas bibliotecas digitais locais. A adoção do Sistema TEDE e da camada 
    do protocolo OAI-PMH, que o acompanha, permitirá às IES utilizar os padrões 
    e tecnologias necessárias para a sua imediata integração à Biblioteca Digital 
    Brasileira de Teses e Dissertações (BDTD) e a exposição dos metadados destas 
    teses na Networked Digital Library of Theses and Dissertations Union Catalog 
    (NDLTD). Ao mesmo tempo, o Sistema TEDE foi desenvolvido para ser compatível 
    com o Open Public Access Catalog (OPACs). Desta forma, as IES poderão efetuar 
    a publicação eletrônica de teses e dissertações pelo Sistema TEDE e, posteriormente, 
    por meio de um módulo específico desse sistema, exportar estes metadados para 
    o <span style='color:black'>OPAC</span>.</font></p>
  <p style='text-align:justify'><font face="Verdana, Arial, Helvetica, sans-serif">O Sistema TEDE utiliza o padrão de metadados MTD-BR 
    (compatível com os padrões ETD-MS (específico para teses e dissertações) e 
    Dublin Core ( usado para a descrição de qualquer tipo de objeto digital). 
  </font></p>
  <h2><font face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc61261433">2.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
    </span>Características do Sistema</a></font></h2>
  <p style='text-align:justify'><font face="Verdana, Arial, Helvetica, sans-serif">Este sistema é uma aplicação desenvolvida em ambiente 
    de banco de dados, implementado em HTML e voltado para WEB. Ele apresenta 
    uma interface amigável com um sistema de navegação orientado por <i>menus</i>. 
    Possui, <u>barra de ferramentas</u> para a navegação nos módulos de operação 
    do sistema; possui ajuda automática na opção <u>Ajuda</u> existente nos <i>menus</i> 
    das telas de execução das <u>opções de operação</u> de cada módulo; possui 
    <u>comandos de operação</u> para a execução das ações demandadas ao longo 
    do processo de publicação eletrônica; possui <u>Caixas de Escolha</u> para 
    a seleção de dados constantes em tabelas disponibilizadas pelo sistema e  
    <u>botões de comandos</u> para a confirmação das ações executadas.</font></p>
  <h2><font face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc61261434">2.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
    </span>Visão Geral do Sistema</a></font></h2>
  <p style='text-align:justify'><font face="Verdana, Arial, Helvetica, sans-serif">O Sistema TEDE permite a automação dos procedimentos 
    de publicação eletrônica de teses e dissertações (TDEs), envolvendo de forma 
    cooperativa os atores principais do processo de publicação de uma tese ou 
    dissertação, tais como os autores, os programas de pós-graduação e as bibliotecas.</font></p>
  <p style='text-align:justify'><font face="Verdana, Arial, Helvetica, sans-serif">O Sistema TEDE foi construído em módulos que possuem 
    funções específicas para cada etapa do processo de publicação eletrônica de 
    uma tese ou dissertação (TDE). Na atual versão é composto pelos seguintes 
    módulos:</font></p>
  </font>
  <ul type=disc>
    <li style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Administração;</font></li>
    <li style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Pós-graduação;</font></li>
    <li style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Autor;</font></li>
    <li style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Biblioteca;</font></li>
    <li style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Busca.</font></li>
  </ul>
  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema exige que estes módulos 
    sejam executados na seqüência aqui apresentada, para permitir a cada operador 
    do sistema envolvido no processo de publicação desempenhar o seu papel e para 
    que o processo seja concluído e viabilize a disponibilização da TDE para o 
    acesso público. Com exceção dos módulos <u>Autor</u> e <u>Busca</u>,  os módulos 
    <u>Administração</u>, <u>Pós-Graduação</u> e <u>Biblioteca</u> devem ter operadores 
    habilitados pela instituição para executá-los.</font></p>
  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Inicialmente o sistema deve ser 
    customizado para a instituição receptora do sistema por meio do Módulo Administração. 
    A customização envolve o cadastro dos dados específicos da instituição receptora, 
     da biblioteca virtual, dos operadores do sistema, bem como de suas unidades, 
    tais como programas de pós-graduação e bibliotecas depositárias. A customização 
    envolve também a escolha de interfaces para o sistema local, a inclusão de 
    logomarcas escolhidas pela instituição para caracterizá-la e a identificação 
    dos servidores de arquivos e de <i>e-mail</i> geral utilizados pelo Sistema 
    TEDE.</font></p>
  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Somente após a customização, o 
    sistema estará preparado para que seja iniciado o processo de publicação das 
    teses ou dissertações, o que ocorre no Módulo Pós-graduação. Neste módulo, 
    são incluídos alguns metadados descritivos do autor e da TDE, bem como os 
    membros da banca examinadora e o papel que estes contribuidores exerceram 
    na construção da tese ou dissertação. Neste módulo, também, é criada uma conta 
    para o autor. Somente após a criação desta conta, o autor terá acesso ao sistema.</font></p>
  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao receber a permissão para entrar 
    no sistema, o autor deve inserir ou alterar os dados de sua TDE e anexar os 
    arquivos (de texto, imagem, animação etc.) que a compõem. Ao final deste processo, 
    o autor submete a TDE à pós-graduação para revisão. Durante o procedimento 
    de revisão, ao serem detectados erros nos dados inseridos pelo autor ou nos 
    arquivos da sua TDE, a pós-graduação manterá um diálogo com o autor por meio 
    da opção de operação <u>Observação</u>. Este   diálogo é administrado pelo 
    sistema, que emite   <i>e-mail</i>s automáticos tanto ao autor, quanto aos 
    operadores habilitados da pós-graduação, informando as ações realizadas. Este 
    fluxo entre autor e pós-graduação ocorre, até que esta considere os dados 
    e arquivos da TDE corretos, para  enviar a tese ou dissertação à biblioteca 
    para a catalogação.</font></p>
  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">No Módulo Biblioteca são inseridos 
    os metadados bibliográficos para a catalogação e feita uma revisão geral dos 
    metadados inseridos na TDE, tanto pelo autor, quanto pela pós-graduação. Esta 
    ação pode dar origem a um novo diálogo entre o Módulo Biblioteca e o Módulo 
    Pós-Graduação e entre este e o Módulo Autor. Este diálogo também será realizado 
    por meio da opção Observação e administrado por meio de <i>e-mail</i> emitido 
    automaticamente pelo sistema aos operadores habilitados destes módulos e ao 
    autor.</font></p>
  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Somente quando este fluxo for encerrado 
    e o registro da TDE considerado correto é que a mesma será disponibilizada 
    para o acesso público. A figura 1, a seguir, é uma representação deste fluxo.</font></p>
