  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Após a publicação, a TDE fica disponível 
    para acesso, por meio do Módulo de Busca do sistema, na biblioteca virtual 
    local, que se integrará  à BDTD Nacional por meio da camada de protocolo distribuída 
    com o sistema.</font></p>
  <h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc61261435">2.4<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
    </span>Instalação e Conexão com o  Sistema TEDE</a></font></h2>
  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O Sistema TEDE, para ser instalado, 
    necessita de Sistema Operacional baseado na plataforma Unix/Lunix, requisitos 
    de <i>software</i> e de <i>hardware</i> adequados ao sistema (utilizar o Manual 
    de Instalação de Ambiente Computacional para a Implantação do Sistema de Publicação 
    Eletrônica de Teses e Dissertações<span style='font-family:"Courier New"'>-</span>TEDE<i>)</i>. 
    Após a sua instalação, o sistema estará disponível para ser utilizado em ambiente 
    WEB e poderá ser acessado por meio da URL criada para o sistema quando da 
    sua instalação (EX:http://nomedoservidor.domínio.br/diretoriotede/).</font></p>
  <h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc61261436"><span style='font-size:13.0pt'>3<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
    </span>Procedimentos Gerais  utilizadas na Operação dos Módulos</span></a></font></h1>
  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O Sistema TEDE foi projetado em 
    módulos com o objetivo de possibilitar a participação cooperativa dos atores 
    envolvidos no processo de publicação eletrônica de uma tese ou dissertação. 
    Cada módulo tem uma função  específica e, para cumpri-la, faz-se necessária 
    a execução de um conjunto de ações envolvendo procedimentos diferenciados. 
    A execução correta destes procedimentos irá permitir a compreensão do manuseio 
    do sistema e do fluxo de navegação entre os módulos. Estes procedimentos são 
    executados por meio das telas disponibilizadas em cada módulo. Cada módulo 
    possui um conjunto de telas, acionadas por meio das opções de operação, localizadas 
    no <i>menu</i> da tela principal de cada módulo e que devem ser acionadas 
    na ordem seqüencial de apresentação no <i>menu</i>. Estas opções solicitam 
    ações específicas que são executadas por meio dos seus comandos de operação. 
    Estes comandos acionam novas telas onde estas ações são efetivadas e finalizadas, 
    utilizando-se os botões de comando existentes em cada tela. Após a finalização 
    das ações, o sistema disponibiliza telas com mensagens de sucesso, de erro 
    ou de solicitação de uma nova ação.</font></p>
  <h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc61261437">3.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
    </span>Telas do Sistema e ferramentas para a sua operação</a></font></h2>
  <p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao longo do processo de operação, 
    o sistema disponibiliza, para cada módulo, telas específicas para a execução 
    das ações que possibilitam a publicação de uma tese ou dissertação. Estas 
    telas foram projetadas com recursos visuais que permitem aos operadores do 
    sistema a execução destas ações com maior agilidade e facilidade. As telas 
    são subdivididas nos seguintes grupos:</font></p>
  <p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">&#8730; Tela Principal do Sistema 
    – Esta tela é apresentada logo após a conexão com o sistema. Ela é identificada 
    pela barra de <i>menu</i>, localizada no topo da tela, por meio da qual se 
    faz a conexão com o módulo que vai ser operado e por uma descrição sucinta 
    do sistema;</font></p>
  <p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> &#8730; Tela de Acesso ao Módulo 
    – Esta tela é disponibilizada após a entrada no módulo  e solicita a chave 
    de identificação do operador habilitado para operá-lo. Esta chave de identificação 
    é liberada somente para os operadores indicados pela instituição receptora 
    do sistema e é criada no Módulo Administração, por solicitação da instituição. 
    </font></p>
  <p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">&#8730; Tela Principal do Módulo 
    – Esta tela é disponibilizada após a identificação do operador habilitado 
    para operar o módulo e é identificada por uma janela central que contém, geralmente, 
    no início, instruções preliminares. Posteriormente, ela contém o resultado 
    das ações executadas com seus respectivos botões de comando para a realização 
    de alterações ou exclusões destas ações. Outro detalhe de identificação é 
    o <i><u>menu</u></i>, localizado na lateral esquerda da tela. Neste <i>menu</i> 
    estão relacionadas, na seqüência de execução, as opões de operação do módulo. 
    </font></p>
  <p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">&#8730; Telas de Operação – Estas 
    telas são disponibilizadas após a seleção de uma <u>opção de operação</u><i> 
    </i>ou de um <u>comando de operação</u>. Estes comandos estão localizados 
    nos <i>menu</i>s laterais destas telas, abaixo de cada opção, ou nas janelas 
    centrais das mesmas onde se encontram os campos, e quando necessárias algumas 
    instruções, para a execução das ações visualizar, inserir, alterar e excluir 
    dados ou arquivos, solicitadas por meio destes comandos.</font></p>
  <p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> &#8730; Telas de Mensagens – 
    Estas telas são disponibilizadas após a execução de uma ação e mostram uma 
    mensagem de sucesso da ação executada, uma mensagem de erro ou uma solicitação 
    de confirmação da ação.</font></p>
  <p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">&#8730; Opção de Operação – É 
    uma rotina disponibilizada pelo sistema no <i>menu</i> existente na tela principal 
    de cada módulo, por meio da qual é acionado um conjunto de instruções que 
    devem ser executadas na seqüência correta, para a finalização da ação solicitada. 
    Cada opção de operação é nomeada de acordo com o nome e o padrão do metadado 
    que vai inserir.</font></p>
  <p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">&#8730; Comandos de Operação – 
    São ferramentas utilizadas pelo sistema para a função de visualizar, inserir, 
    alterar ou excluir dados durante o processo de  execução da opção de operação 
    acionada. Estes comandos estão localizados nos <i>menu</i>s de cada tela logo 
    abaixo de cada opção, ou nas janelas centrais das telas, sucedidos pelos respectivos 
    botões de comando.</font></p>
