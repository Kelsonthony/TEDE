<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123001">2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Acesso e Operação do Módulo Autor</a></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>O acesso ao Módulo Autor é feito por meio de um <i>link</i> 
  enviado ao <i>e-mail</i> do autor, após a criação de sua Conta de Acesso ao 
  Sistema TEDE, pela pós-graduação. O autor, ao recebê-lo, deve primeiramente 
  seguir os procedimentos indicados neste <i>e-mail</i> para desbloquear a sua 
  conta e ter acesso à senha que permite a sua entrada no Sistema TEDE. O acesso 
  ao sistema é feito por meio da tela de acesso ao módulos ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 1</span> ). Nesta 
  tela, o sistema solicita  uma chave de identificação – <i>e-mail </i>da conta 
  – que deve ser confirmada por uma senha. Esta chave de identificação permite 
  ao autor entrar no sistema para complementar os seus dados e inserir os arquivos 
  que compõem a TDE. </span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>A 
  tela principal do Módulo Autor é apresentada no formato de um formulário. Este 
  formulário apresenta, na lateral esquerda da tela, um <i>menu</i> com os <u>comandos 
  de operação</u> adequados à execução das ações solicitadas na opção de operação 
  que está sendo executada; uma janela central com os metadados descritivos  da 
  TDE, inseridos pelo programa de pós-graduação e com os campos para o autor inserir 
  os metadados, cujo preenchimento é de sua responsabilidade ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 4</span> ).</span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>A 
  operação do módulo é feita por meio de um conjunto de <u>opções de operação</u>, 
  com funções específicas,  que são acionadas ao longo da execução. Estas opções 
  não estão, inicialmente, agrupadas na tela principal do módulo. Elas vão sendo 
  apresentadas no <i>menu</i> desta tela, com os seus respectivos comandos de 
  operação, após a execução de ações obrigatórias para a continuidade do processo 
  de disponibilização eletrônica da tese ou dissertação.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Ao longo do processo de operação do módulo, se o autor sentir 
  alguma dificuldade (não esclarecida neste manual) ou detectar algum erro em 
  sua tese ou dissertação, deverá entrar em contato com a pós-graduação para receber 
  os devidos esclarecimentos. Este contato poderá ser feito por telefone, ou por 
  <i>e-mail</i>. Neste caso, o autor deverá utilizar o módulo Contato, cujo acesso 
  é feito pela <u>barra de <i>menu</i></u>, existente na parte superior das telas 
  do Sistema TEDE.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>O autor deverá estar sempre alerta às mensagens enviadas ao 
  seu <i>e-mail</i> pelo programa de pós-graduação a que está vinculado.</span></font></p>
<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123002">3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opções de Operação do Módulo Autor</a></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>As opções de operação do módulo autor, ao serem ativadas, permitem 
  ao autor iniciar o processo de submissão de sua tese ou dissertação. No decorrer 
  da operação do módulo, o autor deve executar as opções de operação que vão sendo 
  apresentadas no <i>menu</i> da tela principal (após a execução de uma ação), 
  obedecendo a ordem de apresentação.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Inicialmente, o sistema apresenta, na tela principal do módulo, 
  na lateral esquerda da tela, um <i>menu</i> com os comandos de operação adequados 
  à execução das ações <u>Visualizar</u> e <u>Alterar</u> TDE e, na janela central 
  da tela, apresenta os metadados descritivos da TDE, inseridos pelo programa 
  de pós-graduação, e os campos para o autor inserir os metadados, cujo preenchimento 
  é de sua responsabilidade. Após a efetivação desta ação, o sistema disponibiliza, 
  nesta tela, um <i>menu</i> específico para a inclusão dos direitos de acesso 
  aos arquivos da TDE. Concluída esta ação, o sistema disponibiliza, nesta tela, 
  um <i>menu</i> para a operação das demais opções do módulo.</span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>Estas 
  opções são executadas por meio dos comandos de operação posicionados abaixo 
  de cada opção. As opções de operação <u>Direitos</u>, <u>Tipo</u> e <u>Arquivo</u> 
  estão assinaladas por um asterisco vermelho, para indicar que têm preenchimento 
  obrigatório. Em algumas opções, o comando de operação <u>Inserir</u> é ressaltado 
  na cor vermelha, um alerta para indicar que estes comandos devem ser executados 
  para que a TDE possa ser submetida à publicação eletrônica com todos os metadados 
  solicitados pelo sistema. As opções de operação do Módulo Autor estão discriminadas 
  a seguir.</span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123003">3.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>TDE</u></a></font></h2>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>Esta 
  opção é utilizada para permitir ao autor visualizar os metadados descritivos 
  da TDE, inseridos pela pós-graduação, e inserir e alterar os metadados cujo 
  preenchimento é de sua inteira responsabilidade. Em um segundo momento, se houver 
  solicitação por parte da pós-graduação de alteração dos metadados ou arquivos 
  da TDE, o sistema disponibiliza, nesta opção, um comando de operação denominado 
  <u>Responder Observação</u>, que permite a troca de observações entre a pós-graduação 
  e o autor.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A tela principal da opção é imediatamente disponibilizada após 
  a  entrada no sistema. Esta tela tem o formato de um formulário e apresenta 
  o <i>menu</i> com os comandos de operação <u>Visualizar </u>e <u>Alterar</u>, 
  alguns metadados preenchidos pela pós-graduação e os campos de preenchimentos 
  dos metadados de responsabilidade do autor vazios. Estes campos são identificados 
  pelo nome do campo e pelo espaço para preenchimento, circundado por uma caixa 
  retangular  ( <span style='font-size:12.0pt;font-family:"Times New Roman"'>figura 
  4</span> ).</span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123012">4.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como operar a Opção <u>TDE Submissão</u></a></font></h2>
<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123013">4.2.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Inserir</u> – Opção TDE Submissão</a></font></h3>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>Nesta 
  opção de operação, o autor insere os metadados descritivos de sua tese ou dissertação, 
  que são de sua responsabilidade, para dar continuidade ao processo de submissão 
  eletrônica da mesma.</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Os campos de 
  preenchimento obrigatório, ao deixarem de ser preenchidos, são solicitados automaticamente 
  pelo sistema, que emite uma mensagem de erro e ressalta estes campos em vermelho. 
  Inclua, nos campos discriminados a seguir, os metadados solicitados:</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>digite, no campo <u>Citação</u>, preenchimento 
  obrigatório,  a forma pela qual o autor deseja ser citado, (Ex.: Bernardes, 
  Pedro S.). Este campo deverá conter, no máximo, 100 caracteres alfanuméricos, 
  também podem ser usados os seguintes caracteres especiais: &amp;, ( ),  /,  
  \ ,  - ,  _  e  * ;</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:10.0pt;font-family:Symbol'>·<span
