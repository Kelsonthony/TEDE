<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123009">3.7<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Finalização</u></a></font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Esta opção é utilizada para a finalização, pelo autor, do processo 
  de inclusão dos metadados e arquivos que compõem a sua TDE. Após cumprir todas 
  as exigências do sistema, o autor utiliza esta opção de operação para finalizar 
  o processo de submissão e enviar a TDE à pós-graduação para a revisão final. 
  </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Esta opção de operação é executada por meio do comando de operação 
  <u>Finalizar Submissão</u> ( ressaltado na cor vermelha), acionado pelo sistema 
  na tela principal do módulo após a inclusão dos arquivos na TDE, ou, acionada 
  por meio do comando <u>Visualizar</u> da opção de operação TDE deste módulo.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A pós-graduação poderá </span><span style='font-size:11.0pt;
font-family:Symbol'>-</span><span style='font-size:11.0pt;font-family:Arial'> 
  caso encontre, neste momento, alguma inconsistência na TDE recebida do autor 
  </span><span
style='font-size:11.0pt;font-family:Symbol'>-</span><span style='font-size:
11.0pt;font-family:Arial'> devolvê-la para as correções devidas.</span></font></p>
