  </span></span><span style='font-size:11.0pt;font-family:Arial'>Clique no botão 
  de comando <u>Alterar</u>, existente no canto direito inferior da tela.</span></font></p>
<p class=MsoHeader style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Observe que o 
  sistema disponibiliza uma nova tela com a mensagem Operação Concluída e com 
  a alteração realizada, para conferência.</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Finalize a operação 
  para a execução da opção de operação Afiliação conforme descrito a seguir (seção 
  4.4), ou selecione o comando <u>Sair</u> para sair do sistema.</span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
style='font-size:11.0pt;font-family:Arial'>Atenção</span></b><span
style='font-size:11.0pt;font-family:Arial'>: após a inserção dos Direitos na TDE, 
  o sistema disponibiliza uma tela com um novo <i>menu</i>. Neste <i>menu,</i> 
  está relacionado um conjunto de opções de operação, assinaladas por asteriscos 
  vermelhos, indicando que os metadados solicitados nestas opções são obrigatórios. 
  O comando de operação <u>Inserir</u> de algumas opções está ressaltado na cor 
  vermelha, um alerta para indicar que a informação requerida não foi incluída. 
  Após a inclusão, o sistema retira o alerta para indicar que a ação foi executada. 
  Todas as ações referentes a estas opções de operação são executadas no <i>menu</i> 
  da mesma tela. </span></font></p>
