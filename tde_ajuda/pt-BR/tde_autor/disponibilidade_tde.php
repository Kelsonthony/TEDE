<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123017"><span style='font-size:
12.0pt'>4.3.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:12.0pt'>Comando de Operação <u>Alterar</u> 
  – Opção Direitos</span></a></font></h3>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Selecione, no 
  <i>menu</i> desta tela, o comando de operação <u>Alterar,</u> abaixo da opção 
  Direito.</span></font></p>
<p style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Observe que o 
  sistema disponibiliza a tela com a Caixa de Escolha para a alteração.</span></font></p>
<p class=MsoHeader style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Faça as alterações 
  necessárias (mudança na escolha do item selecionado).</span></font></p>
<p class=MsoHeader style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
