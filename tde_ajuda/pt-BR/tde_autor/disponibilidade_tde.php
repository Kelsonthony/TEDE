<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123004">3.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Direitos</u></a></font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Esta opção é utilizada para permitir ao autor a inclusão ou 
  a alteração na TDE dos <u>Direitos de Acesso</u>, isto é, as condições de distribuição, 
  reprodução e utilização do conteúdo da tese ou dissertação para a publicação 
  eletrônica.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Esta informação é obrigatória para o sistema. Por esta razão, 
  o comando de operação <u>Inserir</u>, existente no <i>menu</i> das telas abaixo 
  da opção Direitos, aparecerá em alerta na cor vermelha, enquanto o metadado 
  não for inserido na TDE.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A tela principal da opção é identificada pelo <i>menu</i> – 
  lateral esquerda da tela – que contém os comandos de operação para a execução 
  das ações concernentes  à opção e pela janela central da tela que contém uma 
  <u>Caixa de Escolha</u>, exclusiva para o <u>Direito de Acesso</u>, onde o autor 
  deverá selecionar um dos itens numerados ( <span style='font-size:12.0pt;font-family:
"Times New Roman"'>figura 7</span> ).</span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Ref56414501">4.3<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como operar a Opção </a> <u>Direitos</u></font></h2>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:11.0pt;font-family:Marlett'>4</span><span style='font-size:
11.0pt;font-family:Arial'>Atenção para o <i>menu</i> (lateral esquerda) apresentado 
  nas telas resultantes das ações de inclusão ou alteração dos metadados na opção 
  TDE Submissão ( <span style='font-size:12.0pt;font-family:
"Times New Roman"'>figura 5</span> ). Este <i>menu</i> apresenta a opção Direitos 
  assinalada por um asterisco vermelho, indicando que a execução desta opção é 
  obrigatória. O comando de operação <u>Inserir</u> está ressaltado na cor vermelha, 
  para indicar que a informação precisa ser incluída. Após a inclusão, o sistema 
  desliga o comando inserir e disponibiliza somente o comando de operação <u>Alterar</u>, 
  para o caso de possíveis alterações.</span></font></p>
<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123016">4.3.1<span
