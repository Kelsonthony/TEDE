  cadastrada e associada à TDE do autor;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·</span><span style='font-size:
11.0pt;font-family:Arial'>   finalize a operação e selecione, nesta tela, o comando 
