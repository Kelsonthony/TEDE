<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>Os campos de preenchimento obrigatório, 
  ao deixarem de ser preenchidos, são solicitados automaticamente pelo sistema, 
  que emite uma mensagem de erro e ressalta estes campos em vermelho. Inclua, 
  nos campos discriminados a seguir, os dados para o cadastramento da Agência 
  de Fomento do autor:</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>digite, no campo <u>Nome</u>, preenchimento 
  obrigatório, em ordem direta, o nome completo da Agência de Fomento do autor. 
  Este campo deverá conter, no máximo, 100 caracteres alfanuméricos, e também 
  podem ser usados os seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  - 
  ,  _  e  * ;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>digite, no campo <u>Sigla</u>, preenchimento 
  obrigatório, a sigla oficial da Agência de Fomento do autor. Este campo deverá 
  conter, no máximo, 30 caracteres alfanuméricos, e também podem  ser usados os 
  seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  - ,  _  e  * ;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>selecione, no campo <u>País</u>, preenchimento 
  obrigatório, na tabela existente ao lado do campo, o país da Agência de Fomento 
  do autor;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>selecione, no campo <u>UF</u>, preenchimento 
  obrigatório, na tabela existente ao lado do campo, a unidade da federação onde 
  está sediada a Agência de Fomento do autor; </span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>digite, no  campo <u>CNPJ</u>, preenchimento 
  obrigatório, o CNPJ da Agência de Fomento do autor. Este campo deverá ser preenchido 
  apenas com a seqüência de algarismos, com  no máximo, 14 caracteres numéricos. 
  Ex.: 33654831002341;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
