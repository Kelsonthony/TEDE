<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123006">3.4<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Agência de Fomento</u></a></font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Esta opção é utilizada para incluir e excluir na TDE a Agência 
  de Fomento do autor, ou cadastrá-la quando a instituição não constar no Cadastro 
  de Agências de Fomento, apresentado no momento da seleção para a inclusão.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>No <i>menu</i> da tela principal do módulo, o comando de operação 
  <u>Inserir</u>, existente abaixo da opção Agencia de Fomento, vem ressaltado 
  na cor vermelha, alertando que este metadado deve ser incluído na TDE. Este 
  alerta permanecerá na cor vermelha, se o metadado não for inserido.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A tela da opção é identificadas na janela central como Agência 
  de Fomento e contém os campos apropriados para a seleção e inclusão, ou cadastramento 
  da Agência de Fomento do autor. O sistema apresenta, no campo apropriado para 
  a seleção, uma lista com as principais agencias de fomento do país. </span></font></p>
