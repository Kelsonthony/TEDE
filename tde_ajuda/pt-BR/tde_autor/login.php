<p align=center style='text-align:center'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b><span
style='font-size:14.0pt;font-family:Arial'>Módulo Autor</span></b></font></p>
<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123000">1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Introdução</a></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>O Módulo Autor tem a função de permitir aos autores de teses 
  e dissertações, vinculados aos programas de pós-graduação da Instituição Receptora 
  do Sistema TEDE, iniciar o processo de submissão de sua tese ou dissertação 
  para a publicação eletrônica, com a inclusão dos metadados descritivos e arquivos 
  de suas teses ou dissertações. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Os procedimentos para a submissão da TDE são executados pelo 
  autor por meio das <u>opções de operação</u> disponibilizadas pelo sistema no 
  Módulo Autor. Estas opções possuem telas apropriadas à execução das ações imprescindíveis 
  (inclusão ou correção dos metadados da TDE; inclusão ou alteração dos direitos 
  de acesso à TDE; inclusão, alteração ou exclusão da afiliação do autor; inclusão 
  ou exclusão da agência de fomento do autor; inclusão ou exclusão dos tipos de 
  meios utilizados na composição da tese ou dissertação; inclusão dos arquivos 
  de texto, imagem, animação etc. que compõem sua tese ou dissertação), para que 
  o autor possa cumprir as formalidades exigidas pelo sistema para finalizar a 
  sua TDE para a  revisão final pela pós-graduação.</span></font></p>
<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123001">2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Acesso e Operação do Módulo Autor</a></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>O acesso ao Módulo Autor é feito por meio de um <i>link</i> 
  enviado ao <i>e-mail</i> do autor, após a criação de sua Conta de Acesso ao 
  Sistema TEDE, pela pós-graduação. O autor, ao recebê-lo, deve primeiramente 
  seguir os procedimentos indicados neste <i>e-mail</i> para desbloquear a sua 
  conta e ter acesso à senha que permite a sua entrada no Sistema TEDE. O acesso 
  ao sistema é feito por meio da tela de acesso ao módulos ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 1</span> ). Nesta 
  tela, o sistema solicita  uma chave de identificação – <i>e-mail </i>da conta 
  – que deve ser confirmada por uma senha. Esta chave de identificação permite 
  ao autor entrar no sistema para complementar os seus dados e inserir os arquivos 
  que compõem a TDE. </span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>A 
  tela principal do Módulo Autor é apresentada no formato de um formulário. Este 
  formulário apresenta, na lateral esquerda da tela, um <i>menu</i> com os <u>comandos 
  de operação</u> adequados à execução das ações solicitadas na opção de operação 
  que está sendo executada; uma janela central com os metadados descritivos  da 
  TDE, inseridos pelo programa de pós-graduação e com os campos para o autor inserir 
  os metadados, cujo preenchimento é de sua responsabilidade ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 4</span> ).</span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>A 
  operação do módulo é feita por meio de um conjunto de <u>opções de operação</u>, 
  com funções específicas,  que são acionadas ao longo da execução. Estas opções 
  não estão, inicialmente, agrupadas na tela principal do módulo. Elas vão sendo 
  apresentadas no <i>menu</i> desta tela, com os seus respectivos comandos de 
  operação, após a execução de ações obrigatórias para a continuidade do processo 
  de disponibilização eletrônica da tese ou dissertação.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Ao longo do processo de operação do módulo, se o autor sentir 
  alguma dificuldade (não esclarecida neste manual) ou detectar algum erro em 
  sua tese ou dissertação, deverá entrar em contato com a pós-graduação para receber 
  os devidos esclarecimentos. Este contato poderá ser feito por telefone, ou por 
  <i>e-mail</i>. Neste caso, o autor deverá utilizar o módulo Contato, cujo acesso 
  é feito pela <u>barra de <i>menu</i></u>, existente na parte superior das telas 
  do Sistema TEDE.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>O autor deverá estar sempre alerta às mensagens enviadas ao 
  seu <i>e-mail</i> pelo programa de pós-graduação a que está vinculado.</span></font></p>
