<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123005">3.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Afiliação</u></a></font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Esta opção é utilizada para inserir, alterar e excluir a Instituição 
  de Afiliação do autor na TDE, ou cadastrá-la, quando a instituição não constar 
  no Cadastro de Afiliação, apresentado no momento da seleção para a inclusão. 
  No <i>menu</i> da tela principal do módulo, o comando de operação Inserir, existente 
  abaixo da opção Afiliação, poderá vir ressaltado na cor vermelha, alertando 
  que este metadado deve ser inserido na TDE. Este alerta permanecerá em vermelho 
  se o metadado não for inserido.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A tela da opção é identificada, na janela central, como Afiliação 
  e contém os campos apropriados para a seleção e inclusão, ou cadastramento da 
  Instituição de Afiliação do autor, ou seja, a instituição com a qual o autor 
  tem um  vínculo empregatício. </span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Ref56415859">4.4<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como operar a Opção <u>Afiliação</u></a></font></h2>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>Na 
  operação da opção Afiliação, o autor deve ficar atento ao <i>menu</i> da tela 
  apresentada após a operação da opção Direitos, quando podem ocorrer as situações, 
  descritas a seguir,  em que o autor deverá agir de forma diferenciada para associar 
  sua Afiliação à TDE: </span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><i><span style='font-size:11.0pt;font-family:Arial'>Situação 
  1-</span></i><span style='font-size:11.0pt;font-family:Arial'> No <i>menu</i> 
  desta tela, o comando de operação Inserir, da opção Afiliação, é apresentado 
  ao autor com um alerta em vermelho, indicando que a informação deve ser inserida 
  na TDE. Neste caso, o autor pode selecionar sua Afiliação na lista de instituições 
  disponibilizada pelo sistema e associá-la à TDE ou cadastrar a sua Instituição 
  de Afiliação, quando esta não constar no cadastro disponibilizado para a seleção. 
  Em ambos os casos, o autor deve proceder conforme as instruções descritas na 
  seção Comando de Operação Inserir  desta opção (seção  4.4.1). </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><i><span style='font-size:11.0pt;
font-family:Arial'>Situação 2 -</span></i><span style='font-size:11.0pt;
font-family:Arial'> No <i>menu</i> desta tela, abaixo da opção Afiliação, aparecem 
  os comandos de operação <u>Alterar</u> e <u>Excluir</u>, sem o alerta em vermelho. 
  Quando isto ocorre, significa que a Afiliação do autor foi associada à TDE pela 
  pós-graduação. Neste caso, o autor, se for afiliado a uma instituição, deve 
  seguir as instruções descritas na seção Comando de Operação Alterar  desta opção 
  (seção   4.4.2 ).</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><i><span style='font-size:11.0pt;
font-family:Arial'>Situação 3 -</span></i><span style='font-size:11.0pt;
font-family:Arial'> O autor não tem afiliação, e a pós-graduação a inseriu em 
  sua TDE. Neste caso, o autor  deve proceder conforme as instruções descritas 
  na seção Comando de Operação Excluir desta opção (seção  4.4.3).</span></font></p>
