<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123007">3.5<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Tipo</u></a></font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Esta opção é utilizada para incluir e excluir os tipos de meios 
  (natureza ou gênero da composição da tese ou dissertação) utilizados na composição 
  da tese ou dissertação. Esta informação é <u>obrigatória</u> para o sistema. 
  No <i>menu</i> da tela principal do módulo, o comando de operação <u>Inserir</u>, 
  existente abaixo da opção Tipo, vem ressaltado na cor vermelha, alertando que 
  este metadado precisa ser incluído na TDE. Este alerta permanecerá na cor vermelha, 
  enquanto o metadado não for inserido.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A tela principal da opção é identificada, na janela central, 
  como Tipo e contém uma Caixa de Escolha para a seleção e complementação, se 
  for o caso, dos tipos de meios  utilizados na composição da TDE ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 19</span> ).</span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Ref56420605">4.6<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como operar a Opção <u>Tipo</u></a></font></h2>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>Na 
  operação desta opção, o autor deve incluir ou excluir os tipos de meios utilizados 
  na composição da sua tese ou dissertação. O sistema disponibiliza, na tela apresentada, 
  um tipo de meio que é padrão para todas as TDEs (Electronic Thesis and Dissertation), 
  e o autor deverá escolher, se for o caso, os demais tipos de meios que fizeram 
  parte da composição de sua tese ou dissertação.</span></font></p>
