<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123008">3.6<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Arquivo</u></a></font></h2>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Esta opção é utilizada para inserir e excluir o arquivos ou 
  o conjunto de arquivos que compõem a TDE. O sistema exige a inclusão de, no 
  mínimo, um arquivo no formato PDF.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A tela principal da opção é identificada, na janela central, 
  como Arquivo e contém um campo para a busca e inclusão  dos arquivos que compõem 
  a TDE; um campo para descrever a legenda de cada arquivo e uma caixa  de escolha 
  exclusiva para o Direito de Acesso, onde o autor deverá selecionar, se for o 
  caso,  um dos itens numerados (<span style='font-size:12.0pt;
font-family:"Times New Roman"'>figura 22</span>).  </span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Ref56421488">4.7<span
