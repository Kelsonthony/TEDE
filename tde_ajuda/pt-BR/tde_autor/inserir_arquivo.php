  </span></span><span style='font-size:11.0pt;font-family:Arial'>Selecione<i>,</i> 
  no <i>menu</i> da tela apresentada, o comando de operação <u>Inserir</u>, ressaltado 
  na cor vermelha, existente abaixo da opção de operação Arquivo (figura 21).</span></font></p>
