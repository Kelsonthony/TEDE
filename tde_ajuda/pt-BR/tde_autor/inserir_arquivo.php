<p class=EstiloJustificado><span style='font-size:9.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Clique no botão 
  de comando <u>Procurar</u>, ao lado da <u>Caixa de Escolha</u> existente nesta 
  tela.</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>O sistema disponibiliza 
  uma tela (caixa de diálogo) do Navegador (do computador utilizado) para a localização 
  do arquivo a ser inserido na TDE (figura 23).</span></p>
<img width=588 height=324
    src="imagens/image025.jpg"><br clear=ALL>
<p class=MsoCaption>Figura 23 – Módulo Autor, opção Arquivo - tela do navegador 
  do sistema para localização do Arquivo</p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Localize<i> </i>(no 
  computador) a pasta ou local onde se encontra o arquivo a ser inserido na TDE 
  do autor (um arquivo de cada vez).</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Se o arquivo 
  tiver extensão PDF, bloqueie o arquivo conforme as instruções a seguir.</span></p>
<p style='text-align:justify'><b><span style='font-size:11.0pt;
font-family:Arial'>Atenção</span></b></p>
<p class=MsoBodyText><span style='font-size:11.0pt;font-family:Arial'>O sistema 
  TEDE só aceita o arquivo de texto da tese ou dissertação no formato PDF. O autor 
  deverá bloquear o arquivo antes de inseri-lo na TDE. Para fazer este bloqueio, 
  obedeça à instrução a seguir:</span></p>
<p style='text-align:justify'><span style='font-size:11.0pt;
font-family:Arial'>O procedimento para o bloqueio de um arquivo é simples. O autor 
  deve incluir uma senha de acesso aos arquivos PDF, utilizando o programa <i>Adobe 
  Acrobat Writer</i> (Adobe Acrobat 5.0 com o <i>plugin</i> Writer) da seguinte 
  forma:</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>abrir o arquivo 
  pelo programa <i>Adobe Acrobat Writer</i> (Adobe Acrobat 5.0 com o <i>plugin 
  </i>Writer);</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>no <i>menu</i> 
  do <i>Acrobat Writer</i>:</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>selecionar <u>arquivo</u>.</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>em <u>arquivo</u>, 
  selecionar a opção <u>segurança do documento</u>, que abre uma janela com o 
  título <u>segurança do documento</u>;</span></p>
<p class=MsoBodyTextIndent style='text-align:justify;
'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>no item <u>opções 
  de segurança</u>, desta janela, selecionar <u>segurança padrão do Acrobat</u>, 
  que abre uma outra janela com o título <u>segurança padrão</u>, contendo dois 
  títulos: <u>especificar senha</u> e <u>permissões</u>, com itens para seleção;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>no título <u>especificar 
  senha</u> selecionar o segundo item, <u>senha necessária para alterar as permissões 
  e as senhas</u>. Digitar, no item <u>senha mestra</u>, a senha escolhida;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>No título <u>permissões</u>, 
  selecionar:</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>no item <u>nível 
  de criptografia</u>, selecionar <u>“RCA4 de 128 bits (Acrobat 5.0)”</u>; </span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>desativar o item 
  <u>permitir cópia e extração de conteúdo</u>;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>no item <u>alterações 
  permitidas</u>, selecionar a expressão <u>nenhuma;</u></span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>confirmar apertando 
  o botão OK;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>salvar e fechar 
  o documento;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>após salvar e 
  fechar o documento, clique duas vezes sobre o arquivo fechado, para que ele 
  seja inserido na TDE;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>O Sistema TEDE 
  mostra a referência do arquivo inserido, na <u>Caixa de Escolha</u> (existente 
  na tela onde foi realizada a procura do arquivo) (figura 24);</span></p>
<img width=585 height=348
    src="imagens/image026.gif"><br clear=ALL>
<p class=MsoCaption>Figura 24 – Módulo Autor,  opção  Arquivo - Referência do 
  Arquivo localizado para inclusão na TDE</p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>Digite, na caixa existente ao lado 
  do campo <u>Legenda</u>, uma legenda para o arquivo inserido. Esta legenda deve 
  ser um pequeno texto explicativo que identifique o conteúdo do arquivo inserido. 
  Ex.: “Arquivo de Texto, Capítulos I, II e III da tese, ou, “Arquivo completo 
  da TDE, em PDF”; ou “Arquivo de Fotos”;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>Selecione, nesta tela, no campo <u>Disponibilidade 
  do Arquivo</u>, o tipo de disponibilidade (Direito) dada pelo autor para o acesso 
  ao arquivo inserido. O autor pode escolher, para cada arquivo inserido, um dos 
  tipos de disponibilização apresentados na <u>Caixa de Escolha</u>;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>Clique no botão de comando <u>Inserir</u>, 
  existente no canto direito inferior da tela;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>Observe que o sistema disponibiliza 
  a tela principal da opção com a mensagem <u>Operação Concluída</u>, e os dados 
  do arquivo inserido, para conferência, e com a <u>Caixa de Escolha</u>, para 
  a inclusão, se for o caso, dos demais arquivos que irão compor a TDE (figura 
  25). Ou, se o arquivo não tiver sido protegido, uma mensagem de erro informando 
  que o arquivo não está protegido e solicitando que esta ação seja realizada.</span></p>
<img width=600 height=324
    src="imagens/image027.jpg"><br clear=ALL>
<p class=MsoCaption>Figura 25 – Módulo Autor, opção  Arquivo - Mensagem de confirmação 
  de inclusão do arquivo na TDE</p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Confira os dados 
  do arquivo inserido.</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Se OK, adote 
  o seguinte procedimento:</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>selecione<i>,</i> 
  no <i>menu</i> desta tela, o comando de operação, em vermelho, <u>Finalizar 
  Submissão</u> da opção de operação Finalização (seção 4.8 ).</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>Se não OK, execute 
  as instruções descritas na seção Comando de Operação Excluir, a seguir.</span></p>
<h3 style='text-align:justify'><a name="_Toc69123034">4.7.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Excluir</u> – Opção Arquivo</a></h3>
<p class=EstiloArialJustificadoPrimeiralinha064cm><span style='font-size:10.0pt;
font-family:Symbol'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Para substituir ou excluir um arquivo associado à TDE no momento 
  em que  está fazendo inclusões, adote os seguintes procedimentos:</p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>clique no botão de comando <u>Excluir</u>, 
  existente nesta tela ao lado do nome do arquivo que se deseja excluir;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>observe que o sistema disponibiliza 
  uma tela com a mensagem <u>Operação Concluída</u>, para conferência.   </span></p>
<p class=EstiloArialJustificadoPrimeiralinha064cm><span style='font-size:10.0pt;
font-family:Symbol'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Para substituir ou excluir um arquivo associado à TDE, e a ação 
  de excluir vier a ser executada posteriormente, adote os seguintes procedimentos:</p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>Selecione,<i> </i>no <i>menu</i> da 
  tela principal de operação do módulo, o comando de operação <u>Excluir</u>, 
  existente abaixo da opção de operação Arquivo;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>  observe que o sistema disponibiliza 
  a tela para a exclusão do arquivo inserido pelo autor;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>clique<i> </i>no botão de comando, 
  existente nesta tela, abaixo do comando <u>Excluir</u>, correspondente ao arquivo 
  a ser excluído ou substituído;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span><span style='font-size:11.0pt;font-family:Arial'>observe que o 
  sistema disponibiliza uma tela com a mensagem <u>Operação Concluída</u> e com 
  a <u>Caixa de Escolha</u>, para a inclusão do novo arquivo. Se for este o caso, 
  repetir o processo para a inclusão;</span></p>
<p style='text-align:justify;'><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span><span
style='font-size:11.0pt;font-family:Arial'>   finalize a operação e selecione, 
  no <i>menu</i> desta tela, o comando de operação <u>Finalizar Submissão</u>, 
  em vermelho, abaixo da opção de operação Finalização (seção 4.8).</span></p>
