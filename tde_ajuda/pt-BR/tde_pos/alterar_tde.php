  <em>Figura 62 – Módulo Pós-graduação Opção TDE (Revisão) - Mensagem confirmação 
  alteração dados da TDE</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt'>Ao 
      finalizar a operação, proceda à operação da opção Contribuidor (se&ccedil;&atilde;o 
      4.8.2), ou selecione, no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> 
      principal</u> e volte à tela principal do módulo.</span></font></div>
  </li>
</ul>
