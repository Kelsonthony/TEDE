  Grupo Revisão</font></h2>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123252"></a><a
name="_Toc60478972"></a><a name="_Toc60478510">4.8.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>TDE (Revisão)</u></a></font></h3>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Entre no Módulo 
    Pós-graduação.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Identifique, 
    pelo nome do autor, na janela central da tela principal do módulo, a TDE a 
    ser revisada (figura 60).</font></li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=604 height=243 src="imagens/image063.jpg"><br clear=ALL>
  <em>    Figura 60 – Módulo Pós-graduação, opção TDE (Revisão) – Identificação 
  da TDE a ser revisada</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione 
      o comando de operação  <u>Revisar</u> (ressaltado na cor vermelha) existente 
      na janela central desta tela, abaixo do campo <i><u>Status</u></i><u> da 
      TDE</u>, correspondente ao nome do autor cuja TDE irá ser revisada.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt'>O 
      sistema disponibiliza uma tela com os metadados descritivos da TDE, para 
      revisão e alteração, com as opções do Grupo Revisão que devem ser acionadas 
      no <i>menu</i> de operação da tela, para  a execução do processo de revisão 
      da TDE (figura 61).</span></font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=595 height=317 src="imagens/image064.jpg"><br clear=ALL>
  <a name="_Ref63499114"><em>Figura </em></a><em>61 – Módulo Pós-graduação, opção 
  TDE (Revisão) – Revisão dos metadados inseridos na TDE</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt'>Use 
      a Barra de Rolagem (lateral direita da tela) para fazer a revisão detalhada 
      das informações inseridas em cada campo. Os campos possíveis de serem alterados, 
      pelo operador habilitado, estão circundados por uma caixa retangular.</span></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt'>Se 
      existir alguma correção a ser feita nos metadados inseridos pelo autor, 
      adote o seguinte procedimento:</span></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>U</span>tilize as instruções 
      descritas na opção Observação (seção 4.8.5),  para informar ao autor as 
      alterações a serem realizadas nos metadados de sua TDE.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      não houver correções a serem feitas, adote os seguintes procedimentos:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Alterar</u>, existente no canto direito inferior 
      da tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u> 
      e com os metadados alterados pelo operador habilitado (figura 62).</font></div>
  </li>
</ul>
