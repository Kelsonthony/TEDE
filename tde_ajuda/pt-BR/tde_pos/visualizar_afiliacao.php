<h2><a name="_Toc60478483"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.4<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como operar a Opção <u>Afiliação</u></font></a></h2>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta 
  opção é utilizada para a criação, para o sistema, de um cadastro de instituições 
  de afiliação (instituições às quais os autores e/ou contribuidores estão subordinados 
  por vínculo empregatício), com o objetivo de facilitar a sua associação ao autor 
  e ao contribuidor da TDE e também  a alteração ou exclusão dos dados da instituição. 
  Este cadastro também vai sendo formado no momento em que se inclui a afiliação 
  do autor e do contribuidor.</font></p>
<div align="justify"> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione,<i> 
      </i>no <i>menu</i>  da tela principal do módulo ( Figura 3 ),  a  opção 
      de operação  Afiliação;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema 
      disponibiliza a tela principal da opção identificada como Afiliação, pela 
      referência <u>Nenhum registro cadastrado</u>, ou pela relação das instituições 
      de afiliação já cadastradas (figura 22).</font></li>
  </ul>
