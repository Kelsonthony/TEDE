<h3><a name="_Toc60478451"><font size="4">3.3.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>TDE (Revisão)</u></font></a></h3>
<p align="justify">Esta opção permite à pós-graduação alterar alguns metadados 
  da TDE. Os campos possíveis de alteração estão circundados por uma caixa retangular. 
  Quando a pós-graduação constatar erros nos demais metadados da TDE, apresentados 
  fora deste padrão, deverá retornar a TDE ao autor com uma observação sobre a 
  alteração a ser efetivada. Esta comunicação deverá ser feita utilizando-se a 
  opção <u>Observação</u> existente no <i>menu</i> da tela da tela disponibilizada 
  pelo sistema para a efetivação da revisão da TDE.</p>
