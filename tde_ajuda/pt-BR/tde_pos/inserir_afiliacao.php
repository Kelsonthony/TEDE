<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira os dados inseridos:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se OK, inclua<i> </i>uma nova instituição 
      de afiliação ou selecione no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> 
      principal</u> e volte à tela principal do módulo para dar seqüência à operação 
      das demais opções.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong>Para incluir uma nova Instituição 
      de Afiliação</strong>, adote os seguintes procedimentos:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol'>S<span style='font:7.0pt "Times New Roman"'> 
      </span></span>elecione, no <i>menu</i> desta tela,<i> </i>o comando  <u>Inserir</u>; 
      </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O<span
style='font-size:10.0pt;font-family:Symbol'><span style='font:7.0pt "Times New Roman"'> 
      </span></span>bserve que o sistema disponibiliza uma tela para a inclusão 
      da instituição de afiliação ( Figura 23 ); </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol'>E<span style='font:7.0pt "Times New Roman"'> 
      </span></span>xecute os mesmos procedimentos utilizados para a primeira 
      inclusão dos dados, até concluir o processo.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se Não OK, proceder à correção, utilizando 
      as instruções descritas na seção Comando de Operação  Alterar, (seção 4.4.2). 
      </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao finalizar a operação, selecione,<i> 
      </i>no <i>menu</i> desta tela,<i> </i>o comando <i><u>menu</u></i><u> principal</u> 
      e volte à tela principal do módulo Pós-graduação, para dar seqüência à operação 
      das demais opções.</font></div>
  </li>
</ul>
