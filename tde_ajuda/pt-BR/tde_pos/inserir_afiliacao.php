  </span>Como operar a Opção <u>Afiliação</u></font></a></h3>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478484">4.4.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Inserir</u> – Opção Afiliação</a></font></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, no <i>menu</i> da tela principal 
      da opção,  o comando de operação <u>Inserir</u>.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema disponibiliza uma nova tela 
      com os campos para a inclusão dos dados das instituições de afiliação. Estes 
      dados formarão o Cadastro das Instituições de Afiliação, associadas a autores 
      e contribuidores das TDEs  (figura 23).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=600 height=234 src="imagens/image025.jpg"><br clear=ALL>
  <a name="_Ref62447984"><em>   Figura </em></a><em>23- Módulo Pós-graduação, 
  opção Afiliação – Cadastramento  dos  dados da Instituição de Afiliação</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Os campos de preenchimento obrigatório, 
      ao deixarem de ser preenchidos, são solicitados automaticamente pelo sistema, 
      que emite uma mensagem de erro e ressalta estes campos em vermelho. Inclua 
      nos campos discriminados a seguir, os dados da instituição de afiliação 
      do autor ou do contribuidor:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol'>D<span style='font:7.0pt "Times New Roman"'> 
      </span></span>igite, no campo <u>Nome</u>, preenchimento obrigatório, <strong>em 
      ordem direta</strong>, o nome completo da instituição de afiliação(instituições 
      às quais os autores e/ou contribuidores estão subordinados por vínculo empregatício). 
      Este campo deverá conter, no máximo, 100 caracteres alfanuméricos, também 
      podem ser usados os seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  
      - ,  _  e  * ;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>D</span>igite, no campo 
      <u>Sigla</u>, preenchimento obrigatório, <strong>a sigla oficial</strong> 
      da instituição de afiliação. Este campo deverá conter, no máximo, 30 caracteres 
      alfanuméricos,  também podem ser usados os seguintes caracteres especiais: 
      &amp;, ( ),  /,  \ ,  - ,  _ e  * ;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>S</span>elecione, no campo 
      <u>Pais</u>, preenchimento obrigatório, na tabela existente ao lado do campo, 
      o país da instituição de afiliação;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, no campo <u>UF</u>, na tabela 
      existentes ao lado do campo, a unidade da federação onde está sediada a 
      instituição de afiliação;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, no campo <u>CNPJ</u>, preenchimento 
      obrigatório, o CNPJ da instituição de afiliação. Este campo deverá ser preenchido 
      apenas com a seqüência de algarismos, com, no máximo, 14 caracteres numéricos. 
      Ex.: 33654831002341</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, no campo <u>URL</u>, a URL da 
      instituição de afiliação. EX.: <a href="http://www.qualquernome.gov.br/diretório">www.qualquernome.gov.br/diretório</a>. 
      Este campo deve conter, no máximo, 150 caracteres.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>C</span>lique no botão 
      de comando inserir, existente no canto direito inferior da tela; </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que o sistema disponibiliza uma 
      nova tela com a mensagem <u>Operação  Concluída</u> e com os dados da instituição 
      de afiliação -<span
style='font-family:Symbol'>-</span> ressaltados por uma faixa colorida <span
style='font-family:Symbol'>-</span> para conferência (figura 24).</font></div>
  </li>
</ul>
