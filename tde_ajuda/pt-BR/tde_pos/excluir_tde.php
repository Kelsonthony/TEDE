<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478498">4.6.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Excluir</u> - Exclusão da TDE</a></font></h3>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong>Aten&ccedil;&atilde;o:</strong></font></p>
<p align="justify" class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt'>A 
  exclusão de uma TDE só poderá ocorrer no Módulo Pós-graduação. Esta exclusão 
  só é permitida quando, na tela principal da opção TDE, o <i>status</i> apresentado 
  para a TDE que se deseja excluir for <u>Liberar,</u> <u>Criar Conta</u> ou <u>Revisar</u>. 
  Portanto, se a TDE estiver com o <i>status</i> <u>C/Autor</u> ou <u>C/ Catalogador</u>, 
  não poderá ser excluída.</span></font></p>
<div align="justify"> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Exclua a TDE 
      adotando os seguintes procedimentos:</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione,<i> 
      </i> no <i>menu</i> da tela da opção TDE, o comando  <u>Excluir</u> (figura 
      43).<br clear=ALL>
      </font></li>
  </ul>
</div>
