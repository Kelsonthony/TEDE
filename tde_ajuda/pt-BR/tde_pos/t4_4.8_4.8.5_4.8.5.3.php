<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.8.5<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>Observação (Revisão)</u></font></h3>
<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.8.5.3<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Envio 
  de Observação ao Módulo Biblioteca</font></h4>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b>Atenção: </b> esta 
  ação ocorre, somente, quando os operadores habilitados da pós-graduação, após 
  o encerramento do fluxo de operação da TDE com o autor e envio desta ao Módulo 
  Biblioteca, recebem <i>e-mail</i> do sistema informando o retorno de uma TDE 
  do Módulo Biblioteca para correção. </font></p>
<h5><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123263"></a><a name="_Toc60478983"></a><a name="_Toc60478521">4.8.5.3.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Comando 
  de Operação  <u>Inserir</u> (Observação Biblioteca)</a></font></h5>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Após 
      receber o <i>e-mail</i> do sistema, identifique a TDE devolvida, na janela 
      central da tela principal do módulo, pelo nome do autor e pelo <i>status</i> 
      <u>Revisar</u>, ressaltado na cor vermelha.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione 
      a TDE e identifique, após a seleção, o tipo de providência a ser tomada 
      no campo <u>Observação</u>, existente no topo da tela disponibilizada pelo 
      sistema.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Verifique, 
      pelo conteúdo da observação, se a solução da ação solicitada  diz respeito 
      ao autor, ou à pós-graduação (figura 71).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=601 height=396 src="imagens/image074.jpg"><br clear=ALL>
  <em>Figura 71 - Módulo Pós-graduação, opção Observação – Recebimento TDE com 
  Observação da Biblioteca</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      a alteração  for de responsabilidade do autor, proceder conforme instruções 
      descritas na seção Envio de Observação ao Autor ( seção 4.8.5.1.).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      a alteração for de responsabilidade da pós-graduação, esta deverá fazer 
      as alterações solicitadas, e, se houver observação a ser feita à biblioteca, 
      deverá adotar os seguintes procedimentos:</font></div>
  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">selecione, 
        no <i>menu</i> desta tela, o comando de operação  <u>Inserir</u>, existente 
        abaixo da opção Observação;</font></li>
    </ul>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">observe 
        que o sistema disponibiliza a tela principal da opção, identificada como 
        TDE, pelo campo Observação e por pequenas caixas, para a escolha do destinatário 
        que deverá receber a observação (72); </font></li>
    </ul>
  </div>
  <ul>
  </ul>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=607 height=324 src="imagens/image075.jpg"><br clear=ALL>
  <em>Figura 72 - Módulo Pós-graduação, opção Observação - inclusão observação 
  biblioteca</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Verifique 
      se o campo Observação contém alguma informação. Limpe o campo (se existir 
      alguma observação que não deve ser enviada para a catalogação) e digite, 
      de maneira clara, o texto da observação a ser enviada ao catalogador;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no campo Destinatário, a caixa de escolha <u>Catalogador</u>;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Inserir</u>, existente no canto direito inferior 
      da tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u>, 
      com o texto da observação feita pela pós-graduação e a identificação do 
      catalogador como destinatário (figura 73).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=600 height=269 src="imagens/image076.jpg"><br clear=ALL>
  <em>Figura 73 - Módulo Pós-graduação, opção Observação - Mensagem inclusão observação 
  biblioteca</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira 
      se a observação está correta:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      OK,  execute a opção Finalizar  (seção 4.8.6). </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      Não OK, proceda à correção utilizando as instruções descritas na seção Comando 
      de Operação  Alterar, (seção 4.8.5.3.2)</font></div>
  </li>
</ul>
<h5><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123264"></a><a name="_Toc60478984"></a><a name="_Toc60478522"></a><a
name="_Ref58842525"></a><a name="_Hlt58842560"></a>4.8.5.3.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Comando 
  de Operação <u> Alterar</u> (Observação Biblioteca)</font></h5>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta 
  ação ocorre quando for necessário: 1) corrigir o texto da observação feita ao 
  catalogador; 2) limpar o campo Observação para fazer outra observação ao catalogador; 
  4) repassar uma observação do autor à catalogação, e, para isto, o campo Destinatário 
  precisa ser alterado para  Catalogador. </font></p>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> da tela, o comando de operação  <u>Alterar</u>, existente 
      abaixo da opção Observação.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza a tela da opção com o campo Observação preenchido 
      para as alterações: </font> 
      <ul>
        <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span>faça as alterações necessárias 
          (alterar o texto, ou limpar o campo e reescrever a observação, alterar 
          o destinatário etc.);</font></li>
      </ul>
    </div>
  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">selecione, 
        no campo <u>Destinatário</u>, a caixa de escolha <u>Catalogador</u>;</font></li>
    </ul>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">clique no 
        botão de comando <u>Alterar</u>, existente no canto direito inferior da 
        tela;</font></li>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">observe 
        que o sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u>, 
        com o texto da observação alterada pela pós-graduação e a identificação 
        do catalogador como destinatário;</font></li>
    </ul>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">confira 
        novamente a observação. Se não estiver correta, repita o processo e, se 
        estiver correta, execute a opção Finalizar  (seção 4.8.6).</font></li>
    </ul>
  </div>
</ul>
<h5><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123265"></a><a name="_Toc60478985"></a><a name="_Toc60478523">4.8.5.3.3<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Comando 
  de Operação  <u>Excluir</u></a><u> </u>(Observação Biblioteca)</font></h5>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta 
  ação ocorre, quando é imperativa a exclusão de uma observação enviada ao autor 
  ou oriunda deste. Geralmente, esta ação ocorre após o encerramento do fluxo 
  de observações entre o autor e a pós-graduação no momento em que  a TDE é disponibilizada 
  para o Módulo Biblioteca. A pós-graduação tem a permissão para excluir observações 
  feitas pelo autor e pela pós-graduação. As observações oriundas do catalogador 
  só poderão ser excluídas no Módulo Biblioteca.</font></p>
<div align="justify"> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Identifique, 
      na janela central da tela principal do módulo, abaixo do campo <i><u>Status</u></i><u> 
      da TDE</u>, a TDE cuja observação deve ser excluída.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      na janela central da tela, o comando <u>Revisar</u>, em vermelho, correspondente 
      ao nome do autor da TDE.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que 
      o sistema disponibiliza a tela com as opções de operação  específicas para 
      a revisão da TDE.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> desta tela, o comando de operação  <u>Excluir</u>, existente 
      abaixo da opção Observação.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que 
      o sistema disponibiliza a tela da opção com a observação a ser excluída.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique no 
      botão de comando <u>Excluir</u>, existente no canto direito inferior da 
      tela.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema 
      disponibiliza uma tela com a mensagem <u>Operação  Concluída</u> e a referência 
      <u>Nenhuma observação foi inserida</u>, no campo Observação.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Para enviar 
      uma observação ao autor ou ao catalogador, execute as instruções escritas 
      na opção Finalizar (seção 4.8.6).</font></li>
  </ul>
