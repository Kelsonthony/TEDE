<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.6.4<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Associação do Contribuidor à TDE</font></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no menu da tela com a mensagem de confirmação da inclusão dos metadados 
      da TDE ( Figura 40 ), o comando de operação  <u>Inserir</u>, ressaltado 
      na cor vermelha, abaixo da opção Contribuidor.</font></div>
  </li>
</ul>
<p align="justify"> <font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b>Atenção:</b> 
  este comando de operação  aparecerá, <strong>em vermelho</strong>, no <i>menu</i> 
  das telas da opção TDE, <strong>enquanto o contribuidor que exerce o papel de 
  orientador da TDE não for inserido</strong>. <u>A não-inclusão do orientador 
  da TDE</u> impede a continuação das ações para a submissão eletrônica da tese 
  ou dissertação. Ao tentar dar continuidade ao processo, o sistema emite uma 
  mensagem de erro.</font></p>
<div align="justify"> 
  <ul>
    <li> <font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema 
      disponibiliza uma nova tela, identificada como Contribuidor TDE, com um 
      campo para a localização do contribuidor.</font></li>
    <li> <font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, neste 
      campo, as três primeiras letras do nome do contribuidor.</font></li>
    <li> <font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique no 
      botão de comando <u>Localizar</u>, ao lado do campo.</font></li>
    <li> <font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema 
      disponibiliza, nesta mesma tela, o campo Nome<i>,</i> ladeado por uma caixa 
      de seleção contendo a expressão <u>Seleciona</u> (figura 47).</font></li>
  </ul>
</div>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=595 height=209 src="imagens/image050.jpg"><br clear=ALL>
  <em>Figura 47 – Módulo Pós-graduação, opção TDE – Localização e seleção do contribuidor 
  da TDE</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      na seta (ao lado do termo selecionar) para abrir a caixa de seleção que 
      contém a lista dos contribuidores cadastrados.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione<i>, 
      </i>nesta lista, o nome do contribuidor que será associado à TDE.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma nova tela contendo os dados do contribuidor 
      selecionado e um campo para a seleção da forma de participação (Papel) deste 
      contribuidor na TDE (figura 48). </font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=600 height=261 src="imagens/image051.jpg"><br clear=ALL>
  <em>     Figura 48 -  Módulo Pós-graduação, opção TDE - Seleção do papel do 
  contribuidor na TDE</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      o contribuidor não constar no cadastro apresentado nesta caixa de seleção, 
      proceder ao cadastramento do contribuidor conforme descrito na opção Contribuidor, 
      na seção <a name="_Hlt58901054">4.3.1.1</a> - Inclusão dos dados de cadastro 
      do Contribuidor.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt'>Se 
      necessário, neste momento, encerrar os procedimentos de operação desta seção, 
      selecione, no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> principal</u> 
      e volte à tela principal do módulo.</span></font></div>
  </li>
</ul>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Ref56328129">4.6.5<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Inclusão do Papel do Contribuidor na TDE</a></font></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Nesta 
      mesma tela<i>, </i>abra<i> </i>a caixa de escolha contendo a expressão <u>Selecionar</u>, 
      localizada na janela central da tela abaixo do título Papel do Contribuidor.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione<i>, 
      </i>após abrir a caixa de escolha,<i> </i>o papel que o contribuidor exerce 
      na tese ou dissertação. <strong>A TDE só será liberada para a criação da 
      conta do autor, após a seleção e vinculação do nome e papel do orientador 
      à mesma.</strong></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando Inserir, existente no canto direito inferior da tela.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u> 
      e com os dados do contribuidor (nome e papel) para conferência (figura 49).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=604 height=257 src="imagens/image052.jpg"><br clear=ALL>
     <em>Figura 49 - Módulo Pós-graduação, opção Contribuidor - Mensagem confirmação 
  Inclusão contribuidor</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira 
      os dados inseridos.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      OK, repita o processo até incluir todos os contribuidores da TDE.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      após associar todos os contribuidores, no <i>menu</i> desta tela, o comando 
      de operação, em vermelho, <u>Liberar TDE para criação da Conta do Autor</u> 
      e proceda conforme instruções descritas na seção  Criação da Conta Autor 
      durante o processo de iniciar a TDE ( seção 4.7.1.1).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt'>Se 
      necessário, neste momento, encerrar os procedimentos de operação desta seção, 
      selecione, no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> principal</u> 
      e volte à tela principal do módulo.</span></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      não OK, podem ter ocorrido os seguintes erros (proceda de acordo com o tipo 
      de erro ou alteração a ser realizada): </font> 
      <ul>
        <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong><u> 
          Tipo de Erro 1</u></strong> – O orientador da TDE não foi associado 
          à mesma: </font></li>
      </ul>
    </div>
  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">neste caso, 
        o sistema disponibiliza uma tela com a seguinte mensagem: <u>Erro-017- 
        A TDE só poderá ser liberada para criação da conta do autor após a inserção 
        de contribuidor no papel de orientador</u> (figura 50).</font></li>
    </ul>
  </div>
  <ul>
  </ul>
</ul>
