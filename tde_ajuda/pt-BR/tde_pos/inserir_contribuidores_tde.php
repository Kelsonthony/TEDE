<h3><a name="_Toc60478501"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.6.6<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Alteração na Associação do Contribuidor à TDE</font></a></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt'>Após 
      confirmar a associação do contribuir à TDE, o sistema disponibiliza uma 
      tela com os dados inseridos. Esta informação, neste momento, não pode ser   
      alterada na tela disponibilizada. Se houve erro na associação do nome ou 
      do papel do contribuidor, proceder à exclusão deste e à associação correta 
      de outro, conforme descrito em Tipo de Erro 2  ( seção 4.6.5), ou na seção 
      4.6.7, a seguir.</span></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt'>A 
      exclusão de um contribuidor da TDE não implica na exclusão deste do Cadastro 
      de Contribuidores. Em caso de erro de digitação nos dados de identificação 
      do contribuidor, proceder a alteração na opção de operação  Contribuidor 
      deste módulo (seção 4.3.2).</span></font></div>
  </li>
</ul>
<h3><a name="_Toc60478502"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.6.7<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Exclusão da Associação do Contribuidor à TDE</font></a></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione<i>,</i> 
      no <i>menu</i> da tela principal do módulo, a opção TDE.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Localize, 
      nesta tela, o nome do autor em cuja TDE será feita a exclusão do contribuidor.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Alterar</u>, existente na janela central da tela, 
      correspondente ao nome do autor.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza a tela da TDE do autor selecionado.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> desta tela, na opção Contribuidor, o comando de operação 
       <u>Excluir</u>.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma tela com a relação dos contribuidores inseridos 
      na TDE.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique, 
      nesta tela, no botão de comando <span style='font-family:
Symbol'>-</span> posicionado abaixo do comando de operação  <u>Excluir</u>, existente 
      na janela central da tela <span style='font-family:Symbol'>-</span> correspondente 
      ao contribuidor a ser excluído.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma tela com a relação dos contribuidores associados 
      à TDE para a conferência da exclusão (figura 51).</font></div>
  </li>
</ul>
