  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira os dados inseridos:</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se OK, insira um novo 
    autor, ou selecione no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> 
    principal</u> e volte à tela principal do módulo para dar seqüência à operação 
    das demais opções.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span><strong>Para incluir 
    um novo autor adote os seguintes procedimentos</strong>: </font>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span>selecione, no <i>menu</i> 
        desta tela (figura 8),<i> </i>o comando <u>inserir</u>;</font></li>
    </ul>
  </li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">observe que o sistema 
       disponibiliza uma tela para  a inclusão  do  novo  autor ( figura 5 ); 
      </font></li>
  </ul>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">execute os mesmos procedimentos 
      utilizados para a primeira inclusão dos dados e para as alterações, até 
      concluir o processo.</font></li>
  </ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se não OK, proceder à 
    correção, utilizando as instruções descritas na seção Comando de Operação 
    Alterar (seção 4.2.2 ).</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao finalizar a operação, 
    selecione,<i> </i>no <i>menu</i> desta tela,<i> </i>o comando <i><u>menu</u></i><u> 
    principal</u> e volte à tela principal do Módulo Pós-graduação, para dar seqüência 
    à operação das demais opções.</font></li>
</ul>
