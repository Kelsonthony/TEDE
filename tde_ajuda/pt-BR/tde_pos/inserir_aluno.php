<p align="center" class=MsoCaption><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><em>Figura 
  6 – Módulo Pós-graduação, opção Autor  - Mensagem confirmação Inclusão dados 
  do Autor</em></font></p>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira 
      os dados inseridos. </font>
      <ul>
        <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se OK, selecione, 
          no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> principal</u> 
          e volte à tela principal do módulo para dar seqüência à operação das 
          demais opções de operação.</font></li>
      </ul>
    </div>
  </li>
  <div align="justify">
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se não OK, proceder 
        à correção, utilizando as instruções descritas na seção Comando de Operação 
         Alterar, a seguir ( seção <a name="_Hlt58906613"></a>4.2.2).</font></li>
    </ul>
  </div>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span class="EstiloJustificado"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span>Se a instituição de 
    afiliação <strong>não consta </strong>na lista de instituições apresentada, 
    adote os seguintes procedimentos:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">selecione<i>,</i> no 
      final<i> </i>na lista apresentada,<i> </i>a expressão <u>Nenhuma Acima</u>.</font></li>
  </ul>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">observe que o sistema 
      disponibiliza uma nova tela para o cadastramento da instituição de afiliação 
      do autor (figura 7).</font></li>
  </ul>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=603 height=308 src="imagens/image008.jpg"><br clear=ALL>
  <em>Figura 7 -  Módulo Pós-graduação, opção  Autor - Cadastramento 
  Instituição Afiliação do Autor</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Os 
      campos de preenchimento obrigatório, ao deixarem de ser preenchidos, são 
      solicitados automaticamente pelo sistema, que emite uma mensagem de erro 
      e ressalta estes campos em vermelho. Inclua, nos campos discriminados a 
      seguir, os dados para o cadastramento da instituição de afiliação do autor:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, 
      no campo <u>Nome</u>, preenchimento obrigatório, <strong>em ordem direta,</strong> 
      o nome completo da instituição de afiliação do autor (<strong>institui&ccedil;&otilde;es 
      a que estão subordinados por vínculo empregatício os autores das TDEs</strong>). 
      Este campo deverá conter, no máximo, 100 caracteres alfanuméricos, também 
      podem ser usados os seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  
      - ,  _  e  * ;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, 
      no campo <u>Sigla</u>, preenchimento obrigatório, a sigla oficial da instituição 
      de afiliação do autor. Este campo deverá conter, no máximo, 30 caracteres 
      alfanuméricos, também podem ser usados os seguintes caracteres especiais: 
      &amp;, ( ),  /,  \ ,  - ,  _  e  * ;</font></div>
  </li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">selecione, no campo <u>Pais</u>, 
    preenchimento obrigatório, na tabela existente ao lado do campo, o país da 
    instituição de afiliação do autor;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">selecione, no campo <u>UF</u>, 
    preenchimento obrigatório, na tabela existentes ao lado do campo, a unidade 
    da federação onde está sediada a instituição de afiliação do autor -. </font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, no campo <u>CNPJ,</u> 
    preenchimento obrigatório, o CNPJ da instituição de afiliação do autor. Este 
    campo deverá ser preenchido apenas com a seqüência de algarismos, com,  no 
    máximo, 14 caracteres numéricos. Ex.: 33654831002341;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, no campo <u>URL</u>, 
    a URL da instituição de afiliação do autor. Ex.: <a
href="http://www.qualquernome.gov.br/diretório">www.qualquernome.gov.br/diretório</a>. 
    Este campo deve conter, no máximo, 150 caracteres.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">clique no botão de comando 
    <u>Inserir</u>, existente no canto direito inferior da tela;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span>observe que o sistema 
    disponibiliza uma tela com a mensagem <u>Operação  Concluída</u> e com os 
    dados do autor e da instituição de afiliação <span style='font-family:
Symbol'>-</span> ressaltados por uma faixa colorida <span style='font-family:
Symbol'>-</span> para conferência ( figura 8 ).</font></li>
</ul>
