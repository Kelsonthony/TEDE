<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123211"></a><a name="_Toc60478931"></a><a name="_Toc60478461">4.2.1.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Inclusão 
  dos dados de cadastro do Autor</a></font></h4>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, no <i>menu</i> 
    desta tela, o comando de operação <u>Inserir.</u></font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema disponibiliza 
    uma nova tela com os campos para a inclusão dos dados de cadastro do autor 
    e para a associação ou inclusão de sua afiliação (figura 5).<a name="_Ref41710064"></a><a name="_Ref41301478"></a><a name="_Hlt41301511"></a><a
  name="_Hlt41710097"></a></font></li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=609 height=268 src="imagens/image006.jpg"><br clear=ALL>
  </font></div>
<p class=MsoCaption><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Ref56246793"></a><a name="_Hlt56246808"></a><em>Figura 
  5 – Módulo Pós-graduação, opção  Autor – Inclusão dados do autor</em></font></p>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Os campos de preenchimento 
    obrigatório ao deixarem de ser preenchidos, são solicitados automaticamente 
    pelo sistema, que emite uma mensagem de erro e ressalta estes campos em vermelho. 
    Inclua nos campos discriminados a seguir os dados do autor:</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, no campo <u>Nome</u>, 
    preenchimento obrigatório, <strong>em ordem direta, o nome completo do autor</strong>. 
    Este campo deverá conter, no máximo, 100 caracteres alfanuméricos, também 
    podem ser usados os seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  
    - ,  _  e  * ;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">clique, no <i><u>link</u></i><u> 
    <i>Lattes</i></u><i>,</i> (este campo possui um <i>link</i> com a base de 
    dados <i>Lattes</i> do CNPq), entre nesta base de dados e copie o endereço 
    do autor para o campo <i>lattes</i> desta tela;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, no campo <u>CPF</u>, 
    preenchimento obrigatório, o CPF do autor. Este campo deverá ser preenchido 
    apenas com a seqüência de algarismos, com no máximo 11 caracteres numéricos. 
    Ex.: 12345678901;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, no campo <i>E-mail</i>, 
    preenchimento obrigatório, o <i>e-mail</i> do autor. Este campo pode conter, 
    no máximo,   100 caracteres no formato utilizado para o padrão <i>e-mail.</i>  
    Ex.: <a href="mailto:nome@nome.com.br;">nome@nome.com.br</a>.</font></li>
</ul>
<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123212"></a><a name="_Toc60478932"></a><a name="_Toc60478462">4.2.1.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Associação 
  da Instituição de Afiliação ao Autor</a></font></h4>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Nesta mesma tela<i>, 
    </i><a name="OLE_LINK1">abra a<i> </i>caixa de seleção contendo a expressão 
    <u>Selecionar</u></a>, existente abaixo do título Afiliação.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Verifique<i> </i>se a 
    instituição de afiliação do autor consta da lista de instituições  apresentada 
    nesta caixa de seleção.</font></li>
  <li> <font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span>Se a instituição de 
    afiliação consta na lista apresentada, adote os seguintes procedimentos: </font>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span>selecione, nesta lista<i>,</i> 
        o nome da instituição de afiliação do autor;</font></li>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span>clique no botão de comando 
        <u>Inserir</u>, existente no canto direito inferior da tela, para associar 
        a instituição de afiliação ao autor; </font></li>
    </ul>
  </li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">observe que o sistema 
      disponibiliza uma tela com a mensagem <u>Operação  Concluída</u>, com os 
      dados do autor e com a instituição de afiliação, inseridos – ressaltados 
      por uma faixa colorida - para conferência (figura 6).</font></li>
  </ul>
</ul>
