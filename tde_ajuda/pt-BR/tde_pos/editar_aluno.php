      procedimentos:</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">abra, nesta 
      mesma tela, a<i> </i>caixa de seleção contendo a expressão <u>Selecionar</u>, 
      existente abaixo do título Afiliação;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">selecione 
      a instituição de afiliação correta do autor;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">clique<i> 
