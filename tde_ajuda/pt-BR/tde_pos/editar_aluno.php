<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Identifique, 
      na tela principal da opção Autor (Figura 4), ou na tela em que está operando 
      (figura 8 ), o autor cujos dados deverão ser alterados.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique<i> 
      </i>no botão de comando <span style='font-family:"Courier \(W1\)"'>-</span> 
      posicionado abaixo do comando <u>Alterar</u><i>,</i> existente<i> </i>na 
      janela central da tela <span style='font-family:"Courier \(W1\)"'>-</span> 
      correspondente ao nome autor em que será realizada a alteração;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma tela com os dados do autor em que será realizada 
      a alteração (figura 9).<a name="_Toc60478465"></a></font></div>
  </li>
</ul>
