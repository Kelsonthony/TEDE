<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Efetue 
      as alterações necessárias nos dados do autor, os quais vêm circundados por 
      uma caixa retangular.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Existe 
      alguma alteração a ser feita na Instituição de Afiliação do autor?</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      OK, proceda à correção conforme instruções descritas na seção Alteração 
      na Afiliação do Autor (seção 4.2.2.2).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      não OK, confirme<i> </i>a alteração nos dados do autor,<i> </i>clicando 
