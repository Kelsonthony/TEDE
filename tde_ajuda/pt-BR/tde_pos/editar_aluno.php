      da instituição de afiliação do autor <strong>precisarem ser alterados</strong>, 
      proceder à correção utilizando as instruções descritas na seção Operação 
      do Comando Alterar – Opção Afiliação (seção 4.4.2 ).</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao finalizar 
      a operação, selecione, no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> 
