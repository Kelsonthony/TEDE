<h3><a name="_Toc60478455">3.3.5<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>Observação (Revisão)</u></a></h3>
<p align="justify">Esta opção é utilizada para o registro da comunicação que viabiliza 
  o fluxo da TDE entre o Módulo Pós-graduação e o Módulo Autor e entre o Módulo 
  Pós-graduação e o Módulo Biblioteca, sempre que houver correções a serem feitas 
  na TDE. O fluxo de informação entre os atores do processo é monitorado pelo 
  sistema, que emite <i>e-mail</i>s aos mesmos informando a movimentação da TDE.</p>
<p align="justify">A opção observação possui telas específicas para a ação <u>inserir</u>, 
  <u>alterar</u> e <u>excluir</u>. A ação e<u>xcluir</u> deverá ocorrer sempre 
  (antes de enviar a TDE para a publicação eletrônica) que exista, neste campo, 
  uma observação do autor, ou uma observação que a pós-graduação não queira enviar 
  à biblioteca. Para fazer uma observação ao autor ou à biblioteca, utiliza-se 
  o comando de operação  Inserir desta opção.</p>
<p align="justify">As resposta a estas observações, recebidas do autor, devem 
  ser revisadas, para a certificação de que a ação solicitada foi atendida. Em 
  caso afirmativo, utiliza-se o comando de operação  <u>excluir</u>, desta opção, 
  para limpar o campo observação e assim liberar a TDE para a biblioteca. Em caso 
  negativo, repete-se o processo até a sua conclusão.</p>
<p align="justify">O recebimento de uma observação oriunda da biblioteca poderá 
  originar duas ações: ou o envio da TDE ao autor (adotando os mesmos procedimentos) 
  para providências, ou simplesmente  uma solução a ser dada pela pós-graduação. 
  Em ambos os casos, deve-se utilizar o comando de operação  <u>excluir</u>, para 
  limpar o campo observação e assim inserir uma nova observação para a biblioteca, 
  ou apenas disponibilizar a TDE para a biblioteca.</p>
