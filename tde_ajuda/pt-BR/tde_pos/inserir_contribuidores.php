<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira os dados inseridos:</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se OK, insira<i> </i>um novo contribuidor ou selecione no 
    <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> principal</u> e volte 
    à tela principal do módulo para dar seqüência a operação das demais opções.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong>Para incluir um novo <u>Contribuidor</u>, adote os 
    seguintes procedimentos:</strong></font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, no <i>menu</i> desta tela (figura 17),<i> </i>o 
    comando <u>Inserir</u>;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que o sistema disponibiliza uma tela para a inclusão 
    do novo contribuidor ( Figura 14 ); </font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Execute os mesmos procedimentos utilizados para a primeira 
    inclusão dos dados,  até concluir o processo.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se não OK, proceder à correção utilizando as instruções descritas 
    na seção Comando de operação  Alterar, da opção Contribuidor, (seção 4.3.2). 
    </font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao finalizar a operação, selecione,<i> </i>n<i>e</i>sta tela,<i> 
    </i>o comando <i><u>menu</u></i><u> principal</u> e volte à tela principal 
    do módulo Pós-graduação, para dar seqüência à operação das demais opções.</font></li>
</ul>
