<h2><a
name="_Ref56315147"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como operar a Opção <u>Contribuidor</u></font></a></h2>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123220"></a><a
name="_Toc60478940"></a><a name="_Toc60478472"></a><a name="_Ref56315136">4.3.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Inserir</u> – Opção Contribuidor</a></font></h3>
<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123221"></a><a name="_Toc60478941"></a><a name="_Toc60478473"></a><a
name="_Ref56313969"></a><a name="_Hlt57005815"></a>4.3.1.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Inclusão 
  dos dados de cadastro do Contribuidor</font></h4>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, no <i>menu</i> desta tela, 
      o comando de operação <u>Inserir</u>.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema disponibiliza uma nova tela 
      com os campos para a inclusão dos dados dos Contribuidores e para a seleção 
      ou inclusão de sua afiliação (figura 14). Estes dados formarão o Cadastro 
      dos Contribuidores que participam da construção das TDEs.</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=600 height=265 src="imagens/image016.jpg"><br clear=ALL>
  <a name="_Ref56249684"></a><a name="_Hlt57005580"></a><em>         Figura 14 
  – Módulo Pós-graduação, opção Contribuidor - Inclusão dados do Contribuidor</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Os campos de preenchimento obrigatório, 
      ao deixarem de ser preenchidos, são solicitados automaticamente pelo sistema, 
      que emite uma mensagem de erro e ressalta estes campos em vermelho. Inclua, 
      nos campos discriminados a seguir, os dados de cadastro do contribuidor:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, no campo <u>Nome</u>, preenchimento 
      obrigatório, <strong>em ordem direta,</strong> o nome completo do contribuidor. 
      Este campo deverá conter, no máximo, 100 caracteres alfanuméricos, também 
      podem ser usados os seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  
      - ,  _  e  * ;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, no campo <u>Citação</u>, o nome 
      do contribuidor em formato de citação (Ex.: Cunha, Murilo Bastos da). Este 
      campo deverá conter, no máximo, 100 caracteres alfanuméricos;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">clique, no <i><u>link</u></i><u> <i>Lattes</i></u><i>,</i> 
      (este campo possui um <i>link</i> com a base de dados <i>Lattes</i> do CNPq), 
      entre nesta base de dados e copie o endereço do contribuidor para o campo 
      <i>lattes</i> desta tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, no campo <u>CPF</u>, preenchimento 
      obrigatório, o CPF do contribuidor. Este campo deverá ser preenchido apenas 
      com a seqüência de algarismos, com, no máximo, 11 caracteres numéricos. 
      Ex.: 12345678901;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">ativar o campo <u>Estrangeiro sem CPF</u>, 
      <u>apenas</u> quando o contribuidor for estrangeiro e não tiver CPF;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">digite, no campo <i><u>E-mail</u>,</i> 
      o <i>e-mail</i> do contribuidor. Este campo pode conter, no máximo, 100 
      caracteres no formato utilizado para o padrão <i>e-mail. </i>Ex.: <a href="mailto:nome@nome.com.br;">nome@nome.com.br</a> 
      .</font></div>
  </li>
</ul>
<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123222"></a><a name="_Toc60478942"></a><a name="_Toc60478474">4.3.1.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Associação 
  da Instituição de Afiliação ao Contribuidor</a></font></h4>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Nesta mesma tela<i>, </i>abra<i> </i>a 
      janela contendo a expressão <u>Selecionar</u>, existente abaixo do título 
      Afiliação.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Verifique<i>, </i>após abrir a janela,<i> 
      </i>se a instituição de afiliação do contribuidor (<strong>institui&ccedil;&otilde;es 
      a que estão subordinados por vínculo empregatício os autores das TDEs</strong>) 
      consta da lista de instituições;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se a instituição de afiliação consta na 
      lista apresentada, adote os seguintes procedimentos:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, nesta lista<i>,</i> o nome 
      da Instituição de afiliação do contribuidor;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique no botão de comando <u>Inserir</u>, 
      existente no canto direito inferior da tela, para associar a Instituição 
      de Afiliação ao contribuidor; </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que o sistema disponibiliza uma 
      tela, com a mensagem <u>Operação  Concluída</u> e com os dados do contribuidor 
      e da afiliação inseridos – ressaltados por uma faixa colorida <span style='font-family:Symbol'>-</span> 
      para conferência (figura 15).</font></div>
  </li>
</ul>
<p align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=612 height=236 src="imagens/image017.jpg"></font></p>
<p align="center" class=MsoCaption><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><em>Figura 15 – Módulo Pós-graduação, 
  opção  Contribuidor - Associação Instituição de Afiliação do Contribuidor</em></font></p>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira os dados inseridos:</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se OK, selecione, no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> 
    principal</u> e volte à tela principal do módulo para dar seqüência à operação 
    das demais opções de operação.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se não OK, proceder à correção, utilizando as instruções 
    descritas na seção Comando de Operação  Alterar da opção Contribuidor (seção 
    4.3.2). </font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se a instituição de afiliação <strong>não consta na lista 
    de instituições apresentada</strong>, cadastre esta instituição, procedendo 
    da seguinte maneira:</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>S</span>elecione<i>, </i>na 
    janela aberta, a expressão <u>Nenhuma acima</u>, posicionada no final da lista 
    com o nome das instituições;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>O</span>bserve que o sistema 
    disponibiliza uma nova tela para o cadastramento da Instituição de Afiliação 
    do contribuidor (figura 16);</font></li>
</ul>
<p align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=608
  height=329 src="imagens/image018.jpg"></font></p>
<p align="center" class=MsoCaption><font size="2" face="Verdana, Arial, Helvetica, sans-serif">  <em>Figura 16 – Módulo Pós-graduação, 
  opção Contribuidor - Cadastramento  Afiliação do Contribuidor</em></font></p>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Os campos de preenchimento obrigatório, 
      ao deixarem de ser preenchidos, são solicitados, automaticamente, pelo sistema 
      que emite uma mensagem de erro e ressalta estes campos em vermelho. Inclua 
      nos campos discriminados a seguir, os dados para o cadastramento da instituição 
      de afiliação do contribuidor:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, no campo <u>Nome</u>, preenchimento 
      obrigatório, <strong>em ordem direta,</strong> o nome completo da instituição 
      de afiliação do contribuidor (<strong>institui&ccedil;&otilde;es a que estão 
      subordinados por vínculo empregatício os contribuidores das TDEs</strong>). 
      Este campo deverá conter, no máximo, 100 caracteres alfanuméricos, também 
      podem ser usados os seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  
      - ,  _  e  * ;</font></div>
  </li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, no campo <u>Sigla</u>, preenchimento obrigatório, 
    a sigla oficial da instituição de afiliação do contribuidor. Este campo deverá 
    conter, no máximo, 30 caracteres alfanuméricos, também podem ser usados os 
    seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  - ,  _  e  * ;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, no campo <u>Pais</u>, preenchimento obrigatório, 
    na tabela existente ao lado do campo, o país da instituição de afiliação do 
    contribuidor;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>S</span>elecione, no campo 
    <u>UF</u>, preenchimento obrigatório,  a unidade da federação <span
style='font-family:Symbol'>-</span> onde está sediada a instituição de afiliação 
    do contribuidor <span style='font-family:Symbol'>-</span> na tabela existentes 
    ao lado do campo;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style="font-family: Symbol; font-size: 10.0pt; color: black;">D</span>igite, 
    no campo <u>CNPJ,</u> o CNPJ da instituição de afiliação do contribuidor. 
    Este campo deverá ser preenchido apenas com a seqüência de algarismos, com, 
    no máximo, 14 caracteres numéricos. Ex.: 33654831002341;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>D</span>igite, no campo 
    <u>URL</u>, a URL da instituição de afiliação do contribuidor. EX.: <a
href="http://www.qualquernome.gov.br/diretório">www.qualquernome.gov.br/diretório</a>. 
    Este campo deve conter, no máximo, 150 caracteres;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique no botão de comando <u>Inserir</u>, existente no canto 
    direito inferior da tela; </font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que o sistema disponibiliza uma nova tela com a mensagem 
    <u>Operação  Concluída</u> e com os dados do contribuidor e da afiliação <span style='font-family:Symbol'>-</span> 
    ressaltados por uma faixa colorida <span style='font-family:Symbol'>-</span> 
    para conferência (figura 17).</font></li>
</ul>
