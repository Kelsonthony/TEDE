<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> da tela, o comando de operação  <u>Excluir</u>, existente 
      abaixo da opção Observação.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza a tela da opção com a observação a ser excluída 
      (figura 70).<a name="_Ref58906518"></a></font></div>
  </li>
</ul>
