<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Excluir</u>, existente no canto direito inferior 
      da tela.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u> 
      e a referência <u>Nenhuma observação foi inserida</u>, no campo Observação.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Para 
      enviar uma observação ao autor, execute a operação da opção Finalizar (seção 
      4.8.6).</font></div>
  </li>
</ul>
