  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      Não OK, execute as instruções descritas na seção Comando de Operação  Alterar 
      <span style='font-family:Symbol'>-</span> Observação Autor (seção 4.8.5.1.2).</font></div>
  </li>
</ul>
<h5><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123260"></a><a name="_Toc60478980"></a><a name="_Toc60478518">4.8.5.1.3<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Comando 
  de Operação  <u>Excluir</u> (Observação Autor)</a></font></h5>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta 
  ação ocorre, quando é imperativa a exclusão de uma observação enviada ao autor 
  ou oriunda deste. Geralmente, esta ação ocorre após o encerramento do fluxo 
  de observações entre o autor e a pós-graduação, e como também no momento em 
  que  a TDE é disponibilizada para o Módulo Biblioteca. A pós-graduação tem a 
  permissão para excluir observações feitas pelo autor e pela pós-graduação. As 
  observações oriundas do catalogador só poderão ser excluídas no Módulo Biblioteca.</font></p>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Identifique, 
      na janela central da tela principal do módulo, abaixo do campo <i><u>Status</u></i><u> 
      da TDE</u>, a TDE a ser revisada.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      nesta tela, o comando <u>Revisar</u>, em vermelho, correspondente ao nome 
      do autor cuja TDE deve ser revisada. </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza a tela com as opções de operação  específicas 
      para a revisão da TDE (figura 69).</font></div>
  </li>
</ul>
