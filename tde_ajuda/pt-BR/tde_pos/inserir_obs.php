<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.8.5<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>Observação (Revisão)</u></font></h3>
<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123257"></a><a name="_Toc60478977"></a><a name="_Toc60478515"></a><a
name="_Ref58837428"></a><a name="_Hlt58901966"></a>4.8.5.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Envio 
  de Observação ao Autor</font></h4>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta 
  ação ocorre quando a Pós-graduação, ao revisar a TDE, detecta a necessidade 
  de alterações a serem feitas pelo autor, ou quando recebe uma solicitação de 
  alteração do Módulo Biblioteca e esta alteração, também, é de responsabilidade 
  do autor. Para enviar uma observação ao autor, proceder conforme as instruções 
  descritas na seção Comando de Operação Inserir (seção 4.8.5.1.1).</font></p>
<h5 align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123258"></a><a name="_Toc60478978"></a><a name="_Toc60478516">4.8.5.1.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Comando 
  de Operação  <u>Inserir</u> (Observação Autor)</a></font></h5>
<div align="justify"> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Identifique, 
      na janela central da tela principal do módulo, abaixo do campo <i><u>Status</u></i><u> 
      da TDE</u>, a TDE a ser revisada.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      na janela central da tela, o comando <u>Revisar</u>, em vermelho, correspondente 
      ao nome do autor cuja TDE irá ser revisada.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que 
      o sistema disponibiliza a tela com as opções de operação  específicas para 
      a revisão da TDE.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique, no 
      <i>menu</i> desta tela, no comando de operação  <u>Inserir</u>, existente 
      abaixo da Opção Observação.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema 
      disponibiliza a tela principal da opção identificada como TDE, pelo campo 
      <u>Observação</u> e por pequenas caixas, para a escolha do destinatário 
      que deverá receber a observação (figura 66);</font></li>
  </ul>
</div>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=611 height=296 src="imagens/image069.jpg"><br clear=ALL>
  <a name="_Ref63580300"></a><a name="_Ref58906420"><em>Figura </em></a><em>66 
  - Módulo Pós-graduação - tela principal da opção Observação – Inserir observação</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
      Digite, nesta tela, no campo <u>Observação</u>, de maneira clara, o texto 
      da observação a ser enviada ao autor;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
      Selecione, no campo <u>Destinatário</u>, a caixa de escolha <u>Autor</u>;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
      Clique no botão de comando <u>Inserir</u>, existente no canto direito inferior 
      da tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
      Observe que o sistema disponibiliza uma tela com a mensagem <u>Operação 
       Concluída</u>, com o texto da observação feito pela pós-graduação e com 
      a identificação do autor como destinatário (figura 67);</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=617 height=249 src="imagens/image070.jpg"><br clear=ALL>
  <a name="_Ref58906459"></a><a name="_Hlt58906474"></a><em>     Figura 67 - Módulo 
  Pós-graduação, opção Observação - Mensagem inserção de observação ao autor</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira 
      se a observação está correta.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      OK, execute a opção Finalizar ( seção 4.8.6) e aguarde o <i>e-mail</i> do 
      sistema informando o retorno da TDE do Módulo Autor.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao 
      receber este <i>e-mail</i>, revise novamente a TDE e se necessário, execute 
      os mesmos procedimentos até que a TDE seja considerada correta para o envio 
      ao Módulo Biblioteca.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      Não OK, execute o comando de operação  <u>Alterar</u> (seção 4.8.5.1.2).</font></div>
  </li>
</ul>
<h5><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a
name="_Ref58842840">4.8.5.1.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Alterar</u></a><u> </u>(Observação Autor)</font></h5>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta 
  ação ocorre nas seguintes situações: 1) quando for necessário corrigir o texto 
  da observação feita ao autor; 2) quando, ao revisar a TDE, detectar a necessidade 
  de reenviar uma observação feita anteriormente ao autor, e o texto precisa ser 
  alterado; 3) quando for necessário limpar o campo observação para fazer outra 
  observação e enviar ao autor; 4) quando for necessário repassar uma observação 
  do autor à catalogação, ou vice-versa, e para isto o campo destinatário precisa 
  ser alterado. </font></p>
<div align="justify"> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> da tela, o comando de operação  <u>Alterar</u>, existente 
      abaixo da opção Observação.</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que 
      o sistema disponibiliza a tela da opção com a observação feita anteriormente 
      e execute os seguintes procedimentos:</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Faça as alterações 
      necessárias (alterar o texto, ou limpar o campo e reescrever a observação, 
      alterar o destinatário etc. );</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione 
      no campo <u>Destinatário</u>, a caixa de escolha <u>Autor</u>;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique no 
      botão de comando <u>Alterar</u>, existente no canto direito inferior da 
      tela; </font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema 
      disponibiliza uma tela com a mensagem <u>Operação  Concluída</u>, com o 
      texto da observação alterado pela pós-graduação e a identificação do Autor 
      como destinatário (figura 68).</font></li>
  </ul>
</div>
