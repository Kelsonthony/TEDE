<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Este 
  grupo de opções de operação  é utilizado para a realização do processo de revisão 
  e finalização das teses ou dissertações e envio destas ao Módulo Biblioteca 
  para a catalogação e publicação eletrônica.</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
  sistema só torna visível este grupo de opções quando o processo de revisão da 
  TDE tem início. Este processo se inicia quando o operador habilitado identifica 
  e aciona, na tela principal do Módulo Pós-graduação, a TDE, oriunda do autor, 
  que vai revisar. Quando isto ocorre, o sistema j&aacute; disponibiliza a tela 
  de operação  das opções destinadas à revisão da TDE.</font></p>
