<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Identifique, 
      na tela principal da opção Contribuidor, ou na tela em que está operando 
      (figura 17), o contribuidor cujos dados deverão ser alterados.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando – posicionado abaixo do comando <u>Alterar</u> na janela 
      central da tela – correspondente ao nome do contribuidor em que será realizada 
      a alteração. </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma nova tela com os dados do contribuidor para 
      as alterações necessárias (figura 18).</font></div>
  </li>
</ul>
