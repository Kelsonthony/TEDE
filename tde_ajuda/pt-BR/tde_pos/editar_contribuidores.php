<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123225"></a><a name="_Toc60478945"></a><a name="_Toc60478478">4.3.2.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Alteração  
  na  associação da Instituição de Afiliação, ao Contribuidor</a> </font></h4>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      a seleção da Instituição de Afiliação do contribuidor <strong>não está correta,</strong> 
      adote os seguintes procedimentos:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>A</span>bra, nesta mesma 
      tela<i>,</i> a Caixa de Escolha <u>Selecionar</u>, existente abaixo do título 
      Afiliação;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      nesta caixa, a instituição de afiliação correta do contribuidor;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>C</span>lique<i> </i>no 
      botão de comando <u>Alterar</u>, existente no canto direito inferior da 
      tela para associar a instituição de afiliação ao contribuidor;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u> 
      e com os dados alterados <span style='font-family:Symbol'>-</span> ressaltados 
      por uma faixa colorida <span style='font-family:Symbol'>-</span> para conferência.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      os dados da instituição de afiliação do contribuidor <strong>precisarem 
      ser alterados,</strong> proceder à correção utilizando as instruções descritas 
      na seção Comando de Operação Alterar, opção Afiliação (seção 4.4.2 ).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao 
      finalizar a operação, selecione, no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> 
      principal</u> e volte à tela principal do módulo para dar seqüência às demais 
      opções.</font></div>
  </li>
</ul>
