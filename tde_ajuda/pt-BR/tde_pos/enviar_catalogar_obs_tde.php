<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a
name="_Ref56329608">4.8.6.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Disponibilizar para a Biblioteca</a></font></h4>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Antes de finalizar 
  a TDE para a Biblioteca, adote os seguintes procedimentos:</font></p>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione 
      o comando de operação  <u>Excluir</u> ou <u>Alterar</u> (conforme o caso) 
      existente abaixo da opção Observação;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Limpe 
      o texto do campo Observação, ou exclua a observação enviada e/ou recebida 
      do autor, se for o caso;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      houver alguma observação a ser feita para a biblioteca, proceder conforme 
      descrito na seção 4.8.5.3;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp;</span></span>Selecione no campo <u>Destinatário</u> 
      a caixa de escolha <u>Catalogador</u>;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Alterar</u>, existente no canto direito inferior 
      da tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u> 
      e a observação (se for o caso) impressa no topo da tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> desta tela, na opção de operação Finalizar, o comando de 
      operação  <u>Disponibilizar para a Biblioteca</u>, ressaltado na cor vermelha;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma tela com a seguinte mensagem: <u>Liberar 
      TDE para a Biblioteca</u>, para confirmação (figura 75);</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=598 height=262 src="imagens/image078.jpg"><br clear=ALL>
  <em>Figura 75 – Módulo Pós-graduação, opção Finalizar – mensagem “Liberar TDE 
  para a Biblioteca”</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Confirmar</u>, existente no canto direito inferior 
      da tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp;</span></span>Observe que o sistema 
      disponibiliza uma tela com a mensagem <u>Operação  Concluída</u>, indicando 
      que a TDE foi disponibilizada para o Módulo Biblioteca, para a catalogação.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span class="MsoBodyText"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span><span style='font-size:11.0pt'>Após 
      esta etapa, a pós-graduação poderá receber um <i>e-mail</i> do sistema fazendo 
      dois tipos de comunicação:</span></font></div>
  </li>
</ul>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
  1 – <i>e-mail</i> comunicando que a biblioteca está retornado a TDE à pós-graduação 
  para providências;</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">2 
  – <i>e-mail</i> comunicando que a TDE foi publicada eletronicamente.</font></p>
