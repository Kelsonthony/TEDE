<h2><a name="_Toc60478450"><font size="4">3.3<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opções de Operação  </font></a><font size="4"><span style='font-family:"Courier \(W1\)"'>-</span> 
  Grupo Revisão</font></h2>
<p align="justify">Este grupo de opções de operação  é utilizado para a realização 
  do processo de revisão e finalização das teses ou dissertações e envio destas 
  ao Módulo Biblioteca para a catalogação e publicação eletrônica.</p>
<p align="justify">O sistema só torna visível este grupo de opções quando o processo 
  de revisão da TDE tem início. Este processo se inicia quando o operador habilitado 
  identifica e aciona, na tela principal do Módulo Pós-graduação, a TDE que vai 
  revisar. Quando isto ocorre, o sistema disponibiliza a tela de operação  das 
  opções destinadas à revisão da TDE.</p>
<p align="justify">Durante o processo de revisão, os operadores habilitados devem 
  acionar cada opção existente no <i>menu</i> desta tela e revisar o conteúdo 
  das informações disponibilizadas nas telas apresentadas, o conteúdo dos arquivos 
  inseridos na TDE pelo autor, bem como a integridade destes arquivos. Neste processo, 
  se for detectada a necessidade de alguma correção nos metadados inseridos pelo 
  autor, ou nos arquivos da tese ou dissertação que está sendo revisada, deve-se 
  devolvê-la ao autor com as observações, para as correções necessárias.</p>
<p align="justify">Após a conclusão do fluxo entre a pós-graduação e autor e com 
  a efetivação das correções, se a pós-graduação considerar completo o processo 
  de revisão, deve enviar a TDE ao Módulo Biblioteca para a catalogação e a publicação 
  eletrônica.</p>
<p align="justify">As opções do Grupo Revisão são acionadas por meio de uma tela, 
  identificada pelo <i>menu</i> – lateral esquerda tela – com as opções de operação 
   utilizadas no processo de revisão, e por uma janela central identificada como 
  TDE, contendo os metadados descritivos da TDE inseridos, ao longo do processo, 
  pela pós-graduação e pelo autor.  </p>
<p align="justify">O operador responsável pelo processo de revisão de uma TDE 
  deve, sempre, ter o cuidado de, ao encerrar a execução de uma seção relacionada 
  a esta TDE, sair da última tela de operação pelo comando <i><u>menu</u></i><u> 
  principal</u>. Jamais deve fechar a tela pelo <i>browse</i>. A saída pelo <i>browse</i> 
  bloqueia o acesso de outro operador à  TDE, impedindo a continuação do processo 
  de  revisão. Somente com o <i>login</i> e a senha do operador que fechou a tela 
  pelo <i>Browse</i> pode ser realizado o desbloqueio. Para isto, deve-se acionar 
  a TDE, com impedimento de acesso, que está sendo revisada e sair da tela pelo 
  comando <i><u>menu</u></i><u> principal</u>. </p>
