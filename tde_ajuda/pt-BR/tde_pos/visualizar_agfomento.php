<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione,<i> 
    </i>no <i>menu</i>  da tela principal do módulo ( Figura 3 ),  a  opção de 
    operação Agência Fomento; </font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema disponibiliza 
    a tela principal da opção, identificada como Agência Fomento e pela relação 
    das principais agências de fomento do país (figura 29). </font></li>
</ul>
