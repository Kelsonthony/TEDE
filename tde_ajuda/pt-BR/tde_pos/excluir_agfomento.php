<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478493">4.5.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Excluir</u> – Opção Agência Fomento</a></font></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Identifique,<i> 
      </i>na tela principal da opção Agencia Fomento, o nome da agência de fomento 
      a ser excluída.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <span style='font-family:Symbol'>-</span> posicionado 
      abaixo do comando <u>Excluir</u>, existente na janela central da tela <span style='font-family:Symbol'>-</span> 
      correspondente ao nome da agência de fomento a ser excluída.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a
name="_Toc60478494">O sistema disponibiliza uma tela com os dados da agência de 
      fomento para a exclusão (figura 34). O sistema não permite a exclusão de 
      uma agência fomento que esteja associada a alguma TDE.</a>  </font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=610 height=221 src="imagens/image036.jpg"><br clear=ALL>
  <em>       Figura 34 – Módulo Pós-graduação, opção Agência Fomento 
  - Exclusão Agência Fomento</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Excluir</u>, existente no canto direito inferior 
      da tela. </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      a agência fomento não estiver associada a alguma TDE, o sistema disponibiliza 
      uma nova tela com a mensagem <u>Operação  Concluída</u>,  para conferência 
      (figura 35).</font></div>
  </li>
</ul>
