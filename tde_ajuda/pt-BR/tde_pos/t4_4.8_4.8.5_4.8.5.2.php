  </span>Retorno da TDE do Módulo Autor com as correções</a></h4>
<ul>
  <li>
    <div align="justify"><span class="MsoBodyText"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'> </span></span><span style='font-size:11.0pt'>A 
      pós-graduação deve ficar atenta ao recebimento de um <i>e-mail</i> do sistema 
      comunicando que o autor submeteu novamente a TDE para a revisão.</span></div>
  </li>
  <li>
    <div align="justify"><span class="MsoBodyText"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'> </span></span><span style='font-size:11.0pt'>Após 
      receber este <i>e-mail</i> do sistema, adote os seguintes procedimentos:</span></div>
  </li>
  <div align="justify"><span class="MsoBodyText"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'> </span></span> 
    <ul>
      <li><span
style='font-size:11.0pt'>identifique e selecione a TDE a ser revisada na tela 
        principal do módulo;</span></li>
    </ul>
    <span class="MsoBodyText"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'> </span></span> 
    <ul>
      <li><span
style='font-size:11.0pt'>verifique se, no campo Observação, existe alguma observação 
        do autor para a pós-graduação;</span></li>
    </ul>
    <span class="MsoBodyText"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'> </span></span> 
    <ul>
      <li><span
style='font-size:11.0pt'>faça a revisão da TDE conforme descrito nas opções de 
        operação  do Grupo Revisão (4.8);</span></li>
    </ul>
    <span class="MsoBodyText"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'> </span></span> 
    <ul>
      <li><span
style='font-size:11.0pt'>repita este fluxo até que a TDE atenda às condições para 
        o envio para a catalogação e publicação eletrônica.</span></li>
    </ul>
  </div>
  <li>
    <div align="justify"><span class="MsoBodyText"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'> </span></span><span style='font-size:11.0pt'>Se 
      todo o processo de revisão da TDE atender às condições para a publicação 
      eletrônica da tese ou dissertação, execute os procedimentos descritos na 
      opção Finalizar (seção 4.8.6).</span></div>
  </li>
</ul>
