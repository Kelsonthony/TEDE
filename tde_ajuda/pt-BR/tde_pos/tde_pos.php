<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Cada 
  opção deste módulo tem uma denominação relacionada à função que realiza no processo 
  de registro e publicação das TDEs. Estão apresentadas na ordem seqüencial de 
  operação, no <i>menu</i> da tela principal do Módulo Pós-graduação, e devem 
  ser ativadas nesta ordem.</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">As 
  funções de cada opção de operação são executadas por meio dos <u>comandos de 
  operação</u> existentes nos <i>menu</i>s das telas, apresentadas no decorrer 
  da operação do módulo. No processo de operação de uma opção, após a execução 
  de um conjunto de ações, confirmadas por um <u>botão de comando</u>, o sistema 
  apresenta uma nova tela, para a execução de novas ações, ou emite mensagens 
  confirmando as ações executadas, ou indicando erros. A seqüência destas telas 
  é apresentada neste manual <span style='font-family:"Courier \(W1\)"'>-</span> 
  ao longo das descrições de cada opção de operação <span style='font-family:
"Courier \(W1\)"'>-</span> por figuras numeradas com as respectivas legendas.</font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opções de Operação  <span style='font-family:Symbol'>-</span> Grupo Cadastro</font></h2>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção  de Operação  <u>Autor</u></font></h3>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta opção 
  é utilizada para cadastrar, alterar e excluir os dados que identificam os autores 
  das teses ou dissertações vinculadas a um programa de pós-graduação e que serão 
  publicadas eletronicamente utilizando o Sistema TEDE.</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A tela principal 
  da opção é identificada como autor, na janela central da tela e no <i>menu</i> 
  – lateral esquerda tela – que contém os comandos de operação  para a execução 
  das ações concernentes à opção. Na janela central da tela, após a inclusão,  
  é apresentada uma relação com os nomes dos autores, <i>status</i> em que se 
  encontra cada TDE e a possibilidade de alterar os dados destes autores ou excluí-los,  
  desde que o processo de inclusão das TDEs destes autores  não tenha sido iniciado 
  ( Figura 4 ).</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Existe 
  um Cadastro de Autor para cada programa de pós-graduação. Portanto, os operadores 
  do Módulo Pós-graduação somente terão acesso ao cadastro dos autores das TDEs 
  do programa de pós-graduação que está sendo operado.</font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>Contribuidor</u></a></font></h3>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta opção 
  é utilizada para incluir, alterar e excluir os dados do cadastro de contribuidores 
  (orientador, membros da banca examinadora e outros), participantes do processo 
  de concepção, elaboração, defesa etc. das teses ou dissertações defendidas na 
  instituição receptora do Sistema TEDE. Este é um cadastro geral para ser utilizado 
  por todos os programas de pós-graduação.</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A tela principal 
  da opção é identificada como contribuidor,  na janela central da tela e no <i>menu</i> 
  – lateral esquerda tela – que contém os comandos de operação  para a execução 
  das ações concernentes à opção. Na janela central da tela, no decorrer do processo 
  de cadastramento, é apresentada uma relação, em ordem alfabética,  com os nomes 
  dos contribuidores cadastrados. Nesta janela, estão posicionados botões de comando 
  que acionam novas telas para a alteração dos dados incluídos, ou para a exclusão 
  dos contribuidores do cadastro,  desde que estes contribuidores não estejam 
  associados a uma tese ou dissertação iniciada no processo de submissão eletrônica 
  ( Figura 13 ).</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
  cadastramento dos contribuidores deve ser realizado antes da inicialização da 
  TDE para agilizar o processo de associação destes contribuidores à mesma.</font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>Afiliação</u></a></font></h3>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta opção 
  é utilizada para inserir, alterar e excluir os dados que identificam as instituições 
  de afiliação a que estão subordinados por vínculo empregatício os autores e 
  contribuidores das TDEs. O conjunto destas instituições irão formar o Cadastro 
  de Instituições de Afiliação.  O cadastramento destas instituições deve ser 
  realizado antes da inicialização da TDE, para facilitar a associação das mesmas 
  aos autores e contribuidores nas teses e dissertações iniciadas no processo 
  de submissão eletrônica.</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  tela principal da opção é identificada como Afiliação, na janela central da 
  tela e no <i>menu</i> – lateral esquerda tela – que contém os comandos de operação 
   para a execução das ações concernentes à opção. Na janela central da tela, 
  no decorrer do processo de cadastramento, é apresentada uma relação, em ordem 
  alfabética,  dos  nomes das instituições  cadastradas. Nesta janela, estão posicionados 
  botões de comando que acionam novas telas para a alteração dos dados destas 
  instituições ou para a sua exclusão,  desde que não estejam associadas a um 
  autor ou contribuidor de uma tese ou dissertação  iniciada no processo de submissão 
  eletrônica ( Figura 22 ).</font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1.4<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>Agência Fomento</u></a></font></h3>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta opção 
  é utilizada para inserir, alterar e excluir os dados que identificam as instituições 
  de fomento dos autores das TDEs e formam o Cadastro de Instituições de Fomento. 
  O cadastramento destas instituições deve ser realizado, antes da inicialização 
  da TDE, para facilitar a sua associação aos autores nas teses e dissertações 
  iniciadas no processo de submissão eletrônica.</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  tela principal da opção é identificada como Agência Fomento, na janela central 
  da tela e no <i>menu</i> – lateral esquerda tela – que contém os comandos de 
  operação  para a execução das ações concernentes à opção. Na janela central 
  da tela, no decorrer do processo de cadastramento, é apresentada uma relação 
  em ordem alfabética  com o nome das instituições. Nesta janela, estão posicionados 
  botões de comando que acionam telas para a alteração dos dados destas instituições 
  ou para a sua exclusão,  desde que não estejam associadas a um autor de uma 
  tese ou dissertação  iniciada no processo de submissão eletrônica ( Figura 29 
  ).</font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opções de Operação </a><span style='font-family:"Courier \(W1\)"'>-</span> 
  Grupo Submissão</font></h2>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.2.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>TDE</u></a></font></h3>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta opção 
  é utilizada para inicializar as teses e dissertações no processo de submissão 
  eletrônica. Por meio desta opção é feito o gerenciamento das teses e dissertações 
  vinculadas a cada programa de pós-graduação. E ainda é feita a gerência do fluxo 
  das TDEs entre o Módulo Pós-graduação e Módulo Autor e entre O Módulo Pós-graduação 
  e o Módulo Biblioteca, até que sejam revisadas e aprovadas para a publicação 
  eletrônica.</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Para que 
  uma TDE seja inicializada, o sistema solicita uma seqüência obrigatória de ações, 
  executadas em telas diferenciadas. Algumas destas telas apresentam, em seus 
  <i>menu</i>s, comandos de operação, em alerta na cor vermelha, indicando a obrigatoriedade 
  da execução destes comandos para a continuidade do processo.  Os itens da seqüência 
  (seções) obrigatória para a inclusão de uma TDE são: 1) selecionar o nome do 
  Autor, previamente cadastrado; 2) inserir os elementos de dados: grau, titulação, 
  local defesa, data defesa, idioma e título; 3) inserir os contribuidores e o 
  papel (orientador, membro da banca e outros) que desempenham no processo de 
  construção da tese e dissertação; 4) liberar a TDE para a criação de conta do 
  autor; 5) criar a conta do autor para o acesso deste  ao sistema. Cada seqüência 
  (seção) possui um conjunto de telas, com um <i>menu</i> na lateral esquerda, 
  com comandos de operação específicos para cada seqüência.</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O operador 
  responsável pelo processo inicial de submissão de uma TDE deve, sempre, ter 
  o cuidado de, ao encerrar a execução de uma seção relacionada a esta TDE, sair 
  da última tela de operação pelo comando <i><u>menu</u></i><u> principal</u>. 
  Jamais deve fechar a tela pelo <i>browse</i>. A saída pelo <i>browse</i> bloqueia 
  o acesso de outro operador à  TDE, impedindo a continuação do processo de submissão. 
  Somente com o <i>login</i> e a senha do operador que fechou a tela pelo <i>browse</i> 
  pode ser realizado o desbloqueio. Para isto, o operador deve acionar a TDE (no 
  estágio em que encerrou a seção pelo <i>browse</i>) com impedimento de acesso 
  e sair  da tela pelo comando <i><u>menu</u></i><u> principal</u>.</font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  tela principal da Opção é identificada como TDE, na janela central da tela e 
  no <i>menu</i> – lateral esquerda tela – que contém os comandos de operação 
   para a execução das ações concernentes à opção. Na janela central da tela, 
  no decorrer do processo de cadastramento, é apresentada uma relação das TDEs 
  que estão sendo operadas no módulo pós-graduação e botões de comando que acionam 
  telas para a alteração ou exclusão dos dados destas TDEs ( Figura 36 ).</font></p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.2.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>Conta Autor</u></font></h3>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta 
  opção é utilizada para criar as contas dos autores cujos dados iniciais da TDE 
  já foram incluídos, liberar a TDE para o autor iniciar o processo de submissão, 
  alterar dados das contas já criadas e acompanhar o <i>status</i> destas contas. 
  </font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  tela principal da opção é identificada como conta autor, na janela central da 
  tela e no <i>menu</i> – lateral esquerda tela – que contém os comandos de operação 
   para a execução das ações concernentes à opção. Na janela central da tela, 
  é apresentada uma relação com os nomes dos autores cujas contas já foram criadas, 
  prazo de validade de cada conta e botões de comando que acionam telas para a 
  alteração dos dados destas contas ( Figura 58 ).</font></p>

