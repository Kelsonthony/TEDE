<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">As 
  opções de operação do Grupo Revisão (disponibilizadas pelo sistema em uma segunda 
  etapa, após a TDE retornar do módulo autor), relacionadas a seguir, permitem 
  à pós-graduação a revisão e a finalização da TDE para o envio ao módulo biblioteca 
  para a catalogação e publicação eletrônica da tese ou dissertação.</font></p>
