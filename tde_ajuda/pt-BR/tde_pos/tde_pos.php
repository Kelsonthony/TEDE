<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">As 
  opções de operação do Grupo Submissão, relacionadas a seguir, possuem a função 
  de gerenciar o registro inicial dos dados de uma TDE e criar a conta para o 
  autor iniciar o processo de submissão de sua tese ou dissertação no sistema.</font></p>
<div align="justify"> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span class="EstiloJustificado"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
