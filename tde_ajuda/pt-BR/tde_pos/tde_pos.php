<h1><span
style='color:black'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></font></span><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span class=EstiloTtulo111ptChar>Acesso 
  e Operação do Módulo </span>Pós-graduação</a></font></h1>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
  acesso ao módulo pós-graduação é feito por meio da <u>barra de <i>menu</i></u>, 
  disponível na tela principal do sistema ( Figura 1 ), que, ao ser acionada, 
  apresenta a tela de acesso ao módulo ( Figura 2 ).  No <i>menu</i> desta tela 
  o sistema solicita uma chave de identificação <span
style='font-family:Symbol'>-</span> <i>login</i> da conta – que deve ser confirmada 
  por uma senha. O <i>login</i> da conta e a senha são dispositivos de segurança 
  criados no módulo administração para os operadores de cada módulo. </font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  operação do módulo é feita por meio de um conjunto de <u>opções de operação</u>. 
  Estas opções são acionadas para execução, no <i>menu</i> da tela principal do 
  módulo ( Figura 3 ).</font></p>
<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='color:black'>3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Opções de Operação do Módulo Pós-graduação</a></font></h1>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">As 
  opções de operação executadas na primeira fase de operação do módulo pós-graduação 
  são apresentadas na tela principal do módulo em dois grupos: Grupo Cadastro 
  e Grupo Submissão. Existe um terceiro grupo de opções, o Grupo Revisão, que 
  o sistema disponibiliza para execução, apenas, na fase de revisão da TDE. </font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">As 
  opções de operação do Grupo Cadastro, relacionadas a seguir, possuem a função 
  de cadastramento das pessoas e instituições relacionadas com a TDE. </font></p>
<div align="justify"> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span class="EstiloJustificado"
style='font-size:10.0pt;font-family:Symbol;color:black'><span
