  conta do autor pode ser criada de duas maneiras. A primeira, durante o processo 
  de iniciação da TDE, por meio da opção Finalizar (utilizando-se o comando de 
  operação, alerta em vermelho, Liberar TDE para a Criação da Conta Autor). A 
  segunda, acionando-se o comando de operação <u>Inserir</u>  da opção Conta Autor, 
  no <i>menu</i> da tela principal do Módulo.s</span></font></p>
<p><a
name="_Ref56315472"><font size="4"><strong><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.7<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como Operar a Opção <u>Conta Autor</u></font></strong></font></a> </p>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478504">4.7.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Criação da <u>Conta Autor</u></a></font></h3>
<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123247"></a><a name="_Toc60478967"></a><a name="_Toc60478505"></a><a
name="_Ref56314256"></a><a name="_Hlt56314339"></a><strong>4.7.1.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Criação 
  da Conta Autor durante o processo de iniciar a TDE</strong></font></h4>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> das telas resultantes das ações de inclusão, alteração  ou 
      exclusão de um contribuidor, o comando de operação , em vermelho, <u>Liberar 
      TDE para a Criação da Conta do Autor</u>.</font></div>
  </li>
</ul>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=656 height=215
src="imagens/image055.gif"></font></p>
<p align="center" class=MsoCaption><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><em>          
  Figura 52 - Módulo Pós-graduação, opção Finalizar - Liberar TDE para criação 
  da Conta Autor</em></font></p>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O sistema disponibiliza 
    uma nova tela com a seguinte mensagem: <u>Liberar TDE para a criação da conta 
    do autor “X”</u> (figura 53). </font></li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=613 height=229 src="imagens/image056.jpg"><br clear=ALL>
  <em>     Figura 53- Módulo Pós-graduação, opção Conta Autor - Liberar TDE para 
  a criação da Conta do Autor</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Confirmar</u>, existente no canto direito inferior 
      da tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma nova tela com a seguinte mensagem: <u>TDE 
      liberada para a criação da Conta do Autor</u> (figura 54).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=604 height=230 src="imagens/image057.jpg"><br clear=ALL>
  <em>Figura 54 – Módulo Pós-graduação, opção Conta Autor - Liberação TDE para 
  a criação da conta do autor</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Criar Conta</u>, existente no canto direito inferior 
      da tela.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma tela identificada como Conta Autor, com um campo 
      para a localização do autor para o qual a conta vai ser criada (figura 55).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=611 height=227 src="imagens/image058.jpg"><br clear=ALL>
  <a name="_Ref62893092"><em> Figura </em></a><em>55 – Módulo Pós-graduação, opção 
  Conta Autor - Localização do autor para a criação da conta </em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, 
      neste campo, as três primeiras letras do nome do autor para o qual a conta 
      vai ser criada.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Localizar</u>, ao lado do campo.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione 
      o nome do autor para o qual vai ser criada a conta.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma tela, identificada como Conta do Autor e Validade 
      da Conta, contendo dados da conta do autor e o prazo de validade desta conta, 
      informações necessárias para a criação da Conta Autor ( figura 56).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=620 height=251 src="imagens/image059.jpg"><br clear=ALL>
  <em>        <a name="_Ref62893246">Figura </a>56 – Módulo Pós-graduação, opção 
  Conta Autor - Dados  e prazo de validade da  Conta Autor</em></font></div>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b>Observação:</b></font></p>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style="font-family: Marlett">1 
  - O</span> prazo de validade da conta é um prazo padrão concedido, pela Instituição 
  Receptora do Sistema, aos autores para a preparação de sua tese ou dissertação 
  para a publicação eletrônica;</font></p>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-family:Marlett'>2 
  - O</span> campo <u>Bloquear</u> possui duas opções de seleção: <u>Sim</u> e 
  <u>Não</u>. O sistema sempre apresenta esta tela com a seleção <u>Sim</u> acionada. 
  Isto significa que a conta, por medida de segurança, é transmitida ao autor, 
  por <i>e-mail</i>, bloqueada, e será desbloqueada somente por ele, por meio 
  de uma senha emitida pelo sistema. </font></p>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira 
      os dados desta tela.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      OK, confirme no botão de comando <u>Inserir</u>, existente no canto direito 
      inferior da tela.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma tela, com a mensagem <u>Operação  Concluída</u>, 
      com os dados de criação da conta autor e um <i>link</i> (símbolo de impressão) 
      para a impressão destes dados (figura 57). Ao mesmo tempo, o sistema envia 
      um <i>e-mail</i> ao autor comunicando a abertura de sua conta e a senha 
      de acesso ao Sistema TEDE . Isto significa que, a partir deste momento, 
      começa o prazo para o autor dar início ao processo de submissão de sua TDE.</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=605 height=213 src="imagens/image060.jpg"><br clear=ALL>
  <em>Figura 57 – Módulo Pós-graduação, opção Conta Autor - Dados de criação da 
  conta e link para impressão</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      não OK, ocorreram os seguintes erros: </font> 
      <ul>
        <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><u><strong>Tipo 
          de Erro 1</strong></u><b> –</b> o nome do Autor da Conta foi selecionado 
          incorretamente.</font></li>
      </ul>
    </div>
  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">neste caso, 
        digite nesta tela (figura 56) as três primeiras letras do nome do autor 
        para o qual a conta vai  ser criada e repita o processo. </font></li>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><u><strong>Tipo 
        de Erro 2</strong></u> – o prazo de validade da Conta Autor está incorreto.</font></li>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'><span
style='font:7.0pt "Times New Roman"'>&nbsp; </span></span>neste caso, altere as 
        datas de <u>Inicio</u> e <u>Término</u> do prazo de validade da conta.</font></li>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><u><strong>Tipo 
        de Erro 3</strong></u> – a opção Bloquear (bloquear conta do autor) será 
        modificada.</font></li>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">neste caso, 
        altere a seleção para <u>Não</u>.</font></li>
    </ul>
  </div>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao 
      finalizar todas as ações, clique no botão de comando <u>Inserir</u>, existente 
      no canto direito inferior da janela, para criar a Conta Autor.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma tela<i> </i>com a mensagem <u>Operação  Concluída</u>, 
      com os dados da conta do autor e com um <i>link</i> (símbolo de impressão) 
      para imprimir os dados desta conta (figura 57). </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione<i>,</i> 
      no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> principal</u> 
      e volte à tela principal do módulo para dar continuidade ao processo de 
      submissão das demais TDEs ou para sair do sistema.</font></div>
  </li>
</ul>
<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123248"></a><a name="_Toc60478968"></a><a name="_Toc60478506">4.7.1.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Criação 
  Conta Autor usando o Comando de Operação  <u>Inserir</u></a></font></h4>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> da tela principal do módulo ( Figura 3 ), a opção de operação 
      Conta Autor. </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza a tela principal da opção Conta Autor. Esta tela é 
      identificada como Conta Autor e pela relação dos autores cujas contas já 
      foram criadas (figura 58).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=609 height=233 src="imagens/image061.jpg"><br clear=ALL>
  <a name="_Ref64105138"><em>   Figura </em></a><em>58- Módulo Pós-graduação - 
  Tela principal da opção  Conta Autor </em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> desta tela, o comando de operação  <u>Inserir.</u></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma tela identificada como Conta Autor, e possui um 
      campo para a localização do autor para o qual a conta vai ser criada (Figura 
      55).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, 
      neste campo, as três primeiras letras do nome do autor para o qual a conta 
      vai ser criada.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Localizar</u>, ao lado do campo.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma tela, identificada pelos campos Conta do Autor 
      e Validade da Conta, contendo dados da conta do autor e o prazo de validade 
      da mesma, informações necessárias para a criação da Conta Autor (Figura 
      56).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
      partir deste momento, o processo de criação da Conta Autor é idêntico ao 
      da seção anterior (seção 4.7.1.1).</font></div>
  </li>
</ul>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478508">4.7.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Exclusão Conta Autor</a></font></h3>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b>Atenção</b>: 
  depois de criada e enviada ao autor, uma Conta Autor <strong>não pode ser excluída. 
  </strong></font></p>
