<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong>Se 
      a instituição de afiliação estiver associada a alguma TDE,</strong> o sistema 
      disponibiliza uma tela com a seguinte mensagem: <u>Erro –003-operação  não 
      permitida! Afiliação relacionada com alguma TDE</u>.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao 
      finalizar a operação, selecione, no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> 
      principal</u> e volte à tela principal do módulo para dar seqüência às demais 
      opções.</font></div>
  </li>
</ul>
