<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478487">4.4.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Excluir</u> – Opção Afiliação</a></font></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Identifique, 
      na tela principal da opção Afiliação, o nome instituição de afiliação a 
      ser excluída.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando – posicionado abaixo do comando <u>Excluir</u>, existente 
      na janela central da tela <span style='font-family:Symbol'>-</span> correspondente 
      ao nome instituição a ser excluída.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a
name="_Toc60478488">Observe que o sistema disponibiliza uma tela com os dados 
      da instituição para a exclusão (figura 27). O sistema não permite a exclusão 
      de uma instituição de afiliação que esteja associada a alguma TDE.</a>  
      </font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=595 height=186 src="imagens/image029.jpg"><br clear=ALL>
  <em>   Figura 27- Módulo Pós-graduação, opção  Afiliação - Exclusão 
  da Instituição de  Afiliação</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Excluir</u>, existente no canto direito inferior 
      da tela.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      a instituição de afiliação não estiver associada a alguma TDE, o sistema 
      disponibiliza uma nova tela com a mensagem <u>Operação  Concluída</u>, para 
      conferência (figura 28).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=612 height=199 src="imagens/image030.jpg"><br clear=ALL>
