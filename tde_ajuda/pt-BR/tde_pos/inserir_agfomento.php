  <em>   Figura 31 – Módulo Pós-graduação, opção  Agência Fomento - Mensagem  
  inclusão Agência de   Fomento</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira os dados inseridos.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se OK, insira<i> </i>uma nova agência 
      de fomento ou selecione no <i>menu</i> desta tela, o comando <i><u>menu</u></i><u> 
      principal</u> e volte à tela principal do módulo para dar seqüência à operação 
      das demais opções;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Para incluir uma nova Agência Fomento, 
      adote os seguintes procedimentos:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, no <i>menu</i> desta tela,<i> 
      </i>o comando de operação  <u>Inserir</u>;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>O</span>bserve que o sistema 
      disponibiliza a tela para a inclusão dos dados de cadastro da  agência de 
      fomento ( Figura 30); </font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">E<span
style='font-size:10.0pt;font-family:Symbol'><span style='font:7.0pt "Times New Roman"'> 
      </span></span>xecute os mesmos procedimentos utilizados para a primeira 
      inclusão dos dados, até concluir o processo.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se não OK, proceder à correção, utilizando 
      as instruções descritas na seção Comando de Operação  Alterar da opção Agência 
      Fomento, (seção 4.5.2);</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao finalizar a operação, selecione,<i> 
      </i>no <i>menu</i> desta tela,<i> </i>o comando <i><u>menu</u></i><u> principal</u> 
      e volte à tela principal do módulo Pós-graduação, para dar seqüência à operação 
      das demais opções.</font></div>
  </li>
</ul>
