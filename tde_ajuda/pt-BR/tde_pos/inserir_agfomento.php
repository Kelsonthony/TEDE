  </span>Como operar a Opção <u>Agência Fomento</u></font></a></h2>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478490">4.5.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Inserir</u> – Opção Agência Fomento</a></font></h3>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, no <i>menu</i> desta tela, o comando de operação 
    <u>Inserir</u>.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que o sistema disponibiliza uma nova tela com os 
    campos para a inclusão dos dados da agência de fomento (figura 30).</font></li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=604 height=218 src="imagens/image032.jpg"><br clear=ALL>
  <a name="_Ref56253216"></a><a name="_Hlt57005734"></a><em>Figura 30 – Módulo 
  Pós-graduação, opção Agência Fomento - Inclusão dados da  Agência de  Fomento</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Os campos de preenchimento obrigatório, 
      ao deixarem de ser preenchidos, são solicitados automaticamente pelo sistema, 
      que emite uma mensagem de erro e ressalta estes campos em vermelho. Inclua, 
      nos campos discriminados a seguir, os dados da agência de fomento:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol'>D<span style='font:7.0pt "Times New Roman"'> 
      </span></span>igite, no campo <u>Nome</u>, preenchimento obrigatório, <strong>em 
      ordem direta,</strong> o nome completo da agência de fomento. Este campo 
      deverá conter, no máximo, 100  caracteres alfanuméricos, também podem ser 
      usados os seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  - ,  _  
      e  * ;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol'>D<span style='font:7.0pt "Times New Roman"'> 
      </span></span>igite, no campo <u>Sigla</u>, a <strong>sigla oficial da agência 
      de fomento</strong>. Este campo deverá conter, no máximo 30 caracteres alfanuméricos, 
      também podem ser usados os seguintes caracteres especiais: &amp;, ( ),  
      /,  \ ,  - ,  _  e  * ;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol'>S<span style='font:7.0pt "Times New Roman"'> 
      </span></span>elecione, no campo <u>País</u>, preenchimento obrigatório, 
      na tabela existente ao lado do campo, o país da agência de fomento;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style="font-family: Symbol; font-size: 10.0pt;">S</span><span
style='font-size:10.0pt;font-family:Symbol'><span style='font:7.0pt "Times New Roman"'> 
      </span></span>elecione, no campo <u>UF</u>, na tabela existentes ao lado 
      do campo, a unidade da federação onde está sediada a agência de fomento;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">D<span
style='font-size:10.0pt;font-family:Symbol'><span style='font:7.0pt "Times New Roman"'> 
      </span></span>igite, no campo <u>CNPJ</u>, preenchimento obrigatório, o 
      CNPJ da agência de fomento. Este campo deverá ser preenchido apenas com 
      a seqüência de algarismos, com  no máximo, 14 caracteres numéricos. Ex.: 
      33654831002341;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">D<span
style='font-size:10.0pt;font-family:Symbol'><span style='font:7.0pt "Times New Roman"'> 
      </span></span>igite, no campo <u>URL</u>, a URL da agência de fomento. EX.: 
      <a
href="http://www.qualquernome.gov.br/diretório">www.qualquernome.gov.br/diretório</a>. 
      Este campo deve conter, no máximo, 150 caracteres;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol'>C<span style='font:7.0pt "Times New Roman"'> 
      </span></span>lique no botão de comando <u>Inserir</u>, existente no canto 
      direito inferior da tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O<span
style='font-size:10.0pt;font-family:Symbol'><span style='font:7.0pt "Times New Roman"'> 
      </span></span>bserve que o sistema disponibiliza uma nova tela com a mensagem 
      <u>Operação  Concluída</u> e com os dados da agência de fomento inseridos 
      <span
style='font-family:"Courier \(W1\)"'>-</span> ressaltados por uma faixa colorida 
      <span style='font-family:"Courier \(W1\)"'>-</span> para conferência (figura 
      31).</font></div>
  </li>
</ul>
