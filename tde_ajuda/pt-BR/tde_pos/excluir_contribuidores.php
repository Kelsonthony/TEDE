<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478479">4.3.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Excluir</u><i> </i>– Opção Contribuidor</a></font></h3>
<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123227"></a><a name="_Toc60478947"></a><a name="_Toc60478480">4.3.3.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Exclusão 
  de Contribuidor</a></font></h4>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Identifique,<i> </i>na tela principal da opção Contribuidor, 
    o nome do contribuidor a ser excluído. O sistema só permite a exclusão de 
    um contribuidor quando este não estiver associado a alguma TDE.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique no botão de comando – posicionado abaixo do comando 
    <u>Excluir</u> na janela central da tela <span style='font-family:Symbol'>-</span> 
    correspondente ao nome do contribuidor a ser excluído.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a
name="_Toc60478481">O sistema disponibiliza uma tela com os dados do contribuidor 
    para a confirmação da exclusão (figura 20).</a></font></li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=594 height=232 src="imagens/image022.jpg"><br clear=ALL>
  <em>Figura 20 – Módulo Pós-graduação, opção Contribuidor - Exclusão de Contribuidor</em></font></div>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique no botão de comando <u>Excluir</u>, existente no canto 
    direito inferior da tela.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se o contribuidor não estiver relacionado com alguma TDE, 
    o sistema disponibiliza uma  tela com a mensagem <u>Operação  Concluída</u>, 
    para conferência (figura 21).</font></li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=596 height=229 src="imagens/image023.jpg"><br clear=ALL>
  <em>Figura 21 – Módulo Pós-graduação, opção Contribuidor - Mensagem confirmação  
  exclusão de contribuidor</em></font></div>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong>Se o contribuidor estiver associado a alguma TDE</strong>, 
    o sistema disponibiliza uma  tela com a seguinte mensagem: <u>Erro 004 – operação 
     não permitida! Contribuidor relacionado com alguma TDE.</u></font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Ao finalizar a operação, selecione, no <i>menu</i> desta 
    tela, o comando <i><u>menu</u></i><u> principal</u> e volte à tela principal 
    do módulo para dar seqüência às demais opções.</font></li>
</ul>
<h4><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69123228"></a><a name="_Toc60478948"></a><a name="_Toc60478482">4.3.3.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; </span>Exclusão 
  da afiliação do Contribuidor</a></font></h4>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><b>Atenção</b>: Ao excluir o contribuidor, o sistema exclui, 
  na TDE, a associação deste contribuidor com a instituição de afiliação.</font></p>
