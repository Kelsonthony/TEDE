<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478439"><span
style='color:black'>1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Introdução</a></font></h1>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
  Módulo Pós-graduação, no Sistema TEDE, tem a função de gerenciar os procedimentos 
  de inclusão das teses e dissertações produzidas no âmbito dos programas de pós-graduação 
  da instituição receptora do sistema. </font></p>
<p align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  execução dos procedimentos de inclusão das teses e dissertações, no Sistema 
  TEDE, é realizada por meio das opções de operação do módulo e por operadores 
  habilitados. Estes operadores têm a função de incluir os dados dos autores, 
  títulos das teses, papel dos contribuidores, membros das bancas examinadoras 
  etc. e também a função de criar a conta que permite o acesso do autor ao sistema, 
  para dar continuidade ao processo de inclusão ou correção dos dados e à inclusão 
  dos arquivos de texto, imagem, animação etc. que compõem sua tese ou dissertação. 
  Ao mesmo tempo, estes operadores acompanham o fluxo das teses e dissertações 
  eletrônicas (TDEs) entre os programas e os autores e entre os programas e os 
  catalogadores destas teses até a fase final da publicação.</font></p>
