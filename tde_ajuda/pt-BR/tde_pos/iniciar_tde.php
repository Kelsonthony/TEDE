<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc60478496">4.6.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Inserir</u> - Inclusão dos dados da TDE</a></font></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> da tela principal da opção, o comando de operação <u>Inserir.</u></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma nova tela, identificada como Iniciar TDE e pelo 
      campo, para a localização do autor  cuja TDE deverá ser iniciada  (figura  
      37).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=604 height=257
  src="imagens/image040.jpg"><br clear=ALL>
  <em>       Figura 37 – Módulo Pós-graduação, opção TDE - Localização do autor 
  para iniciar TDE</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, 
      neste campo, as três primeiras letras do nome do autor da TDE a ser iniciada.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>localizar</u>, ao lado do campo.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza, nesta mesma tela, o campo Nome<i>,</i> ladeado por 
      uma caixa de seleção contendo a expressão <u>Seleciona </u>(figura 38).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=604 height=229 src="imagens/image041.jpg"><br clear=ALL>
  <em>   Figura 38 – Módulo Pós-graduação, opção TDE - Seleção autor para iniciar 
  TDE</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      na seta (ao lado do termo selecionar) para abrir esta caixa, que contém 
      a lista de nome dos autores cadastrados na opção de operação Autor (seção 
      4.2).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione<i>, 
      </i>nesta lista, o nome do autor correspondente à TDE que irá ser iniciada.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma nova tela contendo um formulário com os campos 
      para a entrada dos metadados da TDE (figura 39).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=600 height=480 src="imagens/image042.gif"><br clear=ALL>
  <em>  Figura 39 – Módulo Pós-graduação, opção TDE - Entrada metadados da TDE</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Os 
      campos de preenchimento obrigatório (exceto os campos onde o dado a ser 
      informado deve ser selecionado em uma lista), ao deixarem de ser preenchidos, 
      são solicitados automaticamente pelo sistema, que emite uma mensagem de 
      erro e ressalta estes campos em vermelho. Inclua nos campos discriminados 
      a seguir os dados da TDE:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
      Selecione, no campo <u>Grau</u>, preenchimento obrigatório, o grau acadêmico 
      correspondente à tese ou dissertação defendida pelo autor. Ex.: Doutor;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>D</span>igite, no campo 
      <u>Titulação</u>, preenchimento obrigatório, a titulação correspondente 
      ao grau obtido pelo autor. Este campo deverá ser preenchido com, no máximo, 
      255 caracteres alfanuméricos, também podem ser usados os seguintes caracteres 
      especiais: &amp;, ( ),   /,  \ ,  - ,  _  e  * ;  Ex.: Doutor em Engenharia 
      Florestal;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no campo <u>Pais</u>, preenchimento obrigatório, na tabela existente ao 
      lado do campo, o país onde a tese ou dissertação foi defendida;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
      Selecione, no campo <u>UF</u>, na tabela existente ao lado do campo, a unidade 
      da federação onde a tese ou dissertação foi defendida;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
      Digite, no campo <u>Cidade</u>, preenchimento obrigatório, o nome da cidade 
      onde a tese ou dissertação foi defendida. Este campo deverá ser preenchido 
      com, no máximo, 100 caracteres alfanuméricos;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no campo <u>Data de Defesa</u>, preenchimento obrigatório,  dia, mês e ano 
      em que a tese ou dissertação foi defendida;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
      Selecione, no campo <u>Idioma</u>, preenchimento obrigatório, o idioma em 
      que a tese ou dissertação foi escrita;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, 
      no campo <u>Título</u>, preenchimento obrigatório, em ordem direta, o título 
      e subtítulo completos da tese ou dissertação. Campo de texto livre;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"> 
      Clique no botão de comando I<u>nserir</u>, existente no canto direito inferior 
      da tela;</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma nova tela, com a mensagem <u>Operação  Concluída</u> 
      e com os dados da TDE inseridos para conferência (figura 40).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=613 height=286 src="imagens/image043.jpg"><br clear=ALL>
  <a name="_Ref69110887"><em>Figura </em></a><em>40 – Módulo Pós-graduação, opção 
  TDE – Mensagem confirmação inclusão metadados da TDE</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira 
      os dados inseridos, campo a campo:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      OK, selecione, no <i>menu</i> desta tela, na opção Contribuidor, o comando 
      de operação  <u>Inserir</u>, em vermelho, para associar os contribuidores 
      à TDE, conforme descrito na seção Associação do Contribuidor à TDE ( seção 
      4.6.4 ).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      não OK, proceder à correção, utilizando as instruções descritas na seção 
      Comando de Operação Alterar, (seção 4.6.2).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span
style='font-size:11.0pt'>Caso seja necessário, neste momento, encerrar os procedimentos 
      de operação desta seção, selecione, no <i>menu</i> desta tela, o comando 
      <i><u>menu</u></i><u> principal</u> e volte à tela principal do módulo.</span></font></div>
  </li>
</ul>
