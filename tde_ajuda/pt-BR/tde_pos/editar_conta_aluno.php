    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione,<i> </i> no <i>menu</i> da tela 
      principal do módulo ( Figura 3 ) a opção Conta Autor.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que o sistema disponibiliza a 
