<h3><a name="_Toc69119023"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.6<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Finalização</u></font></a></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A opção de operação Finalização é utilizada para disponibilizar 
  a TDE ao acesso público, após o registro da mesma ser considerado totalmente 
  correto para a disponibilização, ou para retornar a TDE ao Modulo Pós-graduação 
  para correções.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Estas ações são feitas por meio de um comando de operação, 
  localizado no <i>menu</i> da tela logo abaixo da opção Finalização (figura 4). 
  Este comando aponta tanto para a disponibilização para acesso, quanto para o 
  retorno da TDE à pós-graduação, dependendo da ação a ser realizada pelo operador 
  habilitado. Ao ser acionado,  o sistema disponibiliza uma tela solicitando uma 
  confirmação da ação a ser efetivada. Após a confirmação, a TDE ou retorna à 
  pós-graduação para providências, ou é disponibilizada para o acesso público 
  e não mais pode ser alterada.</span></font></p>
<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Ref58140162">4.7<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação </a><u>Finalização</u></font></h2>
<p align="justify" style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta 
  opção é utilizada tanto para disponibilizar a TDE para o acesso público, quanto 
  para retorná-la à pós-graduação para correções.</font></p>
<h3><a name="_Ref58649793"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.7.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Envio da TDE </font></a><font size="2" face="Verdana, Arial, Helvetica, sans-serif">ao 
  Módulo Pós-Graduação para correções</font></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> desta tela, o comando de operação <u>Voltar TDE para a Pós-graduação</u>, 
      localizado abaixo da opção de operação Finalização.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza uma tela solicitando a confirmação do envio da TDE 
      para a pós-graduação (figura 17).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=575 height=312
    src="imagens/image017.jpg"><br clear=ALL>
  <em>          Figura 17 - Módulo Biblioteca, opção Finalização - Confirmação 
  envio TDE à pós-graduação</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Para 
      devolver a TDE à pós-graduação, clique no botão de comando <u>Confirmar</u>, 
      existente no canto direito da janela central da tela.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza a tela principal do Módulo Biblioteca com a 
      mensagem <u>Operação Concluída</u> e com a indicação se existe, ou não, 
      alguma TDE para a catalogação. A partir deste momento, o operador habilitado 
      deve ficar atento ao <i>e-mail</i> do sistema informando o retorno da TDE 
      do módulo pós-graduação (figura 18).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=568 height=189
    src="imagens/image018.jpg"><br clear=ALL>
  <i><span style='font-size:9.0pt;font-family:Arial'>     Figura 18 - Módulo Biblioteca, 
  opção Finalização - Mensagem confirmação envio TDE à pós-graduação</span></i></font></div>
<h3><a name="_Ref58653075"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.7.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Retorno da TDE do Módulo Pós-graduação para o Módulo Biblioteca</font></a></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Após 
      receber <i>e-mail</i> do sistema comunicando o retorno da TDE da pós-graduação, 
      o operador habilitado deverá proceder conforme as instruções a seguir:</font></div>
  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">acionar 
        a tela principal do Módulo Biblioteca;</font></li>
    </ul>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">selecionar, 
        na janela central desta tela, o nome do autor da TDE devolvida pela pós-graduação;</font></li>
    </ul>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">observar 
        que o  sistema disponibiliza uma tela com as opção do módulo, identificada 
        pelo título Catalogar na janela central da tela, para a execução das ações 
        pertinentes (figura 19).</font></li>
    </ul>
  </div>
  <ul>
  </ul>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=588 height=263
    src="imagens/image019.jpg"><br clear=ALL>
  <em>Figura 19 - Módulo Biblioteca , opção Observação – Recebimento observação 
  da pós-graduação</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Verifique, 
      no campo <u>Observação</u> no topo desta tela, se há alguma observação feita 
      pela pós-graduação e execute a ação solicitada.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Verifique 
      se a solicitação anterior feita pela biblioteca foi atendida pela pós-graduação.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      OK, adote os seguintes procedimentos:</font></div>
  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">exclua a 
        observação, utilizando as instruções contidas na seção Comando de Operação 
        Excluir - opção Observação (4.6.2);</font></li>
    </ul>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">execute 
        a opção Finalização, após a exclusão da observação, (seção 4.7).</font></li>
    </ul>
  </div>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      Não OK, adote o seguinte procedimento:</font></div>
  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">repita o 
        processo de solicitação de correção (seção 4.6) à pós-graduação até o 
        registro da TDE ser considerado correto para a disponibilização ao acesso 
        público.</font></li>
    </ul>
  </div>
</ul>
<h3><a name="_Toc69119045"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.7.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Disponibilizar TDE para o Acesso Público</font></a></h3>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, no 
    <i>menu</i> desta tela, na opção de operação Finalização, o comando de operação 
    <u>Disponibilizar para Acesso</u> (figura 20).</font></li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=600 height=258
    src="imagens/image020.jpg"><br clear=ALL>
  <em>Figura 20 - Módulo Biblioteca, opção Finalização  - Mensagem confirmação 
  disponibilização TDE para acesso público</em></font></div>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que 
    o sistema disponibiliza uma tela com a seguinte mensagem: <u>Disponibilizar 
    TDE para acesso público. Após a disponibilização esta TDE não poderá ser alterada</u><i> 
    </i>(figura 21).</font></li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=600 height=240
    src="imagens/image021.jpg"><br clear=ALL>
    <em>Figura 21 -  Módulo Biblioteca,  opção Finalizar - Confirmar disponibilização</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><strong>Para 
      disponibilizar a TDE para o acesso público</strong>, confirme no botão de 
      comando <u>Confirmar</u>, existente no canto direito inferior da tela. A 
      partir deste momento, a TDE ficará disponível na Biblioteca Virtual da Instituição 
      Receptora do Sistema para acesso, tanto pelo módulo de Busca do Sistema 
      TEDE, quanto pela BDTD à qual a Biblioteca Virtual estará integrada.</font></div>
  </li>
</ul>
