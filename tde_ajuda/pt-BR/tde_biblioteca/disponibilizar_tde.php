<h2><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como operar a Opção <u>Catalogar</u></font></h2>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69119027">4.2.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Inserir </u> – Opção Catalogar</a></font></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Nesta opção, o comando inserir já vem 
      acionado para a inserção das informações solicitadas. Os campos onde devem 
      ser inseridas as informações solicitadas vêm circundados por uma caixa retangular.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, no campo <u>Citação ABNT</u>,<i> 
      </i>a citação do autor de acordo com as normas da ABNT.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Digite, no campo <u>Número de Chamada</u>, 
      o número de chamada (número de localização física da tese ou dissertação 
      na biblioteca depositária) da TDE.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique no botão de comando <u>Alterar</u>, 
      existente no canto direito inferior da tela.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe que o sistema disponibiliza uma 
      tela com a mensagem Operação Concluída e com os dados inseridos para a conferência 
      (figura 5).</font></div>
  </li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=588 height=456
    src="imagens/image005.jpg"><br clear=ALL>
  <em>   Figura 5 - Modulo Biblioteca,  opção Catalogar - Mensagem de confirmação 
  de inclusão de dados</em></font></div>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira 
      os dados inseridos.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      OK, adote o seguinte procedimento:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione 
      o comando de operação <u>Visualizar</u> da opção <u>Arquivo</u> no <i>menu</i> 
      desta tela para executar as ações contidas nessa opção (seção 4.3.1).</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      não OK, adote o seguinte procedimento:</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span class="EstiloArial11ptJustificadoPrimeiralinha076cm"
style='font-size:10.0pt;font-family:Symbol;color:black'>E</span>xecute a correção 
      conforme descrito na seção  <u>Comando de Operação Alterar</u> da  opção 
      <u>Catalogar</u>, (seção 4.2.2).</font></div>
  </li>
</ul>
<h3><a name="_Toc69119028"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.2.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Alterar</u> – Opção Catalogar</font></a></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione, 
      no <i>menu</i> desta tela, o comando de operação <u>Alterar</u> abaixo da 
      opção Catalogar.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
      sistema disponibiliza a tela para a alteração. Os campos com as informações 
      possíveis de serem alteradas (citação ABNT e número de chamada) vêm circundados 
      por uma caixa retangular.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Faça 
      as alterações necessárias nas informações destes campos.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Clique 
      no botão de comando <u>Alterar</u>, existente no canto direito inferior 
      da tela.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza uma tela com a mensagem Operação Concluída e 
      os dados para conferência.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Confira 
      a alteração realizada.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      OK, adote o seguinte procedimento:</font></div>
  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">selecione 
        o comando de operação <u>Visualizar</u> da opção <u>Arquivo</u> no <i>menu</i> 
        desta tela para executar as ações contidas nessa opção (se&ccedil;&atilde;o 
        4.3.1).</font></li>
    </ul>
  </div>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se 
      não OK, adote o seguinte procedimento:</font></div>
  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">execute 
        novamente a correção conforme descrito no item <u>Comando de Operação 
        Alterar</u> desta opção (seção 4.2.2).</font></li>
    </ul>
  </div>
</ul>
