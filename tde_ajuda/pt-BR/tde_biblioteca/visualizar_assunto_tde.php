<h3><a name="_Toc69119020"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Assunto</u></font></a></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A opção de operação Assunto é utilizada para visualizar e conferir 
  o conjunto de termos, palavras-chave e/ou descritores, inseridos pelo autor, 
  que representam o assunto tratado na tese ou dissertação. A catalogação poderá 
  acrescentar outros descritores que permitam o acesso preciso ao conteúdo da 
  TDE. Ao mesmo tempo, deverá, se for o caso, indicar o esquema (instrumento utilizado 
  na indexação,  como tesauros, cabeçalho de assunto etc.) e o idioma utilizados 
  no tratamento da informação acrescentada.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A opção de operação Assunto possui uma tela específica para 
  a ação inserir ( <span style='font-size:12.0pt;font-family:"Times New Roman"'>figura 
  7</span> )  e outra tela para as ações alterar e excluir ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 9</span> ). As ações 
  <u>inserir</u>, <u>alterar</u> e <u>excluir</u> só deverão ser efetivadas após 
  o operador habilitado haver conferido as palavras-chave inseridas pelo autor, 
  ter lido o conteúdo da TDE e observado a necessidade de complementação, alteração 
  ou exclusão da informação. </span></font></p>
