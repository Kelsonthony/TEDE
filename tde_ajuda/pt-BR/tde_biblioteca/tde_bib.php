<h1><a name="_Toc69119017"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opções de Operação do Módulo Biblioteca</font></a></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>O Módulo Biblioteca dispõe de um conjunto de <u>opções de operação</u> 
  que permite, aos operadores habilitados, visualizar os metadados inseridos pela 
  pós-graduação e autor, assim como catalogar a TDE, isto é, inserir, alterar 
  ou excluir alguns metadados de responsabilidade da biblioteca, abrir os arquivos 
  da mesma, conhecer o seu conteúdo  para complementar as palavras-chaves e/ou 
  descritores inseridos pelo autor e fazer a cobertura da TDE. Ao mesmo tempo 
  em que permite aos operadores habilitados fazer uma revisão geral da TDE e, 
  ao detectar erros, solicitar possíveis correções à pós-graduação, por meio da 
  opção de operação Observação. Finalmente, quando este fluxo for completado e 
  o registro da TDE for considerado totalmente correto, a mesma será disponibilizada, 
  neste módulo, para o acesso público.   </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>O acesso a estas opção de operação é feita após a entrada no 
  módulo, por meio da tela principal do Módulo Biblioteca. Esta tela é identificada 
  pelo <i>menu</i>, existente na lateral esquerda da tela, contendo o nome da 
  biblioteca responsável pela catalogação da TDE e pela janela central da tela 
  identificada pelo nome do programa de pós-graduação com a relação  dos autores 
  cujas TDEs se encontram  disponíveis para a catalogação (<span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 4</span>).</span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>Após 
  a seleção do autor cuja TDE irá ser catalogada, o sistema apresenta uma tela 
  contendo um <i>menu</i> com as opção de operação que devem ser executadas na 
  seqüência apresentada no <i>menu</i>. Estas opções são executadas por meio dos 
  comandos de operação existentes no <i>menu</i> desta tela, abaixo de cada opção. 
  O sistema apresenta uma tela específica para a ação a ser executada, após a 
  seleção de cada um destes comandos. As opções de operação do Módulo Biblioteca 
  estão discriminadas a seguir.</span></font></p>
<h3 style='text-align:justify'><a name="_Toc69119018"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>Catalogar</u></font></a></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A opção de operação Catalogar é utilizada para inserir, alterar 
  e visualizar a citação do autor conforme as normas da ABNT e o número de chamada 
  da TDE dentro das normas bibliográficas. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'> Ao disponibilizar a tela com os campos (circundados por uma 
  caixa retangular) para a inserção destes dados, o sistema disponibiliza também 
  o <i>menu</i> com as opções de operação do módulo, a visualização de alguns 
  metadados e o acesso aos arquivos que compõem a TDE. Este conjunto de informação  
  deve ser revisado  pela biblioteca para a total confirmação da integridade dos 
  metadados e conteúdo da TDE, antes da publicação eletrônica (<span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 4</span>).</span></font></p>
