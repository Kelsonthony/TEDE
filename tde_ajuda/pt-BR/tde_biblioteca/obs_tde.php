  </span>Comando de Operação <u>Inserir</u> – Opção Observação</a></font></h3>
<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Selecione<i>,</i> 
      no <i>menu</i> da tela com as opções de operação do módulo, o comando de 
      operação <u>Inserir</u>, existente abaixo da opção Observação.</font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Observe 
      que o sistema disponibiliza a tela principal da opção identificada na janela 
      central da tela, pelo campo <u>Observação</u> e por algumas informações 
      esclarecedoras para a execução da ação solicitada neste campo (figura 14).</font></div>
  </li>
</ul>
