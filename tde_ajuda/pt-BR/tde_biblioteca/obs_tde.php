  </span>Opção de Operação <u>Observação</u></font></h2>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span class="EstiloArial11ptJustificadoPrimeiralinha076cm" style='font-size:
10.0pt;font-family:Symbol;color:black'><span style='font:7.0pt "Times New Roman"'>&nbsp; 
    </span></span>Se o operador habilitado detectou problemas com arquivos ou 
    a necessidade de correções nos metadados da TDE, deverá devolvê-la à pós-graduação 
    para as correções devidas. Neste caso, proceder conforme instruções a seguir.</font></li>
</ul>
<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Ref58145753">4.6.1<span
