<h3><a name="_Toc69119022"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.5<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Observação</u></font></a></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A opção de operação Observação é utilizada para o registro 
  da comunicação que viabiliza o fluxo da TDE entre o Módulo Biblioteca e o Módulo 
  Pós-graduação. Por meio desta opção, o operador habilitado da biblioteca se 
  comunica com a pós-graduação, para solicitar ou receber comunicação sobre possíveis 
  correções na TDE. Para a execução de uma solicitação de correção na TDE à pós-graduação, 
  utiliza-se o comando de operação <u>Inserir</u>, desta opção de operação. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A opção Observação possui uma tela específica para a ação <u>Inserir</u> 
   ( <span style='font-size:12.0pt;font-family:"Times New Roman"'>figura 14</span> 
  ) e outra para a ação</span><span
style='font-size:11.0pt;font-family:Arial'> <u>Excluir</u>  ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 16</span> ). A ação 
  <u>Inserir</u> só deverá ser efetivada após o operador habilitado ter feito 
  a sua revisão e detectado a necessidade do retorno da TDE à pós-graduação. A 
  ação <u>Excluir</u> deverá ocorrer, antes de disponibilizar a TDE para o acesso 
  público, sempre que a TDE tenha uma observação impressa no <u>campo Observação</u>.</span></font></p>
