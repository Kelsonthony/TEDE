<h3><a name="_Toc69119021"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.4<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Cobertura</u></font></a></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A opção de operação Cobertura é utilizada para realizar a cobertura 
  da TDE, isto é, inserir, alterar ou excluir o conjunto de descritores que representam 
  a cobertura espacial, geográfica e ou temporal da tese ou dissertação, e o idioma 
  em que foi inserido esta informação.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A Opção Cobertura possui uma tela específica para a ação Inserir 
  ( <span style='font-size:12.0pt;font-family:"Times New Roman"'>figura 11</span> 
  ) e outra tela para as ações</span><span
style='font-size:11.0pt;font-family:Arial'> alterar e excluir ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 13</span> ). As 
  ações <u>inserir</u>, <u>alterar</u> e <u>excluir</u> só deverão ser efetivadas 
  após o operador habilitado ter lido o conteúdo da TDE e observado a necessidade 
  de inserção, alteração ou exclusão da informação. </span></font></p>
