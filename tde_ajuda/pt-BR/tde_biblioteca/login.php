<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69119015">1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Introdução</a></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>O módulo Biblioteca, no Sistema TEDE, tem a função de gerenciar 
  os procedimentos finais para a publicação eletrônica das teses e dissertações 
  produzidas no âmbito dos programas de pós-graduação cadastrados no sistema TEDE. 
  Estes procedimentos são executados por operadores habilitados para tal, pelas 
  bibliotecas depositárias destas teses ou dissertações. Estes operadores têm 
  a função de inserir os metadados bibliográficos para a catalogação, além de 
  fazer uma revisão geral dos metadados inseridos na TDE pela pós-graduação e 
  pelo autor e disponibilizá-la para o acesso público.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'> A catalogação de uma tese ou dissertação  é realizada no módulo 
  Biblioteca,  somente na fase final do processo de submissão da TDE. Isto é, 
  após o encerramento do fluxo da TDE entre a  pós-graduação e o autor e quando 
  os dados e arquivos desta TDE forem considerados corretos para a publicação 
  eletrônica e acesso público. Nesta fase, o operador habilitado, ao fazer a revisão 
  geral da TDE, poderá ainda detectar algum erro. Neste caso, deverá devolvê-la 
  à pós-graduação para as devidas correções. Somente quando o registro da TDE 
  for considerado totalmente correto é que será disponibilizado para o acesso 
  público.   </span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>A 
  tela principal do módulo Biblioteca  apresenta, na lateral esquerda da tela, 
  um <i>menu</i> com o nome da biblioteca depositária, com os <u>comandos de operação</u> 
  adequados à execução das ações solicitadas na <u>opção de operação</u> que está 
  sendo executada e uma janela central contendo o nome do programa de pós-graduação, 
  com a relação do nome dos autores cujas TDEs serão catalogadas ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 3</span> ).</span></font></p>
<h1><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69119016">2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Acesso e Operação do Módulo Biblioteca</a></font></h1>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>O acesso ao módulo Biblioteca é feito acionando-se o nome Biblioteca 
  na <u>barra de <i>menu</i></u>, disponível na tela principal do sistema (<span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 1</span>). Ao ser 
  acionado, o sistema apresenta a <u>tela de acesso ao módulo</u> (<span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 2</span>),  solicitando 
  uma <u>chave de identificação</u> – <i>login</i> da conta, que deve ser confirmado 
  por uma senha. Esta chave de identificação, que permite ao <u>operador habilitado</u> 
  acessar o módulo Biblioteca, é um dispositivo de segurança criado no módulo 
  Administração para os operadores de cada módulo. </span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>Após a entrada no módulo, o sistema disponibiliza a sua tela 
  principal  com a relação do nome dos autores cujas TDEs estão  disponíveis para 
  a catalogação. Ao selecionar o nome do autor nesta tela, o sistema disponibiliza 
  outra tela com o conjunto de opções de operação disponíveis para execução (<span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 4</span>).</span></font></p>
<p class=MsoBodyText><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>A 
  operação do módulo é feita utilizando este conjunto de opções de operação, com 
  funções específicas, que são acionadas ao longo da operação do módulo, por meio 
  dos comandos de operação existentes no <i>menu</i>, posicionados logo abaixo 
  do nome de cada opção de operação. </span></font></p>
