  </li>
  <div align="justify"> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">se houver 
        necessidade de complementação do assunto da TDE, proceder à execução da 
        opção Assunto (seção 4.4);</font></li>
    </ul>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">se houver 
        cobertura para o assunto da TDE, proceder à execução da opção Cobertura  
        (seção 4.5);</font></li>
    </ul>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">se foi detectado 
        problema com algum arquivo, proceder à execução da opção Observação (seção 
        4.6);</font></li>
    </ul>
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">se nenhuma 
        destas ações precisar ser executada, proceder à execução da opção Finalização 
        (seção 4.7).</font></li>
    </ul>
