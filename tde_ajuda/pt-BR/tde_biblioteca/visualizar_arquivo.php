      para disponibilizar o arquivo.</span></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>Analise 
      o conteúdo de cada arquivo. Após a realização desta análise poderão ocorrer 
      as seguintes situações:  1)  o operador habilitado poderá complementar os 
      descritores sobre o Assunto da TDE;  2) o operador poderá acrescentar os 
      descritores que representam a cobertura espacial, geográfica e ou temporal 
      da TDE;  3) o operador poderá detectar problemas com os arquivos ou qualquer 
      falha que impossibilite ou desqualifique a TDE para a publicação eletrônica; 
      4) nenhuma destas ações precisa ser executada.</span></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>Feche 
