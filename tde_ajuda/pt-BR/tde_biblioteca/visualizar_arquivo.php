<ul>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>Selecione, 
      no <i>menu</i> desta tela  o comando de operação <u>Visualizar</u> existente 
      abaixo da opção Arquivo.</span></font></div>
  </li>
  <li> 
    <div align="justify"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;font-family:Arial'>O 
      sistema disponibiliza uma tela com as informações das TDEs disponíveis  
      para o <i>download</i> do arquivo (figura 6).</span></font></div>
  </li>
</ul>
