<h3><a name="_Toc69119019"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">3.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Arquivo</u></font></a></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A opção de operação Arquivo permite ao operador habilitado 
  para operar o Módulo Biblioteca fazer <i>download</i> dos arquivos que compõem 
  a TDE e examinar o seu conteúdo para a efetivação da catalogação. Isto é, inserir 
  a citação do autor conforme as normas da ABNT,  o número de chamada  e os descritores 
  da cobertura da TDE. Ao mesmo tempo, permite ao operador habilitado fazer examinar 
  a integridade dos arquivos da TDE e, se for o caso, encaminhá-la à pós-graduação 
  para as providências cabíveis.</span></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><span style='font-size:11.0pt;
font-family:Arial'>A tela principal da opção é identificada na janela central 
  da tela como Arquivo, por dados que identificam o arquivo e pelo <i>link download,</i> 
  que permite a visualização dos arquivos inseridos na TDE  ( <span
style='font-size:12.0pt;font-family:"Times New Roman"'>figura 6</span>  )</span></font></p>
<h2><a name="_Toc69119029"><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
