    </span></span> <span style='font-size:12.0pt'>Introdução</span></a></font></p>
  <p><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O Módulo Contato tem a função de facilitar a comunicação entre 
    os usuários do Sistema TEDE, na Instituição. Neste módulo, estes usuários 
    têm acesso aos nomes dos departamentos, programas, operadores habilitados 
    para operar os módulos do sistema e demais pessoas envolvidas na operação 
    do Sistema TEDE. </font></p>
