    Contato</span></a></font></p>
  <p><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O acesso ao Módulo Contato é feito acionando-se o nome <u>Contato</u> 
    na <u>barra de <i>menu</i></u> disponível na tela principal do sistema (figura 
    1). Após o módulo ser acionado, o sistema   disponibiliza uma tela identificada 
    como Contato,  que permite ao usuário do módulo se comunicar com os demais 
    usuários do sistema, por meio do Cadastro de Contatos do Sistema TEDE, que 
    se encontra acessível ao  abrir-se a janela <u>Seleciona</u>, existente na 
    tela de operação do Módulo Contato (figura 2). Deste cadastro, constam apenas 
    os contatos cadastrados no Módulo Administração do Sistema TEDE.</font></p>
