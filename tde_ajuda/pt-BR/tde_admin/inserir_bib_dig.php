<h3 style='text-align:justify'><font size="2"><a name="_Toc69179218"><strong>4.3.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Inserir</u> - Opção Biblioteca Digital</strong></a></font></h3>
</font> 
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Inclua, 
    nesta tela, os dados da biblioteca digital. Os campos de preenchimento obrigatório, 
    ao deixarem de ser preenchidos, são solicitados automaticamente pelo sistema, 
    que emite uma mensagem de erro e ressalta estes campos em vermelho:</font></li> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">digite, 
      no campo <u>Nome</u>, preenchimento obrigatório, <strong>em ordem direta</strong>, 
      o nome da biblioteca digital da instituição. Este campo deverá conter, no 
      máximo, 100  caracteres alfanuméricos, também podem ser usados os seguintes 
      caracteres especiais: &amp;, ( ),  /,  \ ,  - ,  _  e  * ;</font></li>
  </ul> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">digite, 
      no campo <u>Sigla</u>, preenchimento obrigatório, <strong>a sigla oficial</strong> 
      da biblioteca digital. Este campo deverá conter, no máximo, 30 caracteres 
      alfanuméricos, também podem ser usados os seguintes caracteres especiais: 
      &amp;, ( ),  /,  \ ,  - ,  _  e  * ;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">digite, 
      no campo <u>URL</u>, preenchimento obrigatório, a URL da biblioteca digital. 
      Ex.:  <a href="http://www.qualquernome.gov.br/diretório">www.qualquernome.gov.br/diretório</a><a
name="_Hlt49323896"></a><a name="_Hlt49323899"></a>. Este campo deverá conter, 
      no máximo, 150 caracteres; </font></li>
  </ul> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">clique 
      no botão de comando <u>Inserir</u>, existente no canto direito inferior 
      da janela;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;" align="left">observe 
      que o sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u> 
      e com os dados inseridos para conferência (figura 8).</font></li>
  </ul>
</ul>
