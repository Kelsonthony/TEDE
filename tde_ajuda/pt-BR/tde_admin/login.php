<p class=EstiloTtulo111pt><font size="2"><strong>1 Introdução</strong></font></p>
<p style='text-align:justify'><font size="2">O Módulo Administração, dentro do 
  sistema TEDE, inicia a seqüência de operação deste sistema, por possuir as funções 
  de cadastramento da instituição receptora do sistema e suas unidades, as funções 
  de gerenciamento das contas de acesso ao sistema e de sua customização de acordo 
  com as características da instituição receptora. Estas funções são executadas 
  por meio de ferramentas específicas, denominadas opções de operação, as quais 
  estão agrupadas no <i>menu </i>da tela principal do módulo de acordo com as 
  funções que executam.  Esta tela funciona como um roteiro das ações a serem 
  executadas pelos operadores habilitados para a operação do módulo.</font></p>
<p style='text-align:justify'><font size="2">A tela principal do Módulo Administração 
  é identificada como administração do sistema e pelo <i>menu</i> (lateral esquerda 
  da tela) contendo os nomes das opções de operação, agrupadas pelas funções que 
  executam, na seqüência em que se apresentam ( figura 11).</font></p>
</font> 
<p class=EstiloTtulo111pt><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179194"><strong>2 
  Acesso e Operação do Módulo Administração</strong></a></font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
  acesso ao Módulo Administração é feito por meio de uma barra de <i>menu, </i> disponível 
  na tela principal do sistema, que, ao ser acionada, solicita uma chave de identificação 
  <span
style='font-family:Symbol'>-</span> <i>login</i> da conta – que deve ser confirmada 
  por uma senha, ambos são valores inicialmente atribuídos pelo sistema e, como 
  medida de segurança, devem ser trocados por um operador habilitado da instituição 
  receptora do sistema.</font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  operação do módulo é feita por meio de um conjunto de opções de operação que 
  são acionadas no <i>menu</i> da tela principal do módulo ( figura 11).</font></p>
<font face="Verdana, Arial, Helvetica, sans-serif">
