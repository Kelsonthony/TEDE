    para vínculo ao programa de pós-graduação. O cadastramento é realizado na 
    seção <u>Como operar a opção Biblioteca Depositária</u> (ver seção 1.7). O 
    processo para selecionar uma nova biblioteca é realizado na seção <u>Como 
