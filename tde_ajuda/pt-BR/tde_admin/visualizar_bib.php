  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="text-align:justify">Na 
    operação da opção Biblioteca Depositária, não existe a possibilidade de exclusão 
