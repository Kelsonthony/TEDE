<h3><font size="2"><a name="_Toc69179199"><strong>3.1.3<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Biblioteca Depositária</u></strong></a></font></h3>
<p style='text-align:justify'><font size="2">A opção Biblioteca Depositária é 
  utilizada para cadastrar os dados que identificam a(s) biblioteca(s) depositária(s), 
  repositório físico, das TDEs produzidas nos diversos programas de pós-graduação 
  da instituição.</font></p>
<p style='text-align:justify'><font size="2">A tela principal da opção é identificada 
  como Biblioteca Depositária, no <i>menu </i> contendo os comandos   de operação 
   da opção e na janela central da tela que mostra o conjunto de dados cadastrados 
  na opção (figura 12).  </font></p>
<p style='text-align:justify'><font size="2">Ao serem cadastradas, as bibliotecas 
  depositárias recebem o <i>status</i> <u>Ativa</u>. Isto indica que estão acessíveis 
  para serem associadas aos programas de pós-graduação responsáveis pela publicação 
  das TDEs do sistema TEDE. A alteração de <i>status</i> para <u>Inativa</u> só 
  poderá ocorrer quando a biblioteca depositária não mais for depositária das 
  TDEs registradas no sistema. </font></p>
</font>
