<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179210"><strong>3.3.3<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <i><u>E-mail </u></i><u>Geral</u></strong></a></font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
  sistema TEDE é distribuído para as instituições receptoras com o <i>e-mail</i> 
  padrão do sistema <span
style='font-size:10.0pt'>(email@email.br)</span>. A instituição receptora deverá 
  criar uma conta de <i>e-mail</i> para <u>substituir</u> este <i>e-mail</i> padrão 
  distribuído com o Sistema TEDE. Esta opção é utilizada para alterar este <i>e-mail,</i> 
  que tem a função de centralizar o fluxo de todos os <i>e-mail</i> (endereços 
  errados, <i>e-mail</i> não encontrado etc.) entre os operadores habilitados 
  que executam as atividades de operação do sistema.</font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  tela  principal da opção é identificada como <i>E-mail</i> Geral<i>,</i>  no 
  <i>menu </i> contendo os comando   de operação  da opção e na janela central 
  da tela, que contém um conjunto de instruções para a troca do servidor de <i>e-mail</i> 
  do sistema ( figura 65 ). </font></p>
