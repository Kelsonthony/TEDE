<h3 style='text-align:justify'><a name="_Toc69179229">4.6.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Inserir </u> –  Opção Contato</a></h3>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Selecione, no <i>menu </i> desta tela (figura 25), o comando de 
  operação  <u>Inserir</u>.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>O sistema disponibiliza uma nova tela para a inclusão dos dados 
  do contato (figura 27).</p>
<img width=599 height=230
    src="imagens/image027.jpg"><br clear=ALL>
<p class=EstiloLegendaJustificadoPrimeiralinha06cm><a name="_Ref52340237"></a><a
name="_Hlt56222585"></a>Figura 27 – Módulo Administração - Opção Contato  – Inclusão  
  dados do contato</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Inclua, nesta tela, nos campos correspondentes, os dados de identificação 
  de cada setor da instituição receptora do sistema TEDE, onde os operadores habilitados 
  deste sistema possam tirar suas dúvidas ou solicitar a solução para possíveis 
  problemas. Os campos de preenchimento obrigatório, ao deixarem de ser preenchidos, 
  são solicitados automaticamente pelo sistema, que emite uma mensagem de erro 
  e ressalta estes campos em vermelho:</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>inclua, no campo <u>Nome</u>, preenchimento obrigatório, o nome 
  do setor com o qual o operador habilitado fará contato. Este campo deverá conter, 
  no máximo, 100 caracteres alfanuméricos. Ex.: Secretaria Geral do Curso de Pós-Graduação 
  em Engenharia da Produção;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>inclua, no campo <i><u>E-mail</u></i>, preenchimento obrigatório, 
  o <i>e-mail</i> para contato com o setor. Este campo deverá conter, no máximo, 
  150 caracteres alfanuméricos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify;'><span style='font-size:10.0pt;
font-family:Symbol;color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>faça, no campo <u>Descrição</u>, uma descrição sucinta das funções 
  exercidas pelo contato cadastrado no sistema. Ex.: responsável pelo contato 
  “fulano de tal”, secretário do Curso de Pós-Graduação em Engenharia de Produção;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>selecione, ao lado do campo <i><u>Status</u></i> a opção <u>Disponível 
  Não</u>, caso não aceite a escolha padrão do sistema (Disponível Sim):</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>a opção <u>Sim</u> indica que o contato cadastrado ficará disponível 
  para uso no Módulo Contato;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>a opção <u>Não</u> indica que o contato cadastrado não será disponibilizado 
  para uso no Módulo Contato.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>clique no botão de comando <u>Inserir</u>, existente no canto 
  direito inferior da janela;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza uma nova tela (tela principal 
  da opção) com a mensagem <u>Operação  Concluída</u> e com os dados inseridos 
  <span
style='font-family:"Courier New"'>-</span> ressaltados por uma faixa colorida 
  <span
style='font-family:"Courier New"'>-</span> para conferência (figura 28). O resultado 
  da inclusão no campo <i>Status </i> só aparece nesta tela  quando a opção selecionada 
  for <u>Disponível Não</u>.</p>
<img width=602 height=187
    src="imagens/image028.jpg"><br clear=ALL>
<p class=EstiloLegendaJustificadoPrimeiralinha06cm>Figura 28 – Módulo <span style='font-style:normal'>Pós-graduação, 
  </span> opção  Contato  - Mensagem confirmação inclusão  </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Em caso de alteração, utilize as instruções descritas na seção 
  Comando de Operação Alterar, descritas a seguir (seção 4.6.2).</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Para incluir um novo Contato, adote os seguintes procedimentos:</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>selecione, nesta tela (figura 28), o comando  <u>Inserir</u>;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza a tela  para a inclusão de 
  um novo contato (figura 27); </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>execute os mesmos procedimentos utilizados para a primeira inclusão 
  dos dados e para as alterações, até concluir o processo.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Ao finalizar a operação, selecione, no <i>menu </i> desta tela, 
  o comando <i><u>menu</u></i><u> principal</u> e volte à tela principal do módulo 
  Administração, para dar seqüência às demais opções.</p>
