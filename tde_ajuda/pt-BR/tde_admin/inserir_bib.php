<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179221"></a><a name="_Ref52167796"><strong>4.4.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Inserir</u></strong></a> <strong>– 
  Opção Biblioteca Depositária</strong></font></h3>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Selecione, no <i>menu </i> da 
    tela  principal da opção, o comando <u>Inserir</u>.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Observe que o sistema disponibiliza 
    uma nova tela para a inclusão dos dados da biblioteca depositária (figura 
    14).</font></li>
</ul>
<div align="center"><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><img width=588 height=175
    src="imagens/image014.jpg"></font></div>
<p align="center" class=MsoCaption style='text-align:justify;'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a
name="_Ref52331044"></a><a name="_Ref56220495"></a><a name="_Hlt55987162"></a> 
  <em> Figura 14 – Módulo Administração, opção biblioteca depositária 
  - Inclusão dados biblioteca depositária</em></font></p>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Inclua, 
    nestes campos, os dados da biblioteca depositária. Os campos de preenchimento 
    obrigatório ao deixarem de ser preenchidos, são solicitados automaticamente 
    pelo sistema, que emite uma mensagem de erro e ressalta estes campos em vermelho:</font></li> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">digite, 
      no campo <u>Nome</u>, preenchimento obrigatório, o nome da biblioteca depositária. 
      Este campo deverá conter, no máximo, 100 caracteres alfanuméricos, também 
      podem ser usados os seguintes caracteres especiais: &amp;, ( ),  /,  \ ,  
      - ,  _  e  * ;</font></li>
  </ul> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">digite, 
      no campo <u>Sigla</u>, preenchimento obrigatório, a sigla oficial da biblioteca 
      depositária. Este campo deverá conter, no máximo, 30 caracteres alfanuméricos, 
      também podem ser usados os seguintes caracteres especiais: &amp;, ( ),  
      /,  \ ,  - ,  _  e  *; </font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">digite, 
      no campo <u>URL</u>, a URL da biblioteca depositária. Por exemplo: <a href="http://www.qualquernome.gov.br/diretório">www.nome.gov.br/diretório</a><a
name="_Hlt51469702"></a><a name="_Hlt51469708"></a>. Este campo deverá conter, 
      no máximo, 150 caracteres alfanuméricos, também podem ser usados os seguintes 
      caracteres especiais: &amp;, ( ),   /,  \ ,  - ,  _  e  * .</font></li>
  </ul> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">clique 
      no botão de comando <u>Inserir</u>, existente no canto direito inferior 
      da janela;</font></li>
  </ul> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">observe 
      que o sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u> 
      e com os dados inseridos – ressaltados em uma faixa colorida - para conferência 
      (figura 15). <a name="_Ref59511826"></a></font></li>
  </ul>
</ul>
