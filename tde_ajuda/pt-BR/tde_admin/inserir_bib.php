  – Mensagem confirmação Inclusão</em></font></p>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Confira os dados inseridos.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">E</font><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">m 
    caso de alteração, proceder à correção utilizando as instruções contidas na 
    seção <u>Comando de Operação Alterar</u>, (seção 4.4.2).</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Para 
    incluir uma nova biblioteca depositária, adote os seguintes procedimentos:</font></li> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecione, 
      nesta tela  (figura 15), o comando de operação <u>Inserir</u>;</font></li>
  </ul> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">observe 
      que o sistema disponibiliza a tela (figura 14) para a inclusão da nova biblioteca 
      depositária; </font></li>
  </ul> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">execute 
      os mesmos procedimentos utilizados para a primeira inclusão dos dados e 
      para as alterações, até concluir o processo.</font></li>
  </ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Ao finalizar a operação da 
    opção, selecione, no <i>menu </i> desta tela, o comando <i><u>menu </u></i><u> principal</u> 
    e volte à tela  principal do Módulo Administração, para dar seqüência às demais 
    opções de operação.</font></li>
</ul>
