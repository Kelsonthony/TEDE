<p style='text-align:justify'><font size="2">As funções de cada opção são executadas 
  por meio dos comandos  de operação existentes nos <i>menus</i> de suas telas. 
  No processo de operação destas funções, após a execução de uma ação <span
style='font-family:Symbol'>-</span> acionada por um botão de comando <span
style='font-family:Symbol'>-</span>, o sistema apresenta nova tela, para a execução 
  de uma nova ação, ou emite uma mensagem confirmando a ação ou indicando erro. 
  A seqüência destas telas é exemplificada neste manual, no decorrer das descrições 
  contidas em cada seção,  com a figura e legenda das mesmas. </font></p>
