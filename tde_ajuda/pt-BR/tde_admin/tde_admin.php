<p class=EstiloTtulo111pt><font size="4"><strong><font size="2">3 Opções de Operação 
  do Módulo Administração</font></strong></font></p>
<p style='text-align:justify'><font size="2">Cada opção de operação deste módulo 
  tem uma denominação relacionada com a função específica que realiza e é executada 
  por meio dos comandos  de operação existentes no <i>menu</i> de cada tela. O 
  conjunto destas opções está relacionado, em ordem seqüencial de operação, no 
  <i>menu</i> da tela  principal do módulo. Elas devem ser ativadas nesta ordem. 
  Este conjunto de opções está dividido em três grupos <span style='font-family:"Courier New"'>-</span> 
  Cadastro, Contas e Customização <span style='font-family:"Courier New"'>-</span> 
  descritos a seguir.</font></p>
<p style='text-align:justify'><font size="2">As opções de operação do grupo Cadastro, 
  a seguir discriminadas, têm a função de registrar os dados da instituição receptora 
  do sistema TEDE, de suas unidades e dos usuários envolvidos no processo de operacionalização 
  do mesmo. </font></p>
</font> 
