<h3 style='text-align:justify'><a name="_Toc69179237">4.8.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Inserir</u> – Opção Conta Catalogador</a></h3>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Selecione, no <i>menu </i> desta tela (figura 41), o comando de 
  operação  <u>Inserir</u>.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Observe que o sistema disponibiliza a tela  para a inclusão dos 
  dados do operador habilitado para a conta catalogador (figura 42).</p>
<img width=607 height=228
    src="imagens/image042.jpg"><br clear=ALL>
<p class=MsoCaption style='text-align:justify'><a name="_Ref52353775"></a><a
name="_Hlt56223690"></a>Figura 42 – Módulo Administração, opção Conta Catalogador 
  – Inclusão dados da conta catalogador</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Inclua, nesta tela, nos campos apropriados, os dados do operador 
  habilitado para a conta catalogador. Os campos de preenchimento obrigatório, 
  ao deixarem de ser preenchidos, são solicitados automaticamente pelo sistema, 
  que emite uma mensagem de erro e ressalta estes campos em vermelho:</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>selecione, no campo <u>Biblioteca</u>, preenchimento obrigatório, 
  na janela ao lado do campo, a biblioteca depositária à qual a conta ficará vinculada;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <u>Nome</u>, preenchimento obrigatório, o nome 
  do operador habilitado, para o qual está sendo criada a conta. Este campo deverá 
  conter, no máximo, 100 caracteres alfabéticos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <i><u>Login</u></i>, preenchimento obrigatório, 
  o <i>login</i> criado para o operador habilitado. Este campo pode conter qualquer 
  palavra com, no máximo, 50 caracteres alfanuméricos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <u>Senha</u>, preenchimento obrigatório, a senha 
  de acesso criada para o operador habilitado. Este campo pode conter, no máximo, 
  30 caracteres alfanuméricos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <u>Confirmar Senha</u>, preenchimento obrigatório, 
  novamente a mesma senha de acesso criada para o operador habilitado. Este campo 
  pode conter, no máximo, 30 caracteres alfanuméricos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <i><u>E-mail</u></i>, preenchimento obrigatório, 
  o <i>e-mail</i> do operador habilitado. Este campo pode conter, no máximo, 100 
  caracteres no formato utilizado para o padrão <i>e-mail. </i>Ex.: nome@nome.com.br;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>para que o operador habilitado cadastrado tenha acesso ao Módulo 
  que vai operar, mantenha o campo <i>Status</i> com a opção <u>Bloquear Não</u> 
  acionada; </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>clique no botão de comando <u>Inserir</u>, existente no canto 
  direito inferior da janela;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza uma nova tela    com a mensagem 
  <u>Operação  Concluída</u> e com os dados inseridos – ressaltados por uma faixa 
  colorida – para conferência (figura 43). </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify;'><b>Atenção</b>: o resultado da inclusão no campo <i>Status,</i> 
  representado pelo símbolo de um cadeado, só aparece nesta tela quando a opção 
  selecionada for  <u>Bloqueado Sim.</u> </p>
<img width=594 height=191
    src="imagens/image043.jpg"><br clear=ALL>
<p class=MsoCaption style='text-align:justify;'>Figura 43 – Módulo Administração, 
   opção  Conta Catalogador  -  Mensagem de inclusão<span
style='font-style:normal'>  </span></p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Em caso de alteração, utilize as instruções descritas na seção 
  Comando de Operação Alterar, descritas a seguir (seção 4.8.2).</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Em caso de inclusão de uma nova conta, adote os seguintes procedimentos:</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>selecione, nesta tela, o comando <u>Inserir</u>;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza uma tela  para a inclusão 
  da nova conta (figura 42); </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>execute os mesmos procedimentos utilizados para a primeira inclusão 
  dos dados e para as alterações, até concluir o processo.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Ao finalizar a operação da opção, selecione, no <i>menu </i> desta 
  tela, o comando <i><u>menu </u></i><u> principal</u> e volte à tela principal 
  do módulo Administração, para dar seqüência às demais opções.</p>
