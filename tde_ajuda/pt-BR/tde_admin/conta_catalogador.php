  </span></span>Selecione, no <i>menu </i> da tela principal do Módulo Administração 
  (figura 11),  a opção de operação Contas Catalogador. </font></p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><font size="2"><span
