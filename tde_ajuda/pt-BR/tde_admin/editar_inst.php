  </span>Como operar a opção <u>Instituição do Sistema</u></font></h2>
<font face="Verdana, Arial, Helvetica, sans-serif">
<h3 style='text-align:justify'><font size="2"><a name="_Toc69179216">4.2.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Alterar</u> – Opção Instituição do Sistema</a></font></h3>
<p style='text-align:justify'><font size="2">A ação Alterar pode ser feita em 
  dois momentos, logo após a inclusão, ou posteriormente.</font></p>
</font> 
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Se 
    o operador for executar a ação logo após a inclusão, deve adotar o seguinte 
    procedimento:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecionar, 
      na tela com a mensagem de confirmação de inclusão (figura 4), o comando 
      de operação  <u>Alterar</u>;</font></li>
  </ul>
</ul>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloJustificado">Se 
    o operador for executar a ação posteriormente, deve adotar o seguinte procedimento:</font> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecionar, 
        no <i>menu </i> da tela principal do módulo ( figura 11 ), a opção <u>instituição 
        do sistema</u> e, no <i>menu </i> desta tela, o comando de operação  <u>Alterar</u>;</font></li>
    </ul>
  </li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Em 
    ambos os casos, o sistema disponibiliza uma tela para o operador fazer a alteração 
    nos dados da instituição do sistema (figura 5).</font></li>
</ul>
<font face="Verdana, Arial, Helvetica, sans-serif">
