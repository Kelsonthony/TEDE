  Módulo Administração, opção instituição do sistema – alteração  dados da instituição 
  do sistema</em></font></p>
</font> 
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Faça 
    as alterações necessárias.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Clique 
    no botão de comando <u>Alterar</u>, existente no canto direito inferior da 
    janela.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Observe 
    que o sistema disponibiliza uma nova tela com a mensagem <u>Operação  Concluída</u> 
    e com os dados alterados para conferência (figura 6).</font></li>
</ul>
