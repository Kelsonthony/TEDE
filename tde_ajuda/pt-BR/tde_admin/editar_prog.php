<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">4.5.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Alterar</u> – Opção Programa de Pós-graduação</font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  ação Alterar pode ser realizada em dois momentos, logo após a inclusão, ou posteriormente.</font></p>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se o operador 
    for executar a ação logo após a inclusão, deve adotar o seguinte procedimento:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecionar, 
      na tela  com a mensagem de confirmação de inclusão (figura 22), o botão 
      de comando, existente na janela central da tela, abaixo do comando <u>Alterar.</u></font></li>
  </ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se o operador 
    for executar a ação posteriormente, deve adotar o seguinte procedimento:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecionar, 
      na tela principal do módulo (figura 11), a opção Programa de Pós-graduação 
      e, nesta tela, o botão de comando existente na janela central da tela, abaixo 
      do comando <u>Alterar</u>.</font></li>
  </ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Em 
    ambos os casos, o sistema disponibiliza uma tela para a alteração dos dados 
    (figura 23).</font></li>
</ul>
