    src="imagens/image023.jpg"></font></div>
<p align="center" class=MsoCaption style='text-align:justify'> <font size="2" face="Verdana, Arial, Helvetica, sans-serif"><em> 
  Figura 23 – Módulo Administração, Opção  Programa de Pós-graduação – Alteração 
  nos<span
style='font-style:normal'>  </span>dados do programa </em></font></p>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="text-align:justify" align="center">Alteração 
    dos dados dos campos  <u>Nome</u> e <u>Área</u>:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="
text-align:justify">efetue as alterações necessárias nos dados destes 
      campos.</font></li>
  </ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="text-align:justify">Alteração 
    no campo <i><u>Status</u> </i> do programa:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="
text-align:justify">este campo vem selecionado pelo sistema na opção 
      <u>Ativo</u>, e não pode ser alterado para a opção <u>Inativo</u>, quando 
      estiver vinculado a alguma TDE em processo de submissão;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">s</font><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="
text-align:justify">e alguma TDE vinculada ao programa de pós-graduação 
      está em processo de submissão, ao fazer a alteração e confirmá-la, o sistema 
      disponibiliza a tela    que estava sendo operada com a seguinte mensagem: 
      <u>ERRO 010 <span style='font-family:Symbol'>-</span> operação  não permitida! 
      Alguma TDE relacionada a este programa, está em processo de submissão;</u></font></li>
  </ul>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="
text-align:justify">neste caso, as alterações realizadas nos outros campos 
      não foram efetivadas, repetir o processo de alteração, sem alterar o <i>status.</i></font></li>
  </ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="text-align:justify">Se 
    a instituição receptora do sistema desativou o programa de pós-graduação, 
    adote o seguinte procedimento:</font> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="
text-align:justify">selecione, nesta tela, o <i>status</i> <u>Inativo</u>. 
        Neste caso, o programa fica inativo para o sistema TEDE.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="text-align:justify">Alteração 
    no campo <u>Biblioteca Depositária</u>:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="
text-align:justify">selecione – se for o caso <span style='font-family:Symbol'>-</span> 
      na janela ao lado do campo biblioteca depositária, a nova biblioteca que 
      deseja vincular ao programa;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="
text-align:justify">somente após realizar todas as alterações, clique 
      no botão de comando <u>Alterar</u>, canto inferior direito da janela;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" class="EstiloArial11ptJustificadoPrimeiralinha076cm" style="
text-align:justify">observe que o sistema disponibiliza a tela principal 
      da opção, com a mensagem <u>Operação  Concluída</u> e com os dados alterados 
      – ressaltados por uma faixa colorida – para conferência (figura 24). O resultado 
      da alteração no campo <i>Status</i> só aparece nesta tela  quando a opção 
      selecionada for <u>Inativo</u>;</font></li>
  </ul>
</ul>
