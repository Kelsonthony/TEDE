<h3><font size="2"><a name="_Toc69179197"><strong>3.1.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação  <u>Instituição do Sistema</u></strong></a></font></h3>
<p style='text-align:justify'><font size="2">A opção Instituição do Sistema é 
  utilizada para incluir e/ou alterar os dados que identificam a instituição receptora 
  do sistema TEDE responsável pela biblioteca digital na qual as TDEs são publicadas.</font></p>
<p style='text-align:justify'><font size="2">A  tela  principal desta opção é 
  identificada como Instituição do Sistema, no <i>menu </i> contendo os comandos  
   de operação  utilizados na opção e na janela central da tela, que contém os 
  dados identificadores da instituição receptora do sistema (figura 3).</font></p>
<p style='text-align:justify'><font size="2">O processo de operação da opção instituição 
  do sistema se dá logo após a identificação do <i>login</i> da conta e senha 
  pelo operador habilitado, com a apresentação imediata da tela    apropriada 
  para tal. Logo a seguir, sem que se utilize o <i>menu </i> da tela principal 
  do módulo, o sistema solicita a inclusão da biblioteca digital. Somente após 
  a finalização desta rotina é que o operador aciona o comando <i><u>menu </u></i><u>principal</u> 
  e retorna à tela principal do módulo para a operação das demais opções. </font></p>
