</div>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Confira os dados inseridos.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Se OK, adote o seguinte procedimento:</font> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecione, 
        no <i>menu </i> desta tela, o comando <i><u>menu</u></i><u> principal</u>, 
        para realizar o próximo passo, isto é, acionar a tela para o cadastramento 
        da biblioteca digital (veja seção 1.6).</font></li>
    </ul>
  </li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Se 
    não OK, adote o seguinte procedimento:</font></li> 
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">execute 
      a correção, utilizando as instruções contidas na seção <u>Comando de Operação 
      Alterar</u>, (seção 4.2.2).</font></li>
  </ul>
</ul>
