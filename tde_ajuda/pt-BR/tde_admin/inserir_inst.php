<h2><font size="3"><strong><font size="2">4.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como operar a opção <u>Instituição do Sistema</u></font></strong></font></h2>
</font> 
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Ao 
    acessar o Módulo Administração, o sistema disponibiliza uma tela com o comando 
    de operação  <u>Inserir</u> acionado, para a inclusão dos dados de identificação 
    da instituição receptora do Sistema TEDE (figura 3).</font></li>
</ul>
