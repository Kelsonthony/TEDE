<h3 style='text-align:justify'><font size="2"><a name="_Toc69179215"><strong>4.2.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Inserir</u> - Opção Instituição do Sistema</strong></a></font></h3>
</font> 
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Inclua, 
    nos campos apresentados na tela, os dados da instituição receptora do sistema. 
    Os campos de preenchimento obrigatório, ao deixarem de ser preenchidos, são 
    solicitados automaticamente pelo sistema, que emite uma mensagem de erro e 
    ressalta estes campos em vermelho:</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">digite, no campo <u>Nome</u>, 
    preenchimento obrigatório, <strong>em ordem direta</strong>, o nome da instituição 
    receptora do sistema. Este campo deverá conter, no máximo, 100 caracteres 
    alfanuméricos, também podem  ser usados os seguintes caracteres especiais: 
    &amp;, ( ),  /,  \ ,  - ,  _  e  * ;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">digite, 
    no campo <u>Sigla</u>, preenchimento obrigatório, <strong>a sigla oficial 
    </strong>da instituição receptora do sistema. Este campo deverá conter, no 
    máximo, 30 caracteres alfanuméricos, também podem ser usados os seguintes 
    caracteres especiais: &amp;, ( ),  /,  \ ,  - ,  _  e  * ;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecione,  no campo <u>País</u>, 
    preenchimento obrigatório, na tabela existente ao lado do campo, o país da 
    instituição receptora do sistema;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecione, no campo <u>UF</u>, 
    preenchimento obrigatório, a unidade da federação <span style='font-family:"Courier \(W1\)"'>-</span> 
    onde está sediada a instituição receptora do sistema <span style='font-family:"Courier \(W1\)"'>-</span> 
    na tabela existente ao lado do campo;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">digite, 
    no campo <u>CNPJ</u>, preenchimento obrigatório, o CNPJ da instituição receptora 
    do sistema. Este campo deverá ser preenchido apenas com a seqüência de algarismos 
    com,  no máximo, 14 caracteres numéricos. Ex.: 33654831002341;</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, campo <u>URL</u>, preenchimento obrigatório, a URL da 
  instituição receptora do sistema. EX.: <a href="http://www.qualquernome.gov.br/diretório">www.qualquernome.gov.br/diretório</a>. 
  Este campo deve conter, no máximo, 150 caracteres.</font></li><li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>clique no botão de comando <u>Inserir</u>, existente no canto 
  direito inferior da janela;</font></li><li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza uma tela com a mensagem <u>Operação 
   Concluída</u> e com os dados inseridos para conferência (figura 4).</font></li></ul>
