<h3><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179206"><strong>3.2.4<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Expiração Conta Autor</u></strong></a></font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Esta 
  opção é utilizada para alterar o período de vigência das contas dos autores 
  pelo  período definido pela instituição receptora. Inicialmente, o sistema atribui 
  um período padrão de três meses para o autor disponibilizar os arquivos da TDE 
  para a publicação eletrônica. A conta do autor é criada no módulo pós-graduação 
  após a inserção dos dados iniciais da TDE. O período de vigência da conta se 
  inicia a partir da data de criação da mesma. </font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  tela  principal da opção é identificada como Expiração Conta Autor, no <i>menu 
  </i> contendo os comandos   de operação  da opção e na janela central da tela, 
  que contém um conjunto de instruções para a alteração do prazo de expiração 
  da mesma (figura 55).</font></p>
