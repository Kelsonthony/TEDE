<h3 style='text-align:justify'><font size="2">3.1.4<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Programa de Pós-graduação</u></font></h3>
<p style='text-align:justify'><font size="2">A opção Programa de Pós-Graduação 
  é utilizada para cadastrar os dados que identificam os programas de pós-graduação 
  da instituição receptora do sistema responsáveis pelo processo de produção das 
  teses e dissertações publicadas eletronicamente.  </font></p>
<p style='text-align:justify'><font size="2">A tela principal da opção é identificada 
  como Programa<i> </i>de<i> </i>Pós-graduação,  no <i>menu </i> contendo os comandos  
   de operação   da opção e na janela central da tela,  que mostra  o conjunto 
  de dados cadastrados na opção (figura 19).</font></p>
<p style='text-align:justify'><font size="2">Ao ser cadastrado, o programa de 
  pós-graduação assume, para o sistema TEDE, o <i>status</i> de <u>Ativo</u>, 
  indicando que é um programa responsável por publicação eletrônica de teses ou 
  dissertações. Se, por alguma razão, um programa de pós-graduação vier a ser 
  desativado pela instituição receptora do sistema, deverá também ser desativado 
  para o sistema TEDE, desde que não tenha alguma TDE em processo de publicação 
  eletrônica.</font></p>
<p style='text-align:justify'><font size="2">Nesta opção, é feita a seleção da 
  biblioteca depositária das teses e dissertações produzidas no programa de pós-graduação. 
  Quando ocorre esta seleção, cria-se um vínculo entre o programa e a biblioteca 
  depositária escolhida. Este vínculo só poderá ser desfeito, com a indicação 
  de uma nova biblioteca depositária das teses ou dissertações do programa de 
  pós-graduação. Para cada programa só poderá ser associada uma biblioteca depositária.  
  </font></p>
