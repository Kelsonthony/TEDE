<h3 style='text-align:justify'><font size="2">4.9.2<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Alterar</u> – Opção Conta Administração</font></h3>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><font size="2"><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Para realizar alteração nos dados da conta administração, adote 
  os seguintes procedimentos:</font></p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><font size="2"><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>identifique, na tela principal da opção Conta Administração (figura 
  48), ou na tela    resultante da ação realizada (figura 50), a conta cujos dados 
  deverão ser alterados;</font></p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><font size="2"><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>clique no botão de comando <span style='font-family:"Courier \(W1\)"'>-</span> 
  posicionado abaixo do comando <u>Alterar</u>, existente na janela central da 
  tela <span style='font-family:"Courier \(W1\)"'>-</span> correspondente ao nome 
  do <i>login </i>em que será realizada a alteração;</font></p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><font size="2"><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza a tela com os dados para as 
  alterações necessárias (figura 51).</font></p>
<font size="2"><img width=599 height=192
    src="imagens/image051.jpg"><br clear=ALL>
<p class=MsoCaption style='text-align:justify'>    Figura 51 – Módulo Administração, 
  opção Conta Administração – Alteração dados da conta administração<span
style='font-style:normal'>  </span></p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Alteração dos dados dos campos <u>Nome</u>, <i><u>Login</u></i><u>,</u> 
  <u>Senha</u>, <u>Confirmar Senh</u>a e <i><u>E-mail</u></i>:</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>efetue as alterações necessárias nos dados destes campos.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Alteração no campo <i><u>Status</u></i>:</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>este campo vem selecionado pelo sistema na opção <u>Bloqueado 
  Não</u>, indicando que a conta está disponível para o usurário;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>para bloquear a conta, selecione, no campo <i>Status,</i> a opção 
  <u>Bloqueado Sim</u>;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>após realizar todas as alterações, clique no botão de comando 
  <u>Alterar</u>, canto inferior direito da janela;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza a tela principal da opção 
  com a mensagem <u>Operação  Concluída</u> e com os dados alterados – ressaltados 
  por uma faixa colorida – para conferência (figura 52). </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify;'><b>Atenção</b>: o resultado da alteração no campo <i>Status,</i> 
  representada pelo símbolo de um cadeado, só aparece nesta tela quando a opção 
  selecionada for <u>Bloqueado Sim</u>.</p>
