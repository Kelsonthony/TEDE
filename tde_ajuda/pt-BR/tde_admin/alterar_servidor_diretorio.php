<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179211"><strong>3.3.4<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Servidor de Arquivos</u></strong></a></font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
  sistema TEDE é distribuído para as instituições receptoras com o endereço padrão 
  de servidor de arquivos do sistema <span style='font-size:10.0pt'>(</span><span style='letter-spacing:2.6pt'>servidor.com.br/tede/</span>). 
  Esta opção é utilizada para <u>alterar</u> este endereço padrão pelo endereço 
  do servidor de arquivos da instituição receptora. Este endereço tem a finalidade 
  de informar ao sistema em que URL o sistema está armazenado. Com isso, todos 
  os <i>links</i> contidos nos <i>e-mails</i> de comunicação que são gerados pelo 
  sistema serão montados a partir deste item.</font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  tela  principal da opção é identificada como Servidor de Arquivos, no <i>menu 
  </i> contendo os comandos   de operação  da opção e na janela central da tela,  
  que contém um conjunto de instruções para a troca do servidor de arquivos ( 
  figura 67 ).</font></p>
