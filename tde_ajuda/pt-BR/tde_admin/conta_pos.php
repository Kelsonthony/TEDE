<h3><a name="_Toc69179233">4.7.1<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Inserir</u> – Opção Conta Pós-graduação</a></h3>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Selecione, no <i>menu </i> desta tela, o comando de operação  <u>Inserir.</u></p>
<img width=590 height=193
    src="imagens/image035.jpg"><br clear=ALL>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>O sistema disponibiliza uma tela  para a inclusão dos dados do 
  operador habilitado para a conta pós-graduação (figura 35).</p>
<p class=MsoCaption><a name="_Ref52352662"></a><a name="_Hlt56223348"></a>        
  <a name="_Ref66700956">Figura </a>35 – Módulo Administração,  opção Conta Pós- 
  graduação – Inclusão  dados da conta </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Inclua, nesta tela, nos campos apropriados, os dados do operador 
  habilitado da conta pós-graduação. Os campos de preenchimento obrigatório, ao 
  deixarem de ser preenchidos, são solicitados automaticamente pelo sistema, que 
  emite uma mensagem de erro e ressalta estes campos em vermelho:</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>selecione, no campo <u>Programa</u>, preenchimento obrigatório, 
  na janela ao lado do campo, o programa de pós-graduação ao qual a conta ficará 
  vinculada;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <u>Nome</u>, preenchimento obrigatório, o nome 
  do operador habilitado para o qual está sendo criada a conta. Este campo deverá 
  conter, no máximo, 100 caracteres alfabéticos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <i><u>Login</u></i>, preenchimento obrigatório, 
  o <i>login</i> criado para o operador habilitado. Este campo pode conter qualquer 
  palavra com, no máximo, 50 caracteres alfanuméricos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <u>Senha</u>, preenchimento obrigatório, a senha 
  de acesso criada para o operador habilitado. Este campo pode conter até, no 
  máximo, 30 caracteres alfanuméricos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <u>Confirmar Senha</u>, preenchimento obrigatório, 
  novamente a senha de acesso criada para o operador habilitado;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <i><u>E-mail</u></i>, preenchimento obrigatório, 
  o <i>e-mail</i> do operador habilitado. Este campo pode conter, no máximo, 100 
  caracteres no formato utilizado para o padrão <i>e-mail.</i>  Ex.: <a
href="mailto:nome@nome.com.br;">nome@nome.com.br;</a></p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>para que o operador habilitado cadastrado tenha acesso ao módulo 
  que vai operar, mantenha o campo <u>Bloquear Conta</u>  com a escolha <u>Não</u>  
  acionada; </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>clique no botão de comando <u>Inserir</u>, existente no canto 
  direito inferior da janela;<br clear=ALL>
</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza uma nova tela com a mensagem 
  <u>Operação  Concluída</u> e com os dados inseridos – ressaltados por uma faixa 
  colorida – para conferência (figura 36). O resultado da inclusão no campo <i><u>Status</u></i> 
  só aparece nesta tela  quando a opção selecionada  for <u>Bloqueado Sim</u>, 
  que é representada pelo símbolo de um cadeado.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><img width=594 height=213
    src="imagens/image036.jpg"></p>
<p class=MsoCaption style='text-align:justify'>  Figura 36 – Módulo Administração, 
  opção Conta Pós-graduação  - Mensagem confirmação de inclusão<span
style='font-style:normal'> </span></p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Em caso de alteração, utilize as instruções contidas na seção 
  Comando de Operação Alterar, descritas a seguir (seção 4.7.2).</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Para a inclusão de uma nova Conta de Pós-graduação, adote os seguintes 
  procedimentos:</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>selecione, nesta tela (figura 36), o comando <u>Inserir</u>;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza uma tela  para a inclusão 
  da nova conta ( figura 35 );  </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>execute os mesmos procedimentos utilizados para a primeira inclusão 
  dos dados e para as alterações, até concluir o processo.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Ao finalizar a operação da opção, selecione, no <i>menu </i> desta 
  tela, o comando <i><u>menu </u></i><u> principal</u> e volte à tela  principal 
  do módulo Administração, para dar seqüência às demais opções.</p>
