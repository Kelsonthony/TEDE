<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Se 
    a biblioteca foi desativada, selecione, nesta tela, o comando <i><u>menu </u></i><u>principal</u> 
    e volte à tela principal do Módulo Administração, para ativar a opção de operação 
     Programa de Pós-graduação e substituir <span
style='font-family:Symbol'>-</span> se for o caso <span style='font-family:
Symbol'>-</span>  a biblioteca depositária vinculada ao programa ( ver seção 1.8.2 
    ). </font></li>
  <li><font size="2"><span style="font-family: Symbol; font-size: 10.0pt;">A</span><font face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">pós 
    realizar as alterações nos dados da biblioteca depositária, clique no botão 
    de comando <u>Alterar</u>, canto inferior direito da janela.</font></font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O</font><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">bserve 
    que o sistema disponibiliza uma tela com a mensagem <u>Operação Concluída</u> 
    e com os dados – ressaltados por uma faixa colorida <span
style='font-family:"Courier New"'>-</span> para conferência (figura 18). O resultado 
    da alteração no campo <i><u>Status</u></i> só aparece nesta tela quando for 
    selecionada a opção <u>Inativa;</u></font></li>
</ul>
