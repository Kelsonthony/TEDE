<h2><font face="Verdana, Arial, Helvetica, sans-serif"><font size="2">4.4<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Como operar a opção <u>Biblioteca Depositária</u></font></font></h2>
<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179222">4.4.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Alterar</u> – Opção Biblioteca Depositária</a></font></h3>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A ação Alterar 
  pode ser feita em dois momentos: logo após a inclusão, ou posteriormente.</font></p>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se o operador 
    for executar a ação logo após a inclusão, deve adotar o seguinte procedimento:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecionar, 
      na tela com a mensagem de confirmação de inclusão (figura 15), o botão de 
      comando, existente na janela central da tela, abaixo do comando <u>Alterar.</u></font></li>
  </ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se o operador 
    for executar a ação posteriormente, deve adotar o seguinte procedimento:</font> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">sel</font><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">ecionar, 
        no <i>menu </i> da tela principal do módulo ( figura 11 ), a opção Biblioteca 
        Depositária e, nesta tela, o botão de comando existente na janela central 
        da tela, abaixo do comando <u>Alterar</u>.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Em 
    ambos os casos, o sistema disponibiliza uma tela    para a alteração dos dados 
    (figura 16).</font></li>
</ul>
<div align="center"><font size="2"><img width=584 height=181
    src="imagens/image016.jpg"><br clear=ALL>
  <font face="Verdana, Arial, Helvetica, sans-serif">       <em> Figura 16 - Módulo 
  Administração, opção biblioteca depositária - Dados para alteração</em></font></font></div>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Alteração 
    nos dados dos campos <u>Nome</u>, <u>Sigla</u> e <u>URL</u>:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">efetue 
      as alterações necessárias nos dados destes campos.</font></li>
  </ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Alteração 
    no campo <i><u>Status</u></i> da biblioteca depositária:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">este 
      campo vem selecionado pelo sistema na opção <u>Ativa</u>, que não pode ser 
      alterada para a opção <u>Inativa</u>, quando a biblioteca depositária estiver 
      vinculada a um programa de pós-graduação;</font></li>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">após 
      selecionar a opção <u>Inativa</u>, se a biblioteca estiver vinculada a algum 
      programa de pós-graduação e após confirmar a alteração, o sistema disponibiliza 
      esta mesma tela    com a seguinte mensagem: <u>Operação  não permitida, 
      algum programa está relacionado a esta biblioteca</u> (figura 17). Neste 
      caso, as alterações realizadas nos outros campos não foram efetivadas, repetir 
      o processo de alteração, sem alterar o <i>status</i>;</font></li>
  </ul>
</ul>
