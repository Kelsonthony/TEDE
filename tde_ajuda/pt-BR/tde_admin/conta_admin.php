<h3 style='text-align:justify'><a name="_Toc69179241">4.9.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Inserir</u> – Opção Conta Administração</a></h3>
<p style='text-align:justify;'><b>Atenção</b>: o <i><u>Login</u></i><u> da Conta</u> 
  (admin) e a <u>senha</u> (admin), que acompanham o sistema, são valores atribuídos 
  pelo sistema e distribuídos em todas as cópias e, por medida de segurança, devem 
  ser excluídos após o cadastramento da primeira conta administração.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Selecione, no <i>menu </i> desta tela (figura 48), o comando de 
  operação  <u>Inserir</u>.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>O sistema disponibiliza uma nova tela  para a inclusão dos dados 
  do usuário da Conta Administração (figura 49).</p>
<img width=588 height=202
    src="imagens/image049.jpg"><br clear=ALL>
<p class=MsoCaption style='text-align:justify'><a name="_Ref52354552"></a><a
name="_Hlt56224318"></a>   <a name="_Ref68079259">Figura </a>49 – Módulo Administração, 
  opção Conta Administração - Inclusão<span style='font-style:normal'> </span>dados 
  da conta administração</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
style='font-size:10.0pt;font-family:Symbol;color:black'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Inclua, nesta tela, nos campos apropriados, os dados do operador 
  habilitado para a conta administração:</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <u>Nome</u>, preenchimento obrigatório, o nome 
  do operador habilitado para o qual está sendo criada a conta. Este campo deverá 
  conter, no máximo, 100 caracteres alfabéticos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <i><u>Login</u></i><u>,</u> preenchimento obrigatório, 
  o <i>login</i> criado para o operador habilitado. Este campo pode conter qualquer 
  palavra com, no máximo, 50 caracteres alfanuméricos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <u>Senha</u>, preenchimento obrigatório, a senha 
  de acesso criada para o operador habilitado. Este campo pode conter, no máximo, 
  30 caracteres alfanuméricos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <u>Confirmar Senha</u>, preenchimento obrigatório, 
  novamente a mesma senha de acesso criada para o operador habilitado. Este campo 
  pode conter, no máximo, 30 caracteres alfanuméricos;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>digite, no campo <i><u>E-mail</u></i>, preenchimento obrigatório,  
  o <i>e-mail</i> do operador habilitado. Este campo pode conter, no máximo, 100 
  caracteres no formato utilizado para o padrão <i>e-mail.</i> Ex.: nome@nome.com.br;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>para que o operador habilitado cadastrado tenha acesso ao módulo 
  que vai operar, mantenha o campo <i>Status</i> com a opção <u>Bloquear Não</u> 
  acionada; </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>clique no botão de comando <u>Inserir</u>, existente no canto 
  direito inferior da janela;</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify'><span style='font-size:10.0pt;font-family:Symbol;
color:black'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>observe que o sistema disponibiliza uma tela com a mensagem <u>Operação 
   Concluída</u> e com os dados inseridos – ressaltados por uma faixa colorida 
  – para conferência (figura 50). </p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='
text-align:justify;'><b>Atenção</b>: o resultado da inclusão no campo <i>Status, 
  </i>representada pelo símbolo de um cadeado, só aparece nesta tela  quando a 
  opção selecionada for <u>Bloqueado Sim</u> . </p>
<img width=594
  height=221 src="imagens/image050.jpg"><br clear=ALL>
<p class=MsoCaption style='text-align:justify'>    Figura 50 – Módulo Administração, 
  opção Conta Administração – Mensagem de confirmação inclusão<span
