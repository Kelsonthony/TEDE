<h3 style='text-align:justify'><a name="_Toc69179230"><font size="2">4.6.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação  <u>Alterar</u>  –  Opção  Contato</font></a></h3>
<p style='text-align:justify'><font size="2">A ação  <u>Alterar</u> pode ser realizada 
   em dois momentos, logo após a inclusão, ou posteriormente.</font></p>
<p style='text-align:justify;'><font size="2"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Se o operador for executar a ação logo após a inclusão, deve adotar 
  o seguinte procedimento:</font></p>
<p style='text-align:justify;'><font size="2"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>selecionar, no <i>menu </i> da tela com a mensagem de confirmação 
  de inclusão (figura 28), o botão de comando existente na janela central da tela, 
  abaixo do comando <u>Alterar</u>.</font></p>
<p><font size="2"><span
style='font-size:10.0pt;font-family:Symbol'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Se o operador for executar a ação posteriormente, deve adotar 
  o seguinte procedimento:</font></p>
<p style='text-align:justify;'><font size="2"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>selecionar, na tela principal do módulo (figura 11), a opção Contato 
  e, nesta tela, o botão de comando existente na janela da tela  central, abaixo 
  do comando <u>Alterar</u>.</font></p>
<p style='text-align:justify;'><font size="2"><span style='font-size:10.0pt;font-family:Symbol'>·<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span></span>Em ambos os casos, o sistema disponibiliza uma tela para a alteração 
  dos dados (figura 29).</font></p>
