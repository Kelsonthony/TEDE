  </span></span>para tornar o contato indisponível (ausência temporária do contato 
  ou outro motivo), selecione a opção de <i>status</i> <u>Disponível Não</u>.</p>
<p class=EstiloArial11ptJustificadoPrimeiralinha076cm style='text-align:justify'><span
