<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179209"><strong>3.3.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <i><u>Layout</u></i></strong></a></font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
  sistema TEDE é distribuído com três opções de <i>layout</i> para a escolha, 
  pela instituição receptora do sistema, do <i>layout</i> desejado. Esta escolha 
  deve ser feita utilizando-se esta opção de operação. Caso a instituição queira 
  desenvolver novos <i>layouts</i>, o sistema possui, em sua árvore de diretórios, 
  pastas específicas para teste de <i>layout</i>, com <i>templates e css</i> para 
  teste. </font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  tela  principal da opção é identificada como<i> Layout</i>, no <i>menu </i> contendo 
  os comandos   de operação  da opção e na janela central da tela, que contém 
  um conjunto de instruções para a troca do <i>layout</i> do sistema ( figura 
  63 ).</font></p>
