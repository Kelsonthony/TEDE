<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179208">3.3.1<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Opção de Operação <u>Logomarca</u></a></font></h3>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">O 
  sistema TEDE é distribuído com três logomarcas, que são padrão do sistema: <i><span style='font-size:10.0pt'>LOGO 
  DO SISTEMA</span></i><span style='font-size:10.0pt'> </span>(localizada no canto 
  superior esquerdo da tela<span style='font-size:10.0pt'>), <i>LOGO 1 e LOGO 
  2</i> </span>(localizadas no rodapé da tela)<span style='font-size:10.0pt'>.</span> 
  A instituição receptora do sistema deverá fazer a troca destas logomarcas por 
  outras de sua escolha, de modo que possam refletir as suas características. 
  Sugere-se a troca da <i>LOGO DO SISTEMA</i> pela logomarca do sistema de teses 
  de dissertações local; da <i>LOGO 1,</i> pela logomarca da instituição receptora 
  do sistema, e da <i>LOGO 2</i>, por qualquer outra  logomarca que reflita as 
  características da instituição.</font></p>
<p style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A 
  tela principal da opção é identificada como Logomarca, no <i>menu </i> contendo 
  os comandos   de operação  da opção e na janela central da tela,  que contém 
  um conjunto instruções que permitem a troca das logomarcas do sistema. Nesta 
  tela, o sistema disponibiliza campos para busca dos arquivos contendo as imagens 
  das logomarcas, para a troca de <i>link</i> da logomarca e do texto do <i>ALT</i> 
  da imagem (figura 57).</font></p>
<h2><font face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179246"><font size="2">4.11<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp; 
