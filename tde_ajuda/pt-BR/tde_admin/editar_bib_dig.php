  </span>Como operar a opção <u>Biblioteca Digital</u></font></a></h2>
</font> 
<h3 style='text-align:justify'><font size="2" face="Verdana, Arial, Helvetica, sans-serif"><a name="_Toc69179219"><strong>4.3.2<span
style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 
  </span>Comando de Operação <u>Alterar </u>– Opção Biblioteca Digital</strong></a></font></h3>
<p><font size="2" face="Verdana, Arial, Helvetica, sans-serif">A ação Alterar 
  pode ser feita em dois momentos, logo após a inclusão, ou posteriormente.</font></p>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Se 
    o operador for executar a ação <strong>logo após a inclusão</strong>, deve 
    adotar o seguinte procedimento:</font></li>
  <ul>
    <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecionar, 
      na tela com a mensagem de confirmação de inclusão (figura 8), o comando 
      de operação  <u>Alterar</u>.</font></li>
  </ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif">Se o operador 
    for executar a ação posteriormente, deve adotar o seguinte procedimento:</font> 
    <ul>
      <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">selecionar, 
        no <i>menu </i> da tela principal do módulo ( figura 11 ), a opção Biblioteca 
        Digital e, nesta tela,  selecionar o comando de operação  <u>Alterar</u>.</font></li>
    </ul>
  </li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Em 
    ambos os casos, o sistema disponibiliza uma tela para o operador realizar 
    a alteração dos dados (figura 9).</font></li>
</ul>
<div align="center"><font size="2"><img width=586 height=166
    src="imagens/image009.jpg"><br clear=ALL>
  <font face="Verdana, Arial, Helvetica, sans-serif"><em>Figura 9 – Módulo Administração, 
  opção biblioteca digital -  Alteração  nos dados da biblioteca virtual</em></font></font></div>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Faça 
    as alterações necessárias nos dados inseridos.</font></li>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Clique 
    no botão de comando <u>Alterar</u>, existente no canto direito inferior da 
    janela.</font></li>
</ul>
<ul>
  <li><font size="2" face="Verdana, Arial, Helvetica, sans-serif" style="text-align:justify;">Observe 
    que o sistema disponibiliza uma tela com a mensagem <u>Operação  Concluída</u>  
    e com os dados alterados  para conferência (figura 10).</font></li>
</ul>
